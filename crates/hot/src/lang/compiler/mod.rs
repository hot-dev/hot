    /// Walk the program for var aliases (`alias-name ::ns/target`,
    /// `alias-name local-target`, optionally meta-bearing) and copy
    /// the target's `function_mapping` entries (arity keys + typed
    /// signatures) under the alias's qualified name.
    ///
    /// Why this is needed: the worker's routing path
    /// (`bytecode_has_function`) decides which build contains a
    /// scheduled / event-handler / MCP / webhook function purely by
    /// looking the alias's qualified name up in `function_mapping`.
    /// Aliases compile to a `FunctionAlias` value (not a `Function`),
    /// so without this pass they never appear in the mapping and the
    /// worker reports "Function ... not found in any deployed build"
    /// for any artifact registered through an alias.
    ///
    /// Aliases that don't resolve to a known function (e.g. type
    /// aliases or refs to non-function values) are skipped silently —
    /// they don't belong in `function_mapping`.
    fn register_alias_function_mappings(&mut self, program: &crate::lang::ast::Program) {
        use crate::lang::ast::{Ref, Value};

        // Collect first to avoid mutable/immutable borrow conflicts on `self`.
        let mut new_entries: Vec<(String, FunctionId)> = Vec::new();

        for (ns_path, namespace) in &program.namespaces {
            let ns_str = ns_path.to_string();
            for (var, value) in &namespace.scope.vars {
                if !matches!(value, Value::Ref(Ref::Ns(_)) | Value::Ref(Ref::Var(_))) {
                    continue;
                }

                let Some(target_qualified) =
                    crate::lang::compiler::artifact_extraction::resolve_alias_target_qualified_name(
                        program, &ns_str, value,
                    )
                else {
                    continue;
                };

                let alias_qualified = format!("{}/{}", ns_str, var.sym.name());

                // Skip if the target couldn't possibly be in our mapping
                // (defensive — `resolve_alias_target_qualified_name` already
                // requires the target to be `Value::Fn`/`Value::Lambda`).
                let target_prefix = format!("{}/", target_qualified);
                let alias_prefix = format!("{}/", alias_qualified);

                // Copy every arity / signature entry the target owns.
                // Mapping keys look like:
                //   ::ns/fn/N             (arity-only)
                //   ::ns/fn/N:T1,T2,...   (typed signature)
                // We splice the alias prefix in place of the target's so
                // routing can match either name.
                for (key, fn_id) in self.function_mapping.iter() {
                    if let Some(suffix) = key.strip_prefix(&target_prefix) {
                        let new_key = format!("{}{}", alias_prefix, suffix);
                        if !self.function_mapping.contains_key(&new_key) {
                            new_entries.push((new_key, *fn_id));
                        }
                    }
                }
            }
        }

        for (key, fn_id) in new_entries {
            tracing::trace!("Compiler: Registering alias mapping '{}'", key);
            self.function_mapping.insert(key, fn_id);
        }
    }

