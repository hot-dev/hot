    /// Add a source file for error reporting (without setting it as current)
    pub fn add_source_file(&mut self, file_path: std::path::PathBuf, content: String) {
        self.file_contents
            .insert(file_path.clone(), content.clone());

        // Extract namespace from file path and create mapping
        // e.g., "demo/schedule.hot" -> "::demo::schedule"
        if let Some(file_str) = file_path.to_str() {
            // Add file content to source map for pretty error reporting
            self.program
                .source_map
                .add_file_content(file_str.to_string(), content.clone());

            // Extract namespace from file path
            let namespace = format!(
                "::{}",
                file_str.trim_end_matches(".hot").replace(['/', '\\'], "::")
            );

            tracing::trace!(
                "Compiler: Mapping namespace '{}' to file '{}'",
                namespace,
                file_str
            );
            self.namespace_to_file.insert(namespace, file_path.clone());
        }
    }

    /// Set the current file being compiled for error reporting
    pub fn set_current_file(&mut self, file_path: std::path::PathBuf, content: String) {
        self.current_file = Some(file_path.clone());
        self.add_source_file(file_path, content);
    }

    /// Set the current source location for instruction tracking
    pub fn set_source_location(
        &mut self,
        line: usize,
        column: usize,
        position: usize,
        length: usize,
    ) {
        self.current_source_location = Some(crate::lang::bytecode::SourceLocation {
            file: self
                .current_file
                .as_ref()
                .and_then(|p| p.to_str())
                .map(|s| s.to_string()),
            line,
            column,
            position,
            length,
        });
    }

    /// Clear the current source location
    pub fn clear_source_location(&mut self) {
        self.current_source_location = None;
    }

    /// Set source location from a Var's source info
    fn set_source_location_from_var(&mut self, var: &crate::lang::ast::Var) {
        if let Some(ref src) = var.src {
            self.set_source_location(src.line, src.column, src.position, src.length);
        } else {
            // Fallback: use a minimal source location with just the variable name length
            self.set_source_location(1, 1, 0, var.sym.name().len());
        }
    }

    /// Convert an AST Source to a bytecode SourceLocation
    fn source_to_location(
        &self,
        src: &Option<crate::lang::ast::Source>,
    ) -> Option<crate::lang::bytecode::SourceLocation> {
        src.as_ref().map(|s| crate::lang::bytecode::SourceLocation {
            file: s.file.clone().or_else(|| {
                self.current_file
                    .as_ref()
                    .and_then(|p| p.to_str())
                    .map(|s| s.to_string())
            }),
            line: s.line,
            column: s.column,
            position: s.position,
            length: s.length,
        })
    }

    /// Handle ns declarations: switch namespace and set ns variable
    fn handle_ns_declaration(&mut self, value: &Value) -> Result<RegisterId, String> {
        // Extract the namespace reference from the value
        if let Value::Ref(Ref::Ns(ns_ref)) = value {
            // Construct the new namespace path
            let namespace_parts: Vec<String> = ns_ref
                .ns
                .0
                .iter()
                .map(|ns_part| match ns_part {
                    crate::lang::ast::NsPathPart::Sym(sym) => match sym {
                        crate::lang::ast::Sym::String(s) => s.clone(),
                    },
                })
                .collect();

            let new_namespace = format!("::{}", namespace_parts.join("::"));

            tracing::trace!(
                "Compiler: ns declaration switching namespace from '{}' to '{}'",
                self.current_namespace,
                new_namespace
            );

            // Switch the current namespace
            self.current_namespace = new_namespace.clone();

            // Add namespace to registry if not already present
            self.program
                .namespaces
                .add_namespace(self.current_namespace.clone());

            // Generate SetNamespace instruction to switch VM's namespace at runtime
            let namespace_constant = self
                .program
                .add_constant(Constant::Val(Val::from(new_namespace.clone())));

            self.emit_instruction(Instruction::SetNamespace {
                namespace: namespace_constant,
            });

            // Create the ns variable in the new namespace with a proper NamespaceRef as its value
            // This creates a boxed NamespaceRef that Hot code can access as a proper Namespace type
            let reg = self.allocate_register();
            let namespace_ref = crate::lang::refs::namespace_ref(new_namespace.clone());
            let ns_value_constant = self.program.add_constant(Constant::Val(namespace_ref));

            self.emit_instruction(Instruction::LoadConst {
                dest: reg,
                constant: ns_value_constant,
            });

            // Store this in the namespace variables so Hot code can access it via ns
            let ns_var_name_constant = self.program.add_constant(Constant::Val(Val::from("ns")));

            self.emit_instruction(Instruction::StoreVar {
                var_name: ns_var_name_constant,
                value: reg,
                metadata: None, // Namespace metadata is not emitted here yet.
            });

            tracing::trace!(
                "Compiler: Set ns variable in namespace '{}' to '{}'",
                self.current_namespace,
                new_namespace
            );

            Ok(reg)
        } else {
            Err(format!(
                "ns declaration must be a namespace reference, got: {:?}",
                value
            ))
        }
    }

    /// Extract lazy parameter info from all function definitions before bytecode compilation.
    /// This allows call sites to determine which arguments should be wrapped as lazy thunks
    /// even when the target function hasn't been compiled yet.
    fn extract_lazy_params(&mut self, program: &Program) {
        for (ns_path, namespace) in &program.namespaces {
            let namespace_str = ns_path.to_string();

            for (var, value) in &namespace.scope.vars {
                // Check if this is a function definition
                if let Value::Fn(fn_defs) = value {
                    let func_name = var.sym.name();
                    let qualified_name = format!("{}/{}", namespace_str, func_name);
                    let is_core = Self::is_core_var(var);

                    // Process each arity variant of the function
                    for fn_def in fn_defs {
                        // Extract lazy params and variadic info from the function definition
                        let mut lazy_params: Vec<bool> =
                            fn_def.args.args.iter().map(|a| a.lazy).collect();
                        let is_variadic = fn_def.args.variadic;

                        // For `fn match` with Result patterns, mark the first param as lazy
                        // so Result.Err values survive auto-unwrapping at call time.
                        if let Value::Flow(flow) = &fn_def.body {
                            let is_match_flow = matches!(
                                flow.flow_type,
                                crate::lang::ast::FlowType::Match
                                    | crate::lang::ast::FlowType::MatchShort
                                    | crate::lang::ast::FlowType::MatchAll
                                    | crate::lang::ast::FlowType::MatchAllShort
                            );
                            if is_match_flow && !lazy_params.is_empty() {
                                let has_result_arms = flow.expressions.iter().any(|expr| {
                                    if let Value::MatchArm(arm) = expr {
                                        arm.type_name.as_deref() == Some("Result")
                                    } else {
                                        false
                                    }
                                });
                                if has_result_arms {
                                    lazy_params[0] = true;
                                }
                            }
                        }

                        // Only store if there are any lazy params
                        if lazy_params.iter().any(|&is_lazy| is_lazy) {
                            tracing::trace!(
                                "Compiler: Pre-extracted lazy params for '{}': {:?}, variadic={}",
                                qualified_name,
                                lazy_params,
                                is_variadic
                            );
                            self.lazy_params_registry
                                .insert(qualified_name.clone(), (lazy_params.clone(), is_variadic));

                            // Also store with arity suffix for arity-based lookup
                            let arity_name =
                                format!("{}/{}", qualified_name, fn_def.args.args.len());
                            self.lazy_params_registry
                                .insert(arity_name, (lazy_params, is_variadic));

                            // For core functions, record the unqualified -> qualified mapping
                            // so get_function_lazy_params can resolve unqualified names
                            if is_core {
                                self.core_lazy_function_names
                                    .insert(func_name.to_string(), qualified_name.clone());
                                tracing::trace!(
                                    "Compiler: Mapped core lazy function '{}' -> '{}'",
                                    func_name,
                                    qualified_name
                                );
                            }
                        }
                    }
                }
            }
        }
        tracing::debug!(
            "Compiler: Extracted lazy params for {} functions",
            self.lazy_params_registry.len()
        );
    }

    /// Extract all known function names from the program before bytecode compilation.
    /// This allows the compiler to distinguish between function references (which should
    /// create FunctionAlias structures) and type/variable references at compile time,
    /// regardless of compilation order.
    ///
    /// This extracts:
    /// - Built-in (hotlib) functions like `::hot::coll/map`
    /// - User-defined functions using `fn`
    /// - Type constructor functions using `type fn` like `::hot::type/Str`
    /// - Enum variant constructors like `::hot::type/Result.Ok`
    fn extract_known_functions(&mut self, program: &Program) {
        // First, add all built-in (hotlib) functions
        let hotlib_map = crate::lang::hot::get_hotlib_map();
        for func_name in hotlib_map.keys() {
            self.known_functions.insert(func_name.clone());
        }

        let mut user_fn_count = 0;
        let mut type_constructor_count = 0;
        let mut variant_count = 0;

        // Then scan all namespaces for user-defined functions and type constructors
        for (ns_path, namespace) in &program.namespaces {
            let namespace_str = ns_path.to_string();

            for (var, value) in &namespace.scope.vars {
                match value {
                    // Regular function definitions using `fn`
                    Value::Fn(fn_defs) => {
                        let func_name = var.sym.name();
                        let qualified_name = format!("{}/{}", namespace_str, func_name);

                        // Add the qualified function name
                        self.known_functions.insert(qualified_name.clone());
                        user_fn_count += 1;

                        // Also add arity-specific variants for each overload
                        for fn_def in fn_defs {
                            let arity_name =
                                format!("{}/{}", qualified_name, fn_def.args.args.len());
                            self.known_functions.insert(arity_name);
                        }
                    }

                    // Type definitions - may contain constructor functions, enum variants,
                    // or just fields (record types with implicit default constructors)
                    Value::TypeDef(type_def) => {
                        let type_name = var.sym.name();
                        let qualified_type_name = format!("{}/{}", namespace_str, type_name);

                        // Type constructor functions (defined with `type fn`)
                        if let Some(constructor_fns) = &type_def.constructor_functions {
                            // The type name itself acts as a function when it has constructors
                            self.known_functions.insert(qualified_type_name.clone());
                            type_constructor_count += 1;

                            // Also add arity-specific variants
                            for fn_def in constructor_fns {
                                let arity_name =
                                    format!("{}/{}", qualified_type_name, fn_def.args.args.len());
                                self.known_functions.insert(arity_name);
                            }
                        } else if type_def.fields.is_some() {
                            // Record types with fields have implicit default constructors
                            // These can be used as functions: map(items, MyRecordType)
                            self.known_functions.insert(qualified_type_name.clone());
                            type_constructor_count += 1;

                            // Default constructor takes 1 argument (the map/value to construct from)
                            let arity_name = format!("{}/1", qualified_type_name);
                            self.known_functions.insert(arity_name);
                        }

                        // Enum variant constructors (defined with `enum { Ok(Any), Err(Any) }`)
                        if let Some(variants) = &type_def.variants {
                            for variant in variants {
                                // Variant constructors are named like `Type.Variant`
                                let variant_name = match &variant.name {
                                    crate::lang::ast::Sym::String(s) => s.clone(),
                                };
                                let qualified_variant =
                                    format!("{}.{}", qualified_type_name, variant_name);
                                self.known_functions.insert(qualified_variant);
                                variant_count += 1;
                            }
                        }
                    }

                    _ => {}
                }
            }
        }

        tracing::debug!(
            "Compiler: Extracted {} known function names ({} built-ins, {} user fns, {} type constructors, {} enum variants)",
            self.known_functions.len(),
            hotlib_map.len(),
            user_fn_count,
            type_constructor_count,
            variant_count
        );
    }

    /// Check if a qualified name is a known function (either built-in or user-defined)
    fn is_known_function(&self, qualified_name: &str) -> bool {
        self.known_functions.contains(qualified_name)
    }

    /// Compile a complete Hot program to bytecode.
    ///
    /// This is the canonical compile entry point. It runs the full mandatory
    /// pipeline:
    ///
    /// * Phase 0   — core variable extraction
    /// * Phase 0.5 — placeholder (`%`) lowering into lambdas
    /// * Phase 1   — name resolution
    /// * Phase 2   — type checking
    /// * Phase 3   — bytecode generation
    ///
    /// Takes the program by `&mut Program` because Phase 0.5 rewrites the AST
    /// in place, replacing bare `Value::Placeholder` nodes with proper
    /// lambdas at the appropriate `Fn`-typed parameter slots.
    ///
    /// All production callers should use this. The narrow escape hatch
    /// [`Self::compile_program_unchecked`] is reserved for internal tests
    /// that exercise the bytecode emitter against synthetic AST fragments
    /// which cannot satisfy resolution.
    pub fn compile_program(&mut self, program: &mut Program) -> CompilerResult<()> {
        tracing::debug!("Compiler: Starting compilation with validation");

        // Phase 0: Core variable extraction
        tracing::debug!("Compiler: Phase 0 - Core variable extraction");
        self.extract_core_variables(program)?;

        // Phase 0.5: Lower `%` placeholders into lambdas at the nearest
        // enclosing `Fn`-typed parameter slot (using project-wide signatures).
        // This must run before resolution so the resolver sees real lambdas
        // rather than bare `Value::Placeholder` nodes.
        tracing::debug!("Compiler: Phase 0.5 - Placeholder lowering");
        crate::lang::compiler::placeholder_lowering::lower_program(program)?;

        // Phase 1: Variable resolution pass
        tracing::debug!("Compiler: Phase 1 - Variable resolution");
        // Pass both the NamespaceRegistry and CoreVariableRegistry to the resolver
        let mut resolver =
            Resolver::with_registries(self.program.namespaces.clone(), self.core_variables.clone());
        resolver.set_source_info(self.current_file.clone(), self.get_source_content());
        // Add all source files for comprehensive error reporting
        for (file_path, content) in &self.file_contents {
            resolver.add_source_file(file_path.clone(), content.clone());
        }
        resolver.resolve_program(program)?;

        // Phase 2: Type checking pass
        tracing::debug!("Compiler: Phase 2 - Type checking");
        let mut type_checker = TypeChecker::new();
        type_checker.set_source_info(self.current_file.clone(), self.get_source_content());
        // Add all source files for comprehensive error reporting
        for (file_path, content) in &self.file_contents {
            type_checker.add_source_file(file_path.clone(), content.clone());
        }
        let check_result = type_checker.check_program(program);
        // Capture non-fatal warnings (e.g. deprecated-API usage) on both the
        // success and failure paths. Warnings live on the compiler, not in the
        // returned error, so they never count toward compilation failure.
        self.diagnostics = type_checker.take_warnings();
        if let Err(mut errors) = check_result {
            errors.warnings.clear();
            return Err(errors);
        }

        // Phase 3: Bytecode compilation
        tracing::debug!("Compiler: Phase 3 - Bytecode compilation");
        self.compile_program_two_pass_internal(program)
            .map_err(|e| {
                CompilerErrors::with_error(crate::lang::errors::CompilerError::CallLibError {
                    func_name: "compilation".to_string(),
                    message: e,
                    location: None,
                })
            })?;

        // Phase 3.5: Surface var aliases (`alias ::lib/fn`,
        // optionally meta-bearing) in `function_mapping` so the
        // worker's routing table — which uses `function_mapping` as
        // its sole source of truth for "does this build contain this
        // function?" — can find an alias by its own qualified name.
        // Without this, scheduled/event/MCP/webhook artifacts that
        // were registered under the alias's name (because that's
        // where the agentic meta lives) come up empty at routing
        // time and the worker logs "Function ... not found in any
        // deployed build" even though the underlying callable is
        // perfectly compiled.
        self.register_alias_function_mappings(program);

        Ok(())
    }

    /// Internal two-pass compilation method
    fn compile_program_two_pass_internal(&mut self, program: &Program) -> Result<(), String> {
        tracing::debug!("Compiler: Starting two-pass compilation");

        // Pass 0: Extract lazy parameter info and known function names from all function definitions
        // This must happen before bytecode compilation so:
        // - Call sites can determine laziness
        // - Alias creation can distinguish functions from types/variables
        tracing::debug!("Compiler: Pass 0 - Extracting lazy parameter info and known functions");
        self.extract_lazy_params(program);
        self.extract_known_functions(program);

        // Pass 0.5: Register namespace imports (type aliases) before type definitions
        // This ensures imports like `FileMeta ::hot::file/FileMeta` are available
        // when resolving field types in type definitions
        tracing::debug!("Compiler: Pass 0.5 - Registering namespace imports");
        for (ns_path, namespace) in &program.namespaces {
            self.current_namespace = ns_path.to_string();
            self.current_aliases = namespace.aliases.clone();

            // Add namespace to registry
            self.program
                .namespaces
                .add_namespace(self.current_namespace.clone());

            // Register namespace imports (references to other namespaces/types)
            for (var, value) in &namespace.scope.vars {
                if let Value::Ref(Ref::Ns(ns_ref)) = value {
                    // Build the full path including function_name if present
                    // e.g., for `FileMeta ::hot::file/FileMeta`:
                    //   ns_ref.ns = "::hot::file", ns_ref.function_name = Some("FileMeta")
                    //   full path = "::hot::file/FileMeta"
                    let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);
                    let original_path = if let Some(ref func_name) = resolved_ns_ref.function_name {
                        format!("{}/{}", resolved_ns_ref.ns, func_name)
                    } else {
                        resolved_ns_ref.ns.to_string()
                    };

                    let var_info = crate::lang::bytecode::VariableInfo {
                        name: original_path.clone(),
                        var_type: crate::lang::bytecode::VariableType::Value,
                        metadata: var.meta.as_ref().map(|m| m.val.clone()),
                        function_id: None,
                    };
                    self.program.namespaces.add_variable_with_key(
                        &self.current_namespace,
                        var.sym.name(),
                        var_info,
                    );
                    tracing::trace!(
                        "Compiler: Registered import '{}' -> '{}' in namespace '{}'",
                        var.sym.name(),
                        original_path,
                        self.current_namespace
                    );
                }
            }
        }

        // Pass 1: Compile only type definitions
        tracing::debug!("Compiler: Pass 1 - Compiling type definitions");
        for (ns_path, namespace) in &program.namespaces {
            self.current_namespace = ns_path.to_string();
            // Load namespace aliases for alias resolution during compilation
            self.current_aliases = namespace.aliases.clone();
            tracing::trace!(
                "Compiler: Processing namespace {:?} for type definitions ({} aliases)",
                ns_path,
                self.current_aliases.len()
            );

            // Update current_file from namespace-to-file mapping
            let ns_str = ns_path.to_string();
            if let Some(file_path) = self.namespace_to_file.get(&ns_str) {
                self.current_file = Some(file_path.clone());
                tracing::trace!(
                    "Compiler: Updated current_file to {:?} for namespace {}",
                    file_path,
                    ns_str
                );
            }

            // Only compile type definitions in this pass
            for (var, value) in &namespace.scope.vars {
                if matches!(value, Value::TypeDef(_)) {
                    tracing::trace!(
                        "Compiler: Compiling type definition for '{}'",
                        var.sym.name()
                    );

                    // Set source location from AST node
                    self.set_source_location_from_var(var);

                    self.compile_variable_declaration(var, value)?;
                }
            }
        }

        // Pass 2: Compile everything else (with sub-passes for proper ordering)
        tracing::debug!("Compiler: Pass 2 - Compiling all other declarations");
        tracing::trace!("Total namespaces to compile: {}", program.namespaces.len());

        // Create a compilation order that ensures ::hot::main (target file namespace) is processed last
        let mut namespace_order: Vec<_> = program.namespaces.iter().collect();

        // Find ::hot::main namespace and move it to the end if it exists
        if let Some(main_pos) = namespace_order
            .iter()
            .position(|(ns_path, _)| ns_path.to_string() == "::hot::main")
        {
            let main_entry = namespace_order.remove(main_pos);
            namespace_order.push(main_entry);
            tracing::trace!("Compiler: Moved ::hot::main namespace to end of compilation order");
        }

        for (ns_path, namespace) in namespace_order {
            self.current_namespace = ns_path.to_string();
            // Load namespace aliases for alias resolution during compilation
            self.current_aliases = namespace.aliases.clone();
            tracing::trace!(
                "Processing namespace '{}' with {} variables ({} aliases)",
                ns_path,
                namespace.scope.vars.len(),
                self.current_aliases.len()
            );
            tracing::trace!(
                "Compiler: Processing namespace {:?} for non-type declarations",
                ns_path
            );

            // Update current_file from namespace-to-file mapping
            let ns_str = ns_path.to_string();
            if let Some(file_path) = self.namespace_to_file.get(&ns_str) {
                self.current_file = Some(file_path.clone());
                tracing::trace!(
                    "Compiler: Updated current_file to {:?} for namespace {}",
                    file_path,
                    ns_str
                );
            }

            // Compile all variable declarations in source order (preserved by IndexMap)
            // This ensures that variables are available when referenced by later variables
            // TypeDefs are skipped here as they are handled in pass 1
            for (var, value) in &namespace.scope.vars {
                if matches!(value, Value::TypeDef(_)) {
                    continue; // TypeDefs already handled in pass 1
                }

                let value_type = match value {
                    Value::FnCall(_) => "FnCall",
                    _ => "Other",
                };
                tracing::trace!(
                    "Compiler: Compiling variable '{}' (type: {})",
                    var.sym.name(),
                    value_type
                );

                // Set source location from AST node
                self.set_source_location_from_var(var);

                self.compile_variable_declaration(var, value)?;
            }
        }

        tracing::debug!("Compiler: Two-pass compilation completed");

        // Set entry_register_count to the total number of registers allocated
        self.program.entry_register_count = self.next_register;
        tracing::trace!(
            "Compiler: Set entry_register_count to {} (total registers allocated)",
            self.next_register
        );

        Ok(())
    }

    /// Compile a Hot program to bytecode, **skipping name resolution, type
    /// checking, and box/ctx checks**.
    ///
    /// This is a narrow escape hatch for tests that exercise the bytecode
    /// emitter against synthetic AST fragments which cannot satisfy
    /// resolution (no `hot-std`, missing namespaces, etc.). It still runs
    /// placeholder lowering (Phase 0.5) so that `%` shorthand compiles
    /// correctly.
    ///
    /// **Production code must use [`Self::compile_program`].** Skipping
    /// validation in production silently allows ill-typed code, unresolved
    /// references, and missing-context bugs to reach bytecode generation —
    /// where they manifest as confusing runtime errors instead of clear
    /// compile-time diagnostics.
    pub fn compile_program_unchecked(&mut self, program: &mut Program) -> Result<(), String> {
        let start_time = Instant::now();

        crate::lang::compiler::placeholder_lowering::lower_program(program)
            .map_err(|e| format!("Placeholder lowering failed: {:?}", e))?;

        // Extract lazy parameter info and known functions before bytecode compilation
        self.extract_lazy_params(program);
        self.extract_known_functions(program);

        // Compile each namespace
        for (ns_path, namespace) in &program.namespaces {
            self.current_namespace = ns_path.to_unprefixed_string();
            self.compile_namespace(namespace)?;
        }

        // Surface var aliases in `function_mapping`. Same rationale as
        // in `compile_program` — the worker's routing table looks up
        // alias qualified names directly. We also run it here so
        // direct callers of `compile_program_unchecked` (tests, the
        // additional-code path) see the same behavior.
        self.register_alias_function_mappings(program);

        // Update compilation statistics
        self.stats.compile_time_ms = start_time.elapsed().as_millis() as u64;
        self.stats.function_count = self.function_mapping.len();
        self.stats.constant_count = self.program.constants.len();

        // Set entry_register_count to the total number of registers allocated
        self.program.entry_register_count = self.next_register;

        Ok(())
    }

    /// Compile additional Hot code (like test invocation) into the existing program
    pub fn compile_additional_code(&mut self, code: &str) -> Result<(), String> {
        // Parse the additional code
        let additional_program = crate::lang::parser::parse_hot(code)
            .map_err(|e| format!("Parser error in additional code: {}", e))?;

        // Compile the additional program into the existing bytecode
        for (ns_path, namespace) in &additional_program.namespaces {
            self.current_namespace = ns_path.to_unprefixed_string();
            self.compile_namespace(namespace)?;
        }

        Ok(())
    }

