    /// Add file content for error reporting
    pub fn add_file_content(&mut self, path: std::path::PathBuf, content: String) {
        self.file_contents.insert(path, content);
    }

    /// Resolve a namespace path through aliases
    /// If the namespace path starts with an alias, replace it with the source path
    /// Example: ::http with alias (::http -> ::hot::http) becomes ::hot::http
    fn resolve_namespace_alias(
        &self,
        ns_path: &crate::lang::ast::NsPath,
    ) -> crate::lang::ast::NsPath {
        // Check if the entire path matches an alias
        if let Some(source_path) = self.current_aliases.get(ns_path) {
            tracing::trace!(
                "Compiler: Resolved namespace alias {} -> {}",
                ns_path,
                source_path
            );
            return source_path.clone();
        }

        // No alias found, return original
        ns_path.clone()
    }

    /// Resolve a namespace reference through aliases
    /// Returns a new NsRef with the namespace path resolved through aliases
    fn resolve_ns_ref_alias(&self, ns_ref: &crate::lang::ast::NsRef) -> crate::lang::ast::NsRef {
        let resolved_ns = self.resolve_namespace_alias(&ns_ref.ns);
        crate::lang::ast::NsRef {
            ns: resolved_ns,
            src: ns_ref.src.clone(),
            function_name: ns_ref.function_name.clone(),
        }
    }

    /// Resolve a type name to its full path, returning whether it was found
    /// Resolution order (matching UnifiedResolver):
    /// 1. Fully qualified names (::namespace/Type) -> Direct lookup
    /// 2. Local namespace (current_namespace/Type)
    /// 3. Imports/aliases in current namespace
    /// 4. Core variables (auto-imported types like Result)
    fn resolve_type_path(&self, type_name: &str) -> (String, bool) {
        // 1. If already fully qualified, return as-is
        if type_name.starts_with("::") {
            return (type_name.to_string(), true);
        }

        // 2. Check local namespace
        let local_path = format!("{}/{}", self.current_namespace, type_name);
        if let Some(ns_info) = self
            .program
            .namespaces
            .namespaces
            .get(&self.current_namespace)
        {
            // Check if it exists in the current namespace with full path as key
            if ns_info.variables.contains_key(&local_path) {
                return (local_path, true);
            }

            // 3. Check imports/aliases (stored with short name as key)
            if let Some(var_info) = ns_info.variables.get(type_name) {
                // Use the variable's full name (which is the original path if imported)
                return (var_info.name.clone(), true);
            }
        }

        // 4. Check core variables (auto-imported types like Result, Option, etc.)
        if let Some(core_info) = self.core_variables.get(type_name) {
            let core_path = format!("{}/{}", core_info.namespace_path, type_name);
            return (core_path, true);
        }

        // Not found - assume local (for function-scoped types)
        (local_path, false)
    }

