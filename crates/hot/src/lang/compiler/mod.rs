// Bytecode compiler for Hot.
//
// This compiler converts the parsed Hot AST into bytecode instructions.
//
// Submodules under `compiler/` host the discrete passes the orchestrator
// invokes (see `Compiler::compile_program`):
//   * `core_registry`        — registry of core-tagged variables (Phase 0)
//   * `placeholder_lowering` — rewrites `%` shorthand into explicit lambdas (Phase 0.5)
//   * `resolver`             — compile-time name resolution (Phase 1)
//   * `type_checker`         — full program type-check (Phase 2)
//   * `box_checker`          — extracts `meta { box: ... }` requirements
//   * `ctx_checker`          — extracts/validates `ctx.*` requirements
//   * `call_graph`           — builds the function call graph used by
//                              the ctx/box requirement aggregator
// Runtime-side name resolution lives separately in `crate::lang::runtime::resolution`.

