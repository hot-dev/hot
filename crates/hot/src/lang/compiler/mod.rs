    /// Compile a namespace
    fn compile_namespace(&mut self, namespace: &Namespace) -> Result<(), String> {
        // Set the current namespace's aliases for resolution during compilation
        self.current_aliases = namespace.aliases.clone();
        tracing::trace!(
            "Compiler: Compiling namespace {} with {} aliases",
            namespace.path,
            self.current_aliases.len()
        );

        // Compile each variable in the namespace
        for (var, value) in &namespace.scope.vars {
            self.compile_variable_declaration(var, value)?;
        }
        Ok(())
    }

    /// Compile a variable declaration
    fn compile_variable_declaration(
        &mut self,
        var: &Var,
        value: &Value,
    ) -> Result<RegisterId, String> {
        tracing::trace!(
            "Compiler: Compiling variable '{}' in namespace '{}' with value type: {:?}",
            var.sym.name(),
            self.current_namespace,
            std::mem::discriminant(value)
        );

        // Special handling for ns declarations
        if var.sym.name() == "ns" {
            return self.handle_ns_declaration(value);
        }
        match value {
            Value::Val(val, _metadata) => {
                // Compile literal value
                let reg = self.allocate_register();
                self.compile_literal_value(val, reg)?;

                // Add variable to namespace registry (for core variable support)
                let var_type = if Self::is_core_var(var) {
                    crate::lang::bytecode::VariableType::CoreFunction
                } else {
                    crate::lang::bytecode::VariableType::Value
                };

                let var_info = crate::lang::bytecode::VariableInfo {
                    name: var.sym.name().to_string(),
                    var_type,
                    metadata: var.meta.as_ref().map(|m| m.val.clone()),
                    function_id: None,
                };
                self.program
                    .namespaces
                    .add_variable(&self.current_namespace, var_info);

                // Check if this is a deep set operation (e.g., hot.db.uri "value")
                if let Some(deep_set_path) = &var.deep_set {
                    tracing::trace!(
                        "Compiler: Deep set operation for '{}' with path {:?}",
                        var.sym.name(),
                        deep_set_path
                    );
                    // This is a deep property assignment - we need to:
                    // 1. Load the existing variable (or create empty map if it doesn't exist)
                    // 2. Set the deep property on it
                    // 3. Store the modified variable back
                    self.compile_deep_set_assignment(var.sym.name(), deep_set_path, reg)?;
                    Ok(reg)
                } else {
                    // Regular variable assignment
                    tracing::trace!(
                        "Compiler: About to store variable '{}' (literal value)",
                        var.sym.name()
                    );
                    self.store_variable(var.sym.name(), reg);
                    Ok(reg)
                }
            }
            Value::Fn(function_defs) => {
                // Compile function definitions
                let function_name = format!("{}/{}", self.current_namespace, var.sym.name());

                tracing::trace!(
                    "Compiler: Compiling function '{}' in namespace '{}'",
                    var.sym.name(),
                    self.current_namespace
                );

                // Add function to namespace registry
                let var_info = crate::lang::bytecode::VariableInfo {
                    name: var.sym.name().to_string(),
                    var_type: crate::lang::bytecode::VariableType::Function,
                    metadata: var.meta.as_ref().map(|m| m.val.clone()),
                    function_id: None, // Will be set later when we have the actual function ID
                };
                self.program
                    .namespaces
                    .add_variable(&self.current_namespace, var_info);

                // Check if this is a core function by examining metadata
                let is_core_function = Self::is_core_var(var);
                if is_core_function {
                    tracing::trace!("Compiler: Detected core function: '{}'", function_name);
                    // Register both qualified and unqualified names for core functions
                    self.core_functions.insert(var.sym.name().to_string(), 0); // Will be updated with actual ID
                    self.core_functions.insert(function_name.clone(), 0); // Will be updated with actual ID
                }

                // We'll get the actual function ID from add_function, but we need to compile
                // all function definitions first to get the correct function info
                let mut actual_function_id = None;

                // Convert Var.src to SourceLocation for function info
                let source = var
                    .src
                    .as_ref()
                    .map(|s| crate::lang::bytecode::SourceLocation {
                        file: s.file.clone(),
                        line: s.line,
                        column: s.column,
                        position: s.position,
                        length: s.length,
                    });

                // Two-pass compilation for multi-arity functions:
                // Pass 1: Pre-register ALL arities in function_mapping so that
                //         any arity can call any sibling arity during body compilation.
                // Pass 2: Compile ALL function bodies.

                // Pass 1: Pre-register all arities with placeholder IDs
                let mut arity_registrations: Vec<(String, String, FunctionId)> = Vec::new();
                for fn_def in function_defs.iter() {
                    let arity = fn_def.args.args.len();
                    let arity_key = format!("{}/{}", function_name, arity);
                    let signature = self.create_function_signature(&function_name, fn_def);

                    // Reserve a slot in program.functions with a placeholder FunctionInfo.
                    // This ensures the ID is stable even if sibling body compilation
                    // adds nested functions (lambdas) that consume intermediate IDs.
                    let placeholder_info = FunctionInfo {
                        name: function_name.to_string(),
                        namespace: self.current_namespace.clone(),
                        arity: fn_def.args.args.len() as u8,
                        is_variadic: fn_def.args.variadic,
                        param_names: fn_def
                            .args
                            .args
                            .iter()
                            .map(|a| a.var.sym.name().to_string())
                            .collect(),
                        param_types: vec![0; fn_def.args.args.len()],
                        return_type: 0,
                        lazy_params: vec![false; fn_def.args.args.len()],
                        flow_type: None,
                        instructions: Vec::new(),
                        register_count: 10,
                        source: source.clone(),
                    };
                    let reserved_id = self.program.add_function(placeholder_info);

                    self.function_mapping.insert(arity_key.clone(), reserved_id);
                    self.function_mapping.insert(signature.clone(), reserved_id);

                    tracing::trace!(
                        "Compiler: Pass 1 - Pre-registered arity {} of '{}' with reserved ID {} (signature: {})",
                        arity,
                        function_name,
                        reserved_id,
                        signature
                    );

                    arity_registrations.push((arity_key, signature, reserved_id));
                }

                // Pass 2: Compile all function bodies using pre-reserved IDs
                for (i, fn_def) in function_defs.iter().enumerate() {
                    let (ref arity_key, ref signature, reserved_id) = arity_registrations[i];

                    let function_id = self.compile_function_definition_with_reserved_id(
                        &function_name,
                        fn_def,
                        source.clone(),
                        reserved_id,
                    )?;

                    if actual_function_id.is_none() {
                        actual_function_id = Some(function_id);
                    }

                    // Update function mapping if the actual ID differs from reserved
                    // (should not happen with pre-reserved IDs, but safety check)
                    if function_id != reserved_id {
                        self.function_mapping.insert(signature.clone(), function_id);
                        self.function_mapping.insert(arity_key.clone(), function_id);
                    }

                    // If this is a core function, also register it in core_functions
                    if is_core_function {
                        self.core_functions
                            .insert(function_name.clone(), function_id);
                        // Also register unqualified name for auto-import
                        self.core_functions
                            .insert(var.sym.name().to_string(), function_id);
                        tracing::trace!(
                            "Compiler: Registered core function '{}' (unqualified: '{}') with ID {} (signature: {})",
                            function_name,
                            var.sym.name(),
                            function_id,
                            signature
                        );
                    }

                    tracing::trace!(
                        "Compiler: Pass 2 - Compiled function '{}' with signature '{}' (ID: {})",
                        function_name,
                        signature,
                        function_id
                    );
                }

                let _function_id = actual_function_id.unwrap_or(0);

                // Return a register containing the function reference
                let reg = self.allocate_register();
                let fn_name_id = self.program.add_string_ref(function_name.clone());
                self.emit_instruction(Instruction::LoadFunctionRef {
                    dest: reg,
                    function_name: fn_name_id,
                });

                // Store the function variable
                tracing::trace!(
                    "Compiler: About to store function variable '{}'",
                    var.sym.name()
                );
                self.store_variable_with_meta(var.sym.name(), reg, var.meta.as_ref());
                Ok(reg)
            }
            Value::FnCall(fn_call) => {
                // Compile function call and store result in variable
                tracing::trace!(
                    "Compiler: Compiling function call for variable '{}'",
                    var.sym.name()
                );
                let result_reg = self.compile_function_call(fn_call)?;

                // Check if this is a deep set operation
                if let Some(deep_set_path) = &var.deep_set {
                    tracing::trace!(
                        "Compiler: Deep set operation for '{}' with path {:?}",
                        var.sym.name(),
                        deep_set_path
                    );
                    self.compile_deep_set_assignment(var.sym.name(), deep_set_path, result_reg)?;
                    Ok(result_reg)
                } else {
                    // Regular variable assignment
                    tracing::trace!(
                        "Compiler: Storing function call result in variable '{}'",
                        var.sym.name()
                    );
                    self.store_variable(var.sym.name(), result_reg);
                    Ok(result_reg)
                }
            }
            Value::Lambda(lambda) => {
                // Compile lambda and store result in variable
                tracing::trace!(
                    "Compiler: Compiling lambda for variable '{}'",
                    var.sym.name()
                );
                let lambda_reg = self.compile_lambda(lambda)?;

                // Store the lambda in the variable
                tracing::trace!(
                    "Compiler: About to store variable '{}' (lambda)",
                    var.sym.name()
                );
                self.store_variable(var.sym.name(), lambda_reg);
                Ok(lambda_reg)
            }
            Value::TypeDef(type_def) => {
                // Compile type definition as a callable constructor function
                tracing::trace!(
                    "Compiler: Compiling type definition for '{}'",
                    var.sym.name()
                );

                // Check if this is a core type with constructor functions
                let is_core_type = Self::is_core_var(var);
                tracing::trace!(
                    "Compiler: Processing type definition '{}' in namespace '{}', is_core_type: {}, has_constructor_functions: {}, metadata: {:?}",
                    var.sym.name(),
                    self.current_namespace,
                    is_core_type,
                    type_def.constructor_functions.is_some(),
                    var.meta.as_ref().map(|m| &m.val)
                );

                // Pre-register core types (but not variant unions, which use TypeRef instead of functions)
                if is_core_type && type_def.variants.is_none() {
                    let base_function_name =
                        format!("{}/{}", self.current_namespace, var.sym.name());
                    tracing::trace!(
                        "Compiler: Detected core type: '{}' (has explicit constructors: {})",
                        base_function_name,
                        type_def.constructor_functions.is_some()
                    );

                    // Register both qualified and unqualified names for ALL core type constructors
                    // This includes both types with explicit constructor functions AND types with just fields (default constructors)
                    // Use base names (without __compiled suffix) for core function lookups
                    // Will be updated with actual function ID after compilation
                    self.core_functions.insert(var.sym.name().to_string(), 0);
                    self.core_functions.insert(base_function_name.clone(), 0);
                    tracing::trace!(
                        "Compiler: Pre-registered core type constructor '{}' and '{}' in core_functions",
                        var.sym.name(),
                        base_function_name
                    );
                }

                // Create a constructor function for the type
                let type_constructor_reg =
                    self.compile_type_constructor(var.sym.name(), type_def, is_core_type)?;

                // If this was a core type, update the core_functions registry with actual IDs
                // This applies to both explicit constructor functions AND default constructors for types with fields
                // NOTE: Variant union types don't create a single constructor function, so skip this for them
                if is_core_type && type_def.variants.is_none() {
                    let base_function_name =
                        format!("{}/{}", self.current_namespace, var.sym.name());
                    let compiled_function_name = format!("{}__compiled", base_function_name);

                    tracing::trace!(
                        "Compiler: Looking for compiled function '{}' in function_mapping",
                        compiled_function_name
                    );

                    // Find the function ID that was created for this type constructor (with __compiled suffix)
                    if let Some(&function_id) = self.function_mapping.get(&compiled_function_name) {
                        // Register using base names (without suffix) so resolve() can find them
                        // BUT store the compiled function name as the VALUE so VM can find the actual function
                        self.core_functions
                            .insert(base_function_name.clone(), function_id);
                        self.core_functions
                            .insert(var.sym.name().to_string(), function_id);
                        tracing::trace!(
                            "Compiler: Successfully registered core type constructor '{}' (unqualified: '{}') with compiled ID {} from '{}'",
                            base_function_name,
                            var.sym.name(),
                            function_id,
                            compiled_function_name
                        );
                    } else {
                        tracing::error!(
                            "Compiler: Failed to find compiled function '{}' in function_mapping for core type '{}'",
                            compiled_function_name,
                            var.sym.name()
                        );
                    }
                }

                // Store the constructor function in the variable
                // For variant unions, use store_variable_global to ensure they're stored
                // in the correct namespace, since they're looked up during test execution
                // when the VM's current_namespace may differ from the defining namespace
                if type_def.variants.is_some() {
                    self.store_variable_global(
                        &self.current_namespace.clone(),
                        var.sym.name(),
                        type_constructor_reg,
                    );
                } else {
                    self.store_variable_with_meta(
                        var.sym.name(),
                        type_constructor_reg,
                        var.meta.as_ref(),
                    );

                    // Also add to the namespace registry for compile-time type resolution
                    // (store_variable_global does this automatically, but store_variable_with_meta doesn't)
                    let full_name = format!("{}/{}", self.current_namespace, var.sym.name());
                    let var_info = crate::lang::bytecode::VariableInfo {
                        name: full_name.clone(),
                        var_type: crate::lang::bytecode::VariableType::TypeConstructor,
                        metadata: var.meta.as_ref().map(|m| m.val.clone()),
                        function_id: None,
                    };
                    self.program
                        .namespaces
                        .add_variable(&self.current_namespace, var_info);
                }
                Ok(type_constructor_reg)
            }
            Value::MultipleValues(values) => {
                // Handle multiple values in variable declaration context
                // This shouldn't normally happen in variable declarations, but handle it gracefully
                if !values.is_empty() {
                    let value_reg = self.compile_value(&values[values.len() - 1])?;
                    tracing::trace!(
                        "Compiler: About to store variable '{}' (MultipleValues)",
                        var.sym.name()
                    );
                    self.store_variable(var.sym.name(), value_reg);
                    Ok(value_reg)
                } else {
                    let reg = self.allocate_register();
                    let constant_id = self.program.add_constant(Constant::Val(Val::Null));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: reg,
                        constant: constant_id,
                    });
                    self.store_variable(var.sym.name(), reg);
                    Ok(reg)
                }
            }
            Value::TypeImplementation(type_impl) => {
                // Compile type implementation but DO NOT store under the variable name.
                // Only register the impl function and keep the original type binding intact.
                let _ = self.compile_type_implementation(type_impl)?;
                // Return a null register (no binding change)
                let reg = self.allocate_register();
                let null_c = self.program.add_constant(Constant::Val(Val::Null));
                self.emit_instruction(Instruction::LoadConst {
                    dest: reg,
                    constant: null_c,
                });
                Ok(reg)
            }
            Value::Ref(hot_ref) => {
                // Check if this is a function alias (reference to a namespace function)
                // A function alias is like: my_fn ::some::namespace/function
                // But NOT a type/variable reference like: Media ::hot::media/Media
                if let Ref::Ns(ns_ref) = hot_ref
                    && let Some(func_name) = &ns_ref.function_name
                {
                    // Resolve namespace aliases first
                    let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);
                    let target_qualified = format!("{}/{}", resolved_ns_ref.ns, func_name);

                    // Use the pre-extracted known_functions registry to determine if this
                    // is a function reference. This is populated during Pass 0 and includes
                    // both built-in (hotlib) functions and user-defined functions from all
                    // namespaces, so it's independent of compilation order.
                    if self.is_known_function(&target_qualified) {
                        // This is a function alias - create the FunctionAlias structure
                        tracing::trace!(
                            "Compiler: Creating function alias '{}' -> '{}'",
                            var.sym.name(),
                            target_qualified
                        );

                        // Register the alias in the symbol table for lazy parameter detection
                        let alias_key = format!("{}/{}", self.current_namespace, var.sym.name());
                        self.symbol_table
                            .insert(alias_key, target_qualified.clone());

                        // Store the target function name as a string variable
                        // This allows the alias to follow proper lexical scoping rules
                        let target_reg = self.allocate_register();
                        let target_constant = self
                            .program
                            .add_constant(Constant::Val(Val::from(target_qualified.clone())));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: target_reg,
                            constant: target_constant,
                        });

                        // Store as a variable with special metadata to indicate it's a function alias
                        let mut alias_map = indexmap::IndexMap::new();
                        alias_map
                            .insert(Val::from("$type"), Val::from("::hot::type/FunctionAlias"));
                        alias_map.insert(Val::from("$target"), Val::from(target_qualified.clone()));

                        let alias_value_reg = self.allocate_register();
                        let alias_constant = self
                            .program
                            .add_constant(Constant::Val(Val::Map(Box::new(alias_map))));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: alias_value_reg,
                            constant: alias_constant,
                        });

                        // Also add to namespace registry for type resolution in match expressions
                        // This allows patterns like `MyOk` to resolve to `::hot::type/Result.Ok`
                        let var_info = crate::lang::bytecode::VariableInfo {
                            name: target_qualified, // Store the target path for type resolution
                            var_type: crate::lang::bytecode::VariableType::Value,
                            metadata: var.meta.as_ref().map(|m| m.val.clone()),
                            function_id: None,
                        };
                        self.program.namespaces.add_variable_with_key(
                            &self.current_namespace,
                            var.sym.name(),
                            var_info,
                        );

                        self.store_variable(var.sym.name(), alias_value_reg);
                        return Ok(alias_value_reg);
                    }
                    // If not a known function, fall through to compile as type/variable reference
                }

                // Handle reference in variable declaration context (e.g., namespace references, variable references)
                tracing::trace!(
                    "Compiler: Compiling reference for variable '{}'",
                    var.sym.name()
                );
                let ref_reg = self.compile_reference(hot_ref)?;

                // Get the original path for namespace references (for type resolution in match expressions)
                // This allows imports like `Media ::hot::media/Media` to be resolved correctly
                let original_path = match hot_ref {
                    Ref::Ns(ns_ref) => {
                        let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);
                        if let Some(func_name) = &resolved_ns_ref.function_name {
                            format!("{}/{}", resolved_ns_ref.ns, func_name)
                        } else {
                            resolved_ns_ref.ns.to_string()
                        }
                    }
                    _ => var.sym.name().to_string(),
                };

                // Add variable to namespace registry for type resolution
                // The short name (var.sym.name()) is the lookup key
                // The full path (original_path) is stored in var_info.name for type resolution
                let var_info = crate::lang::bytecode::VariableInfo {
                    name: original_path, // Store the full path for type resolution
                    var_type: crate::lang::bytecode::VariableType::Value,
                    metadata: var.meta.as_ref().map(|m| m.val.clone()),
                    function_id: None,
                };
                self.program.namespaces.add_variable_with_key(
                    &self.current_namespace,
                    var.sym.name(),
                    var_info,
                );

                // Check if this is a deep set operation
                if let Some(deep_set_path) = &var.deep_set {
                    tracing::trace!(
                        "Compiler: Deep set operation for '{}' with path {:?}",
                        var.sym.name(),
                        deep_set_path
                    );
                    self.compile_deep_set_assignment(var.sym.name(), deep_set_path, ref_reg)?;
                    Ok(ref_reg)
                } else {
                    // Regular variable assignment
                    tracing::trace!(
                        "Compiler: About to store variable '{}' (reference)",
                        var.sym.name()
                    );
                    self.store_variable(var.sym.name(), ref_reg);
                    Ok(ref_reg)
                }
            }
            Value::Flow(flow) => {
                // Handle flow in variable declaration context
                let flow_reg = self.compile_flow(flow)?;
                tracing::trace!(
                    "Compiler: About to store variable '{}' (flow)",
                    var.sym.name()
                );
                self.store_variable(var.sym.name(), flow_reg);
                Ok(flow_reg)
            }
            Value::TemplateLiteral(template) => {
                // Handle template literal in variable declaration context
                let template_reg = self.compile_template_literal(template)?;
                tracing::trace!(
                    "Compiler: About to store variable '{}' (template)",
                    var.sym.name()
                );
                self.store_variable(var.sym.name(), template_reg);
                Ok(template_reg)
            }
            Value::Raw(raw_value) => {
                // Emit BeginErrorCapture, compile inner, then EndErrorCapture
                self.emit_instruction(Instruction::BeginErrorCapture);
                let dest = self.compile_value(raw_value.as_ref())?;
                self.emit_instruction(Instruction::EndErrorCapture);
                // Wrap the compiled result value into {"ok": value}
                let wrap_dest = self.allocate_register();
                self.emit_instruction(Instruction::WrapOk {
                    dest: wrap_dest,
                    src: dest,
                });
                Ok(wrap_dest)
            }
            Value::Do(inner) => {
                // Handle do construct in variable declaration context
                let do_reg = self.compile_value(inner)?;
                tracing::trace!(
                    "Compiler: About to store variable '{}' (do)",
                    var.sym.name()
                );
                self.store_variable(var.sym.name(), do_reg);
                Ok(do_reg)
            }
            Value::Unbound(unbound) => {
                // Unbound reference in variable declaration - this is an error
                Err(format!(
                    "Cannot declare variable '{}' with unbound reference: {}",
                    var.sym.name(),
                    unbound.name
                ))
            }
            Value::Cond(_label, condition, flow) => {
                // Compile the condition, then the flow result (condition register unused)
                let _ = self.compile_value(condition)?;
                let result_reg = self.compile_flow(flow)?;
                Ok(result_reg)
            }
            Value::CondDefault(flow) => {
                let result_reg = self.compile_flow(flow)?;
                Ok(result_reg)
            }
            Value::Match(match_expr) => self.compile_match_expression(match_expr),
            Value::MatchArm(_) => {
                // MatchArm should only appear inside match flow function bodies
                Err(format!(
                    "Cannot declare variable '{}' with standalone match arm",
                    var.sym.name()
                ))
            }
            Value::VariadicExpansion(identifier) => {
                // Variadic expansion in variable declaration - this is an error
                Err(format!(
                    "Cannot declare variable '{}' with variadic expansion: {}",
                    var.sym.name(),
                    identifier
                ))
            }
            Value::MapWithSpread {
                base_entries,
                spread_entries,
            } => {
                let reg = self.compile_map_with_spread(base_entries, spread_entries)?;

                let var_info = crate::lang::bytecode::VariableInfo {
                    name: var.sym.name().to_string(),
                    var_type: crate::lang::bytecode::VariableType::Value,
                    metadata: var.meta.as_ref().map(|m| m.val.clone()),
                    function_id: None,
                };
                self.program
                    .namespaces
                    .add_variable(&self.current_namespace, var_info);

                if let Some(deep_set_path) = &var.deep_set {
                    self.compile_deep_set_assignment(var.sym.name(), deep_set_path, reg)?;
                    Ok(reg)
                } else {
                    self.store_variable(var.sym.name(), reg);
                    Ok(reg)
                }
            }
            Value::Placeholder(_) => {
                Err("Placeholder `%` used outside of a function call argument".to_string())
            }
        }
    }

    /// Create a function signature for overload resolution
    /// Format: "function_name/arity:type1,type2,type3"
    /// Examples:
    /// - "assert-eq/2:Any,Any"
    /// - "assert-eq/3:Any,Any,Str"
    /// - "convert/1:Str" (for convert(value: Str): Int)
    /// - "convert/1:Int" (for convert(value: Int): Str)
    fn create_function_signature(&self, function_name: &str, fn_def: &FnDef) -> String {
        let arity = fn_def.args.args.len();

        // Extract parameter types
        let param_types: Vec<String> = fn_def
            .args
            .args
            .iter()
            .map(|arg| {
                arg.type_annotation
                    .clone()
                    .unwrap_or_else(|| "Any".to_string())
            })
            .collect();

        if param_types.iter().all(|t| t == "Any") {
            // If all types are Any, use arity-only signature for simplicity
            format!("{}/{}", function_name, arity)
        } else {
            // Include type information for precise overload resolution
            format!("{}/{}:{}", function_name, arity, param_types.join(","))
        }
    }

    /// Compile a function definition and return the actual function ID
    fn compile_function_definition_and_get_id(
        &mut self,
        function_name: &str,
        fn_def: &FnDef,
        source: Option<crate::lang::bytecode::SourceLocation>,
    ) -> Result<FunctionId, String> {
        // Extract parameter names
        let param_names: Vec<String> = fn_def
            .args
            .args
            .iter()
            .map(|arg| arg.var.sym.name().to_string())
            .collect();

        // Extract flow type from function body if it's a flow function (cond, parallel, etc.)
        // Skip serial as it's the default
        let flow_type = match &fn_def.body {
            crate::lang::ast::Value::Flow(flow) => match flow.flow_type {
                crate::lang::ast::FlowType::Serial | crate::lang::ast::FlowType::SerialShort => {
                    None
                }
                crate::lang::ast::FlowType::Parallel
                | crate::lang::ast::FlowType::ParallelShort => {
                    Some(crate::lang::bytecode::FlowType::Parallel)
                }
                crate::lang::ast::FlowType::Cond | crate::lang::ast::FlowType::CondShort => {
                    Some(crate::lang::bytecode::FlowType::Cond)
                }
                crate::lang::ast::FlowType::CondAll | crate::lang::ast::FlowType::CondAllShort => {
                    Some(crate::lang::bytecode::FlowType::CondAll)
                }
                crate::lang::ast::FlowType::Pipe => Some(crate::lang::bytecode::FlowType::Pipe),
                crate::lang::ast::FlowType::Match | crate::lang::ast::FlowType::MatchShort => {
                    Some(crate::lang::bytecode::FlowType::Match)
                }
                crate::lang::ast::FlowType::MatchAll
                | crate::lang::ast::FlowType::MatchAllShort => {
                    Some(crate::lang::bytecode::FlowType::MatchAll)
                }
            },
            _ => None,
        };

        // Build lazy_params from AST flags
        let mut lazy_params: Vec<bool> = fn_def.args.args.iter().map(|a| a.lazy).collect();

        // For `fn match` with Result patterns (Result.Ok / Result.Err),
        // mark the first parameter as lazy so Result.Err values survive
        // auto-unwrapping at call time and can be matched in the flow.
        if matches!(
            flow_type,
            Some(crate::lang::bytecode::FlowType::Match)
                | Some(crate::lang::bytecode::FlowType::MatchAll)
        ) && let crate::lang::ast::Value::Flow(flow) = &fn_def.body
        {
            let has_result_arms = flow.expressions.iter().any(|expr| {
                if let crate::lang::ast::Value::MatchArm(arm) = expr {
                    if let Some(type_name) = &arm.type_name {
                        let (resolved, _) = self.resolve_type_path(type_name);
                        resolved == "::hot::type/Result"
                    } else {
                        false
                    }
                } else {
                    false
                }
            });

            if has_result_arms && !lazy_params.is_empty() {
                lazy_params[0] = true;
            }
        }

        // Create function info with empty instructions (will be filled after compilation)
        let function_info = FunctionInfo {
            name: function_name.to_string(),
            namespace: self.current_namespace.clone(),
            arity: fn_def.args.args.len() as u8,
            is_variadic: fn_def.args.variadic,
            param_names: param_names.clone(),
            param_types: vec![0; fn_def.args.args.len()], // Default type IDs
            return_type: 0,                               // Default return type
            lazy_params,
            flow_type,
            instructions: Vec::new(),
            register_count: 10, // Default register count, will be updated
            source,
        };

        // Add function to program and get the actual function ID
        let actual_function_id = self.program.add_function(function_info.clone());

        // Set current function parameters for compilation context
        let saved_params = std::mem::take(&mut self.current_function_params);
        self.current_function_params = param_names;

        // TCO: Set current function context for tail call detection
        let saved_function_name = self.current_function_name.take();
        let saved_function_id = self.current_function_id.take();
        self.current_function_name = Some(function_name.to_string());
        self.current_function_id = Some(actual_function_id);

        // Compile function body into separate instruction context
        let function_instructions = self.compile_function_body(&fn_def.body)?;

        // TCO: Restore previous function context
        self.current_function_name = saved_function_name;
        self.current_function_id = saved_function_id;

        // Restore previous function parameters
        self.current_function_params = saved_params;

        // Update the function info with the compiled instructions
        if let Some(func_info) = self.program.functions.get_mut(actual_function_id as usize) {
            func_info.register_count = self.next_register;
            func_info.instructions = function_instructions;
            tracing::trace!(
                "Compiler: Compiled {} instructions for function '{}'",
                func_info.instructions.len(),
                function_name
            );
        }

        Ok(actual_function_id)
    }

    /// Compile a function definition into a pre-reserved slot in program.functions.
    /// Used by the two-pass multi-arity compilation: pass 1 reserves slots for all
    /// arities (so sibling arities are visible during compilation), and pass 2 calls
    /// this method to compile each body and update the reserved slot in-place.
    fn compile_function_definition_with_reserved_id(
        &mut self,
        function_name: &str,
        fn_def: &FnDef,
        source: Option<crate::lang::bytecode::SourceLocation>,
        reserved_id: FunctionId,
    ) -> Result<FunctionId, String> {
        // Extract parameter names
        let param_names: Vec<String> = fn_def
            .args
            .args
            .iter()
            .map(|arg| arg.var.sym.name().to_string())
            .collect();

        // Extract flow type from function body if it's a flow function (cond, parallel, etc.)
        let flow_type = match &fn_def.body {
            crate::lang::ast::Value::Flow(flow) => match flow.flow_type {
                crate::lang::ast::FlowType::Serial | crate::lang::ast::FlowType::SerialShort => {
                    None
                }
                crate::lang::ast::FlowType::Parallel
                | crate::lang::ast::FlowType::ParallelShort => {
                    Some(crate::lang::bytecode::FlowType::Parallel)
                }
                crate::lang::ast::FlowType::Cond | crate::lang::ast::FlowType::CondShort => {
                    Some(crate::lang::bytecode::FlowType::Cond)
                }
                crate::lang::ast::FlowType::CondAll | crate::lang::ast::FlowType::CondAllShort => {
                    Some(crate::lang::bytecode::FlowType::CondAll)
                }
                crate::lang::ast::FlowType::Pipe => Some(crate::lang::bytecode::FlowType::Pipe),
                crate::lang::ast::FlowType::Match | crate::lang::ast::FlowType::MatchShort => {
                    Some(crate::lang::bytecode::FlowType::Match)
                }
                crate::lang::ast::FlowType::MatchAll
                | crate::lang::ast::FlowType::MatchAllShort => {
                    Some(crate::lang::bytecode::FlowType::MatchAll)
                }
            },
            _ => None,
        };

        // Build lazy_params from AST flags
        let mut lazy_params: Vec<bool> = fn_def.args.args.iter().map(|a| a.lazy).collect();

        // For `fn match` with Result patterns (Result.Ok / Result.Err),
        // mark the first parameter as lazy so Result.Err values survive
        // auto-unwrapping at call time and can be matched in the flow.
        if matches!(
            flow_type,
            Some(crate::lang::bytecode::FlowType::Match)
                | Some(crate::lang::bytecode::FlowType::MatchAll)
        ) && let crate::lang::ast::Value::Flow(flow) = &fn_def.body
        {
            let has_result_arms = flow.expressions.iter().any(|expr| {
                if let crate::lang::ast::Value::MatchArm(arm) = expr {
                    if let Some(type_name) = &arm.type_name {
                        let (resolved, _) = self.resolve_type_path(type_name);
                        resolved == "::hot::type/Result"
                    } else {
                        false
                    }
                } else {
                    false
                }
            });

            if has_result_arms && !lazy_params.is_empty() {
                lazy_params[0] = true;
            }
        }

        // Update the pre-reserved FunctionInfo slot with full metadata
        if let Some(func_info) = self.program.functions.get_mut(reserved_id as usize) {
            func_info.name = function_name.to_string();
            func_info.namespace = self.current_namespace.clone();
            func_info.arity = fn_def.args.args.len() as u8;
            func_info.is_variadic = fn_def.args.variadic;
            func_info.param_names = param_names.clone();
            func_info.param_types = vec![0; fn_def.args.args.len()];
            func_info.return_type = 0;
            func_info.lazy_params = lazy_params;
            func_info.flow_type = flow_type;
            func_info.source = source;
        }

        // Set current function parameters for compilation context
        let saved_params = std::mem::take(&mut self.current_function_params);
        self.current_function_params = param_names;

        // TCO: Set current function context for tail call detection
        let saved_function_name = self.current_function_name.take();
        let saved_function_id = self.current_function_id.take();
        self.current_function_name = Some(function_name.to_string());
        self.current_function_id = Some(reserved_id);

        // Compile function body into separate instruction context
        let function_instructions = self.compile_function_body(&fn_def.body)?;

        // TCO: Restore previous function context
        self.current_function_name = saved_function_name;
        self.current_function_id = saved_function_id;

        // Restore previous function parameters
        self.current_function_params = saved_params;

        // Update the reserved slot with the compiled instructions
        if let Some(func_info) = self.program.functions.get_mut(reserved_id as usize) {
            func_info.register_count = self.next_register;
            func_info.instructions = function_instructions;
            tracing::trace!(
                "Compiler: Compiled {} instructions for function '{}' (reserved ID: {})",
                func_info.instructions.len(),
                function_name,
                reserved_id
            );
        }

        Ok(reserved_id)
    }

    /// Compile a function body into a separate instruction sequence
    fn compile_function_body(&mut self, body: &Value) -> Result<Vec<Instruction>, String> {
        tracing::trace!("Compiler: compile_function_body called");
        // Save the current instruction context (entry point instructions)
        let saved_instructions = std::mem::take(&mut self.program.entry_point);

        // TCO: The function body is in tail position - the result becomes the function's return value
        let saved_tail_position = self.in_tail_position;
        self.in_tail_position = true;

        // Compile the function body - instructions will go to entry_point temporarily
        // Note: We don't create a separate scope here because execute_user_function
        // already creates a scope with the function parameters
        let result_reg = self.compile_value(body)?;

        // TCO: Restore previous tail position state
        self.in_tail_position = saved_tail_position;

        // Return the result from the function
        // Note: If a TailCall was emitted, this Return becomes dead code
        self.emit_instruction(Instruction::Return { value: result_reg });

        // Extract the compiled instructions for this function
        let function_instructions = std::mem::take(&mut self.program.entry_point);

        // Restore the original entry point instructions
        self.program.entry_point = saved_instructions;

        Ok(function_instructions)
    }

    /// Compile a value expression
    fn compile_value(&mut self, value: &Value) -> Result<RegisterId, String> {
        match value {
            Value::Val(val, _metadata) => {
                let reg = self.allocate_register();
                self.compile_literal_value(val, reg)?;
                Ok(reg)
            }
            Value::FnCall(fn_call) => self.compile_function_call(fn_call),
            Value::Flow(flow) => self.compile_flow(flow),
            Value::Ref(hot_ref) => {
                // Bare namespace references (function_name=None) should be compiled as Namespace objects,
                // not as NULL. The ns declarations are handled separately in compile_variable_declaration.
                self.compile_reference(hot_ref)
            }
            Value::Lambda(lambda) => self.compile_lambda(lambda),
            Value::TypeDef(type_def) => {
                // Compile type definition as a constructor function
                self.compile_type_constructor("anonymous_type", type_def, false)
            }
            Value::MultipleValues(values) => {
                tracing::trace!(
                    "Compiler: Processing MultipleValues with {} elements: {:?}",
                    values.len(),
                    values
                        .iter()
                        .map(|v| format!("{:?}", v))
                        .collect::<Vec<_>>()
                );

                // Check if this MultipleValues represents a vector with complex elements
                // This happens when the parser encounters vectors with FnCall nodes or variable references
                // If we have exactly 2 values and the first is a variable reference, it's likely a variable assignment
                // If we have more than 2 values, it's likely a vector
                let is_variable_assignment =
                    values.len() == 2 && matches!(values.first(), Some(Value::Ref(Ref::Var(_))));

                let is_vector_elements = !is_variable_assignment;

                tracing::trace!(
                    "Compiler: MultipleValues analysis - is_variable_assignment: {}, is_vector_elements: {}",
                    is_variable_assignment,
                    is_vector_elements
                );

                if is_vector_elements {
                    // This is a vector with complex elements (FnCall nodes)
                    // Compile each element and create a vector at runtime
                    tracing::trace!(
                        "Compiler: Compiling vector with {} complex elements: {:?}",
                        values.len(),
                        values
                            .iter()
                            .map(|v| format!("{:?}", v))
                            .collect::<Vec<_>>()
                    );

                    let dest_reg = self.allocate_register();

                    // Track which elements are spread (VariadicExpansion)
                    let mut spread_mask: u64 = 0;
                    let mut compiled_elements = Vec::new();

                    for (i, element) in values.iter().enumerate() {
                        // Check if this element is a VariadicExpansion
                        if matches!(element, Value::VariadicExpansion(_)) {
                            spread_mask |= 1 << i;
                        }
                        let element_reg = self.compile_value(element)?;
                        compiled_elements.push(element_reg);
                    }

                    // Use MakeVec instruction to create vector from consecutive registers
                    if compiled_elements.is_empty() {
                        // Empty vector
                        let empty_vector_constant =
                            self.program.add_constant(Constant::Val(Val::Vec(vec![])));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: dest_reg,
                            constant: empty_vector_constant,
                        });
                    } else {
                        // Ensure elements are in consecutive registers
                        let elements_start = compiled_elements[0];

                        // Move all elements to consecutive registers starting from elements_start
                        for (i, &element_reg) in compiled_elements.iter().enumerate() {
                            let target_reg = elements_start + i as RegisterId;
                            if element_reg != target_reg {
                                self.emit_instruction(Instruction::Move {
                                    dest: target_reg,
                                    src: element_reg,
                                });
                            }
                        }

                        // Create vector from consecutive registers
                        // Use MakeVecWithSpread if any elements need spreading
                        if spread_mask != 0 {
                            self.emit_instruction(Instruction::MakeVecWithSpread {
                                dest: dest_reg,
                                elements_start,
                                count: compiled_elements.len() as u16,
                                spread_mask,
                            });
                            tracing::trace!(
                                "Compiler: Created vector with spread (spread_mask: {:b})",
                                spread_mask
                            );
                        } else {
                            self.emit_instruction(Instruction::MakeVec {
                                dest: dest_reg,
                                elements_start,
                                count: compiled_elements.len() as u16,
                            });
                        }
                    }

                    tracing::trace!(
                        "Compiler: Created vector with {} compiled elements",
                        values.len()
                    );
                    return Ok(dest_reg);
                }

                // Multiple values represent variable assignments like: var_name value
                // The first value should be a variable reference, the second is the value to assign
                tracing::trace!("MultipleValues assignment check - {} values", values.len());
                if values.len() >= 2 {
                    tracing::trace!("Checking if first value is Var reference: {:?}", &values[0]);
                    if let Value::Ref(Ref::Var(var_ref)) = &values[0] {
                        tracing::trace!(
                            "First value IS a Var reference: {}",
                            var_ref.var.sym.name()
                        );

                        // Compile the value expression
                        tracing::trace!("Compiling value expression: {:?}", &values[1]);
                        let value_reg = match &values[1] {
                            Value::TypeDef(type_def) => {
                                // Special handling for type definitions in variable assignments
                                // Use the variable name instead of "anonymous_type"
                                tracing::trace!(
                                    "Compiling type definition for variable '{}'",
                                    var_ref.var.sym.name()
                                );
                                self.compile_type_constructor(
                                    var_ref.var.sym.name(),
                                    type_def,
                                    false,
                                )?
                            }
                            Value::TypeImplementation(type_impl) => {
                                // Special handling for type implementations in variable assignments
                                tracing::trace!(
                                    "Compiling type implementation in function scope: {} -> {}",
                                    type_impl.source_type,
                                    type_impl.target_type
                                );
                                self.compile_type_implementation(type_impl)?
                            }
                            _ => {
                                // Regular value compilation
                                self.compile_value(&values[1])?
                            }
                        };
                        tracing::trace!("Value compiled to register: {}", value_reg);

                        // Store the value in the variable
                        let var_name = &var_ref.var.sym.name();
                        tracing::trace!(
                            "About to store variable '{}' in register {}",
                            var_name,
                            value_reg
                        );

                        // Check if this is a deep set operation (e.g., items[] "value")
                        if let Some(deep_set_path) = &var_ref.var.deep_set {
                            self.compile_deep_set_assignment(var_name, deep_set_path, value_reg)?;
                        } else {
                            self.store_variable(var_name, value_reg);
                        }

                        // If the variable has metadata in the AST, also store a hidden
                        // metadata variable so VM-aware meta() can retrieve it for locals.
                        if let Some(meta) = &var_ref.var.meta {
                            let meta_hidden_name = format!("__meta__{}", var_name);
                            // Load metadata literal into a temp register
                            let meta_reg = self.allocate_register();
                            let meta_const =
                                self.program.add_constant(Constant::Val(meta.val.clone()));
                            self.emit_instruction(Instruction::LoadConst {
                                dest: meta_reg,
                                constant: meta_const,
                            });
                            // Store hidden metadata variable
                            self.store_variable(&meta_hidden_name, meta_reg);
                            tracing::trace!(
                                "Stored hidden metadata variable '{}'",
                                meta_hidden_name
                            );
                        }

                        tracing::trace!(
                            "Compiler: Compiled MultipleValues assignment for '{}'",
                            var_name
                        );

                        Ok(value_reg)
                    } else {
                        tracing::trace!("First value is NOT a Var reference: {:?}", &values[0]);
                        // First value is not a variable reference, treat as expression sequence
                        let mut last_reg = self.allocate_register();
                        for value in values {
                            last_reg = self.compile_value(value)?;
                        }
                        Ok(last_reg)
                    }
                } else {
                    tracing::trace!(
                        "MultipleValues has less than 2 values, treating as expression"
                    );
                    // Empty or single value, just compile the first value if any
                    if let Some(value) = values.first() {
                        self.compile_value(value)
                    } else {
                        let reg = self.allocate_register();
                        let constant_id = self.program.add_constant(Constant::Val(Val::Null));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: reg,
                            constant: constant_id,
                        });
                        Ok(reg)
                    }
                }
            }
            Value::TemplateLiteral(template) => {
                // Compile template literal like `"Hello ${name}"`
                self.compile_template_literal(template)
            }
            Value::Raw(raw_value) => {
                // Emit BeginErrorCapture, compile inner, then EndErrorCapture
                self.emit_instruction(Instruction::BeginErrorCapture);
                let dest = self.compile_value(raw_value.as_ref())?;
                self.emit_instruction(Instruction::EndErrorCapture);
                // Wrap the compiled result value into {"ok": value}
                let wrap_dest = self.allocate_register();
                self.emit_instruction(Instruction::WrapOk {
                    dest: wrap_dest,
                    src: dest,
                });
                Ok(wrap_dest)
            }
            Value::TypeImplementation(type_impl) => {
                // Compile type implementation but don't return the lambda register
                // Only register the implementation in the type registry
                let _ = self.compile_type_implementation(type_impl)?;
                // Return a null register to avoid storing anything under the source type name
                let reg = self.allocate_register();
                let null_constant = self.program.add_constant(Constant::Val(Val::Null));
                self.emit_instruction(Instruction::LoadConst {
                    dest: reg,
                    constant: null_constant,
                });
                Ok(reg)
            }
            Value::Fn(function_defs) => {
                // Compile anonymous function into a LambdaInfo and return it boxed
                if let Some(first_fn_def) = function_defs.first() {
                    // Extract parameter names for free variable analysis
                    let parameters: Vec<String> = first_fn_def
                        .args
                        .args
                        .iter()
                        .map(|arg| arg.var.sym.name().to_string())
                        .collect();

                    // Analyze free variables in the function body for closure capture
                    let capture_vars = self.analyze_free_variables(&first_fn_def.body, &parameters);

                    // Compile body to instructions using existing mechanism
                    // Lambdas don't have a named variable, so no source location from Var
                    let function_id = self.compile_function_definition_and_get_id(
                        "<lambda>",
                        first_fn_def,
                        None,
                    )?;

                    // Extract compiled function instructions to build a LambdaInfo-like value
                    let func_info = self
                        .program
                        .functions
                        .get(function_id as usize)
                        .cloned()
                        .ok_or_else(|| "Missing compiled function info".to_string())?;

                    // Create closure environment placeholders (will be populated at runtime)
                    let mut closure_env = AHashMap::new();
                    for var_name in &capture_vars {
                        closure_env.insert(var_name.clone(), crate::val::Val::Null);
                    }

                    let used_registers = crate::lang::bytecode::LambdaInfo::compute_used_registers(
                        &func_info.instructions,
                    );
                    let lambda_info = crate::lang::bytecode::LambdaInfo {
                        parameters: func_info.param_names.clone(),
                        instructions: func_info.instructions.clone(),
                        register_count: func_info.register_count,
                        capture_vars,
                        closure_env,
                        defining_namespace: func_info.namespace.clone(),
                        is_lazy_param: false,
                        used_registers,
                        structural_hash_cache: Default::default(),
                    };

                    // Add boxed LambdaInfo as a constant and load it
                    let reg = self.allocate_register();
                    let constant_id = self
                        .program
                        .add_constant(Constant::Val(Val::Box(Box::new(lambda_info))));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: reg,
                        constant: constant_id,
                    });
                    Ok(reg)
                } else {
                    let reg = self.allocate_register();
                    let constant_id = self.program.add_constant(Constant::Val(Val::Null));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: reg,
                        constant: constant_id,
                    });
                    Ok(reg)
                }
            }
            Value::Do(inner) => {
                // Do construct for lazy evaluation - compile inner value and force thunk evaluation
                tracing::trace!(
                    "Compiler: Processing do construct (forcing lazy thunk evaluation)"
                );
                let inner_reg = self.compile_value(inner)?;

                // Generate call to ::hot::core/do to evaluate lazy thunks
                // If the value is not a thunk, it will be returned as-is
                let dest_reg = self.allocate_register();

                // Load the do function reference
                let do_fn_reg = self.allocate_register();
                let do_fn_name_id = self.program.add_string_ref("::hot::core/do".to_string());
                self.emit_instruction(Instruction::LoadFunctionRef {
                    dest: do_fn_reg,
                    function_name: do_fn_name_id,
                });

                // Call the do function with the inner value as argument
                self.emit_instruction(Instruction::Call {
                    dest: dest_reg,
                    function: do_fn_reg,
                    args_start: inner_reg,
                    args_count: 1,
                });
                Ok(dest_reg)
            }
            Value::Unbound(unbound) => {
                // Unbound reference - this should be an error in most cases
                Err(format!("Unbound reference: {}", unbound.name))
            }
            Value::Cond(_label, condition, flow) => {
                // For conditional expressions, compile the condition then the flow
                let _ = self.compile_value(condition)?;
                self.compile_flow(flow)
            }
            Value::CondDefault(flow) => self.compile_flow(flow),
            Value::Match(match_expr) => self.compile_match_expression(match_expr),
            Value::MatchArm(_) => {
                // MatchArm should only appear inside match flow function bodies
                Err("Standalone match arm is not valid outside a match flow".to_string())
            }
            Value::VariadicExpansion(identifier) => {
                // Variadic expansion - create a reference to the variadic parameter
                // This will be resolved at runtime when the function is called
                tracing::trace!("Compiler: Processing variadic expansion: {}", identifier);

                // Create a variable reference to the variadic parameter
                let reg = self.allocate_register();
                let var_name_constant =
                    self.program
                        .add_constant(crate::lang::bytecode::Constant::Val(Val::from(
                            identifier.clone(),
                        )));

                self.emit_instruction(Instruction::LoadVar {
                    dest: reg,
                    var_name: var_name_constant,
                });

                tracing::trace!(
                    "Compiler: Created variable reference for variadic expansion '{}'",
                    identifier
                );
                Ok(reg)
            }
            Value::MapWithSpread {
                base_entries,
                spread_entries,
            } => self.compile_map_with_spread(base_entries, spread_entries),
            Value::Placeholder(_) => {
                Err("Placeholder `%` used outside of a function call argument".to_string())
            }
        }
    }

    /// Validate call-lib function reference at compile time and collect errors
    fn validate_call_lib_function(&mut self, fn_call: &FnCall) {
        // Check if we have at least one argument (the function reference)
        if fn_call.args.is_empty() {
            self.call_lib_errors
                .push("call-lib requires at least one argument (function reference)".to_string());
            return;
        }

        // Try to extract the function name from the first argument if it's a static reference
        let first_arg = &fn_call.args[0].value;
        let function_name = match first_arg {
            Value::Ref(Ref::Var(var_ref)) => var_ref.var.sym.name().to_string(),
            Value::Ref(Ref::Ns(ns_ref)) => {
                // Resolve namespace aliases first
                let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);

                // Construct the full function name from namespace and function
                let ns_parts: Vec<String> = resolved_ns_ref
                    .ns
                    .0
                    .iter()
                    .map(|ns_part| match ns_part {
                        crate::lang::ast::NsPathPart::Sym(sym) => sym.name().to_string(),
                    })
                    .collect();
                let ns_path = ns_parts.join("::");

                if let Some(function_name) = &resolved_ns_ref.function_name {
                    format!("::{}/{}", ns_path, function_name)
                } else {
                    return; // No function name, can't validate
                }
            }
            _ => {
                // Not a static reference, can't validate at compile time
                tracing::trace!(
                    "Compiler: call-lib with dynamic function reference - skipping compile-time validation"
                );
                return;
            }
        };

        // Check if this function exists in the Hot library map.
        let hotlib_map = crate::lang::hot::get_hotlib_map();

        // Try exact match first
        if hotlib_map.contains_key(&function_name) {
            tracing::trace!(
                "Compiler: call-lib function '{}' found in Hot library map",
                function_name
            );
            return;
        }

        // Try with different namespace prefixes if not already present
        let namespaced_name = if !function_name.starts_with("::") {
            if function_name.contains("/") {
                format!("::{}", function_name)
            } else {
                format!("::hot::{}", function_name)
            }
        } else {
            function_name.clone()
        };

        if hotlib_map.contains_key(&namespaced_name) {
            tracing::trace!(
                "Compiler: call-lib function '{}' found as '{}' in Hot library map",
                function_name,
                namespaced_name
            );
            return;
        }

        // Function not found - collect this as a compile-time error
        self.call_lib_errors.push(format!(
            "Unknown hotlib function '{}'. Available functions must be registered in lang/hot/libmap.rs",
            function_name
        ));
    }

    /// Check if there are any collected call-lib errors and return them
    pub fn get_call_lib_errors(&self) -> Vec<String> {
        self.call_lib_errors.clone()
    }

    /// Clear collected call-lib errors
    pub fn clear_call_lib_errors(&mut self) {
        self.call_lib_errors.clear();
    }

    /// Get source content for the current file
    fn get_source_content(&self) -> Option<String> {
        self.current_file
            .as_ref()
            .and_then(|file| self.file_contents.get(file))
            .cloned()
    }

    /// Get lazy parameter information for a function call
    /// Returns (lazy_params, is_variadic_with_lazy_rest)
    /// If the function is variadic and the last (variadic) param is lazy,
    /// all overflow arguments should be wrapped as lazy thunks.
    fn get_function_lazy_params(&self, fn_call: &FnCall) -> (Vec<bool>, bool) {
        // Try to determine the target function name
        let function_name = match fn_call.function.as_ref() {
            Value::Ref(Ref::Var(var_ref)) => {
                let name = var_ref.var.sym.name();

                // Check if this is an alias to another function
                // Look up the variable in the current namespace to see if it's an alias
                let mut candidates = vec![
                    name.to_string(),
                    format!("{}/{}", self.current_namespace, name),
                    format!("{}/{}", name, fn_call.args.len()), // arity-based
                    format!("{}/{}/{}", self.current_namespace, name, fn_call.args.len()),
                ];

                // Check symbol table for function aliases
                let alias_key = format!("{}/{}", self.current_namespace, name);
                if let Some(target_function) = self.symbol_table.get(&alias_key) {
                    tracing::trace!(
                        "Compiler: Found function alias '{}' -> '{}' in symbol table",
                        alias_key,
                        target_function
                    );
                    candidates.push(target_function.clone());
                }

                // Resolve unqualified core function names to their qualified form
                // e.g. "is-err" -> "::hot::type/is-err" so lazy params can be found
                if let Some(qualified) = self.core_lazy_function_names.get(name) {
                    candidates.push(qualified.clone());
                    candidates.push(format!("{}/{}", qualified, fn_call.args.len()));
                }

                candidates
            }
            Value::Ref(Ref::Ns(ns_ref)) => {
                // Resolve namespace aliases
                let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);
                if let Some(func_name) = &resolved_ns_ref.function_name {
                    vec![format!("{}/{}", resolved_ns_ref.ns, func_name)]
                } else {
                    vec![]
                }
            }
            _ => vec![],
        };

        // Look up function ID and get lazy params from function info
        // We check both function_mapping and core_functions because they store different key spaces:
        // - function_mapping: signature keys and arity keys (e.g., "::hot::meta/get/1")
        // - core_functions: qualified and unqualified names (e.g., "::hot::meta/get", "get")
        for name in &function_name {
            // Check compiled functions for lazy parameter info
            if let Some(&function_id) = self.function_mapping.get(name)
                && let Some(function_info) = self.program.functions.get(function_id as usize)
            {
                tracing::trace!(
                    "Compiler: Found function '{}' with lazy_params: {:?}, is_variadic: {}",
                    name,
                    function_info.lazy_params,
                    function_info.is_variadic
                );
                if !function_info.lazy_params.is_empty() {
                    // Check if variadic with lazy last param
                    let is_variadic_with_lazy_rest = function_info.is_variadic
                        && function_info.lazy_params.last().copied().unwrap_or(false);
                    return (
                        function_info.lazy_params.clone(),
                        is_variadic_with_lazy_rest,
                    );
                }
            }

            // Also check core_functions registry (contains unqualified names not in function_mapping)
            if let Some(&function_id) = self.core_functions.get(name)
                && let Some(function_info) = self.program.functions.get(function_id as usize)
            {
                tracing::trace!(
                    "Compiler: Found core function '{}' with lazy_params: {:?}, is_variadic: {}",
                    name,
                    function_info.lazy_params,
                    function_info.is_variadic
                );
                if !function_info.lazy_params.is_empty() {
                    // Check if variadic with lazy last param
                    let is_variadic_with_lazy_rest = function_info.is_variadic
                        && function_info.lazy_params.last().copied().unwrap_or(false);
                    return (
                        function_info.lazy_params.clone(),
                        is_variadic_with_lazy_rest,
                    );
                }
            }

            // Check pre-extracted lazy params registry (for functions not yet compiled)
            if let Some((lazy_params, is_variadic)) = self.lazy_params_registry.get(name) {
                tracing::trace!(
                    "Compiler: Found pre-extracted lazy params for '{}': {:?}, is_variadic: {}",
                    name,
                    lazy_params,
                    is_variadic
                );
                // Check if variadic with lazy last param
                let is_variadic_with_lazy_rest =
                    *is_variadic && lazy_params.last().copied().unwrap_or(false);
                return (lazy_params.clone(), is_variadic_with_lazy_rest);
            }
        }

        (vec![], false) // No lazy parameter info found
    }

    /// Compile `if(pred, then[, else])` calls that statically resolve to the
    /// core `::hot::bool/if` as an inline cond flow instead of a lazy-thunk
    /// call. `if` is defined in hot-std as exactly this cond, so semantics are
    /// preserved, but the inline form:
    /// - avoids allocating two thunk lambdas and a full call frame per `if`,
    /// - keeps branch bodies in the enclosing function, so `in_tail_position`
    ///   propagates and self-recursive tail calls through `if` emit `TailCall`
    ///   (interpreter TCO loop; native loop in the JIT). Without this, deep
    ///   `if`-based tail recursion overflows the native stack.
    /// - matches the JIT's existing lazy-branch inlining, which also evaluates
    ///   the condition by truthiness in the caller's frame.
    ///
    /// Returns Ok(None) when the call doesn't match (shadowed `if`, spread or
    /// explicitly-lazy args, result path, wrong arity) so the caller falls
    /// back to the normal call path.
    fn try_compile_inline_if(&mut self, fn_call: &FnCall) -> Result<Option<RegisterId>, String> {
        if !(fn_call.args.len() == 2 || fn_call.args.len() == 3)
            || fn_call.result_path.is_some()
            || fn_call.args.iter().any(|a| a.spread || a.lazy)
        {
            return Ok(None);
        }

        // The callee must statically resolve to the core lazy `if`.
        let is_core_if = match fn_call.function.as_ref() {
            Value::Ref(Ref::Var(var_ref)) => {
                let name = var_ref.var.sym.name();
                name == "if"
                    && var_ref.var.deep_path.is_none()
                    // The core registry must map the bare name to a bool/if...
                    && self
                        .core_lazy_function_names
                        .get(name)
                        .is_some_and(|q| q.ends_with("::bool/if"))
                    // ...and nothing user-defined may shadow it in this namespace.
                    && !self
                        .function_mapping
                        .contains_key(&format!("{}/if", self.current_namespace))
                    && !self.function_mapping.contains_key(&format!(
                        "{}/if/{}",
                        self.current_namespace,
                        fn_call.args.len()
                    ))
                    && !self
                        .symbol_table
                        .contains_key(&format!("{}/if", self.current_namespace))
            }
            Value::Ref(Ref::Ns(ns_ref)) => {
                let resolved = self.resolve_ns_ref_alias(ns_ref);
                resolved.function_name.as_deref() == Some("if")
                    && self
                        .core_lazy_function_names
                        .get("if")
                        .is_some_and(|q| *q == format!("{}/if", resolved.ns))
            }
            _ => false,
        };
        if !is_core_if {
            return Ok(None);
        }

        let result_reg = self.allocate_register();
        // See `next_cond_id` doc on `Compiler`: unique suffix keeps branch
        // names from colliding across sibling/nested conds.
        let cond_id = self.next_cond_id;
        self.next_cond_id += 1;

        self.emit_instruction(Instruction::BeginFlow {
            flow_type: crate::lang::bytecode::FlowType::Cond,
            result_modifier: crate::lang::bytecode::FlowResultModifier::One,
            source: None,
        });

        // Initialize result register with null only for the 2-arg form (a
        // falsy 2-arg `if` yields null). With an else branch some branch
        // always produces the result, and pre-tainting the result register
        // with a Null kind makes the JIT bail out of arithmetic on the
        // flow's (otherwise uniformly typed) result.
        if fn_call.args.len() == 2 {
            let null_constant = self.program.add_constant(Constant::Val(Val::Null));
            self.emit_instruction(Instruction::LoadConst {
                dest: result_reg,
                constant: null_constant,
            });
        }

        let saved_tail_position = self.in_tail_position;

        // then-branch: `pred => then`
        self.in_tail_position = false;
        let condition_reg = self.compile_value(&fn_call.args[0].value)?;
        let then_name = format!("if_then\0c{}", cond_id);
        let then_name_id = self.program.add_string_ref(then_name);
        self.emit_instruction(Instruction::CondBranchStart {
            branch_name: then_name_id,
            condition: condition_reg,
            skip_target: 0,
        });
        self.emit_instruction(Instruction::EnterScope {
            scope_type: crate::lang::bytecode::ScopeType::Flow,
        });
        self.in_tail_position = saved_tail_position;
        let then_reg = self.compile_if_branch(&fn_call.args[1].value)?;
        self.emit_instruction(Instruction::ExitScope);
        self.emit_instruction(Instruction::CondBranchEnd {
            branch_name: then_name_id,
            result: then_reg,
        });

        // else-branch: `=> else` (default arm with always-true condition)
        if let Some(else_arg) = fn_call.args.get(2) {
            let else_name = format!("if_else\0c{}", cond_id);
            let else_name_id = self.program.add_string_ref(else_name);
            let true_const = self.program.add_constant(Constant::Val(Val::Bool(true)));
            let true_reg = self.allocate_register();
            self.emit_instruction(Instruction::LoadConst {
                dest: true_reg,
                constant: true_const,
            });
            self.emit_instruction(Instruction::CondBranchStart {
                branch_name: else_name_id,
                condition: true_reg,
                skip_target: 0,
            });
            self.emit_instruction(Instruction::EnterScope {
                scope_type: crate::lang::bytecode::ScopeType::Flow,
            });
            self.in_tail_position = saved_tail_position;
            let else_reg = self.compile_if_branch(&else_arg.value)?;
            self.emit_instruction(Instruction::ExitScope);
            self.emit_instruction(Instruction::CondBranchEnd {
                branch_name: else_name_id,
                result: else_reg,
            });
        }

        self.in_tail_position = saved_tail_position;
        self.emit_instruction(Instruction::EndFlow { dest: result_reg });

        Ok(Some(result_reg))
    }

    /// Compile one `if` branch inline. Zero-arg block literals (`{ ... }`)
    /// would have become the lazy thunk itself in the call form and been
    /// forced by `do`; inline, their body executes directly (mirroring how
    /// cond arms compile their result flow).
    fn compile_if_branch(&mut self, value: &Value) -> Result<RegisterId, String> {
        if let Value::Lambda(lambda) = value
            && lambda.args.args.is_empty()
        {
            return self.compile_value(&lambda.body);
        }
        self.compile_value(value)
    }

    /// Compile a function call
    fn compile_function_call(&mut self, fn_call: &FnCall) -> Result<RegisterId, String> {
        if let Some(result_reg) = self.try_compile_inline_if(fn_call)? {
            return Ok(result_reg);
        }
        let dest_reg = self.allocate_register();

        // Track which arguments are spread (bitmask)
        let mut spread_mask: u64 = 0;

        // Compile arguments into consecutive registers.
        // TCO: arguments are never in tail position — only the call itself is.
        // Without this reset, a self-recursive call appearing as an argument
        // (e.g. `add(fib(sub(n, 1)), fib(sub(n, 2)))` in tail position) would
        // wrongly emit TailCall and abandon the enclosing expression.
        // Historically this was masked because such shapes only occurred
        // inside lazy thunks, which reset the flag; inline `if` compilation
        // exposes them directly.
        let saved_tail_position_for_args = self.in_tail_position;
        self.in_tail_position = false;
        let args_start = if fn_call.args.is_empty() {
            0 // No arguments
        } else {
            let first_arg_reg = self.allocate_register();

            // Try to determine if the target function has lazy parameters
            let (target_function_lazy_params, is_variadic_with_lazy_rest) =
                self.get_function_lazy_params(fn_call);

            for (i, arg) in fn_call.args.iter().enumerate() {
                let target_reg = first_arg_reg + i as RegisterId;

                // Check for fn syntax used as argument - should use lambda syntax instead
                if let Value::Fn(_) = &arg.value {
                    // Get function name for better error message
                    let fn_name = match fn_call.function.as_ref() {
                        Value::Ref(Ref::Var(var_ref)) => var_ref.var.sym.name().to_string(),
                        Value::Ref(Ref::Ns(ns_ref)) => {
                            if let Some(func_name) = &ns_ref.function_name {
                                format!("{}/{}", ns_ref.ns, func_name)
                            } else {
                                format!("{}", ns_ref.ns)
                            }
                        }
                        _ => "<unknown>".to_string(),
                    };

                    // Build source location info for error message
                    let src_info = arg
                        .src
                        .as_ref()
                        .map(|src| {
                            format!(
                                " at {}:{}",
                                src.file.as_deref().unwrap_or("<unknown>"),
                                src.line
                            )
                        })
                        .unwrap_or_default();

                    return Err(format!(
                        "Use lambda syntax `(args) {{ body }}` instead of `fn (args) {{ body }}` when passing functions as arguments (argument {} in call to `{}` in namespace `{}`{})",
                        i + 1,
                        fn_name,
                        self.current_namespace,
                        src_info
                    ));
                }

                // Track spread arguments
                if arg.spread {
                    spread_mask |= 1 << i;
                    tracing::trace!("Compiler: Argument {} is a spread argument", i);
                }

                // Check if this argument should be lazy based on the target function's parameter info
                // For variadic functions with lazy rest params, all overflow args are lazy
                let from_target = if i < target_function_lazy_params.len() {
                    target_function_lazy_params[i]
                } else {
                    // Overflow argument - lazy if variadic with lazy rest
                    is_variadic_with_lazy_rest
                };
                let should_be_lazy = from_target || arg.lazy;

                tracing::trace!(
                    "Compiler: Argument {} - should_be_lazy={} (from_target={}, from_arg={}, is_variadic_lazy_rest={})",
                    i,
                    should_be_lazy,
                    from_target,
                    arg.lazy,
                    is_variadic_with_lazy_rest
                );

                if should_be_lazy {
                    // For lazy arg: compile it as a zero-argument lambda function
                    // This allows it to be evaluated later via a function call
                    tracing::trace!(
                        "Compiler: Creating zero-argument lambda for lazy argument {}",
                        i
                    );

                    // Check if the value is already a zero-argument lambda (block).
                    // If so, don't double-wrap it - just compile it directly as a lazy param.
                    if let Value::Lambda(existing_lambda) = &arg.value
                        && existing_lambda.args.args.is_empty()
                    {
                        tracing::trace!(
                            "Compiler: Argument {} is already a zero-arg lambda, compiling directly as lazy",
                            i
                        );
                        let lambda_reg = self.compile_lambda_internal(existing_lambda, true)?;
                        self.emit_instruction(Instruction::Move {
                            dest: target_reg,
                            src: lambda_reg,
                        });
                        continue;
                    }

                    // Create a lambda with no parameters that returns the expression
                    let lambda = crate::lang::ast::Lambda {
                        args: crate::lang::ast::FnArgs {
                            args: vec![],
                            variadic: false,
                        },
                        body: Box::new(arg.value.clone()),
                        flow_type: None, // Wrapper lambda, no specific flow type
                    };

                    // Compile the lambda as a lazy parameter (won't be auto-evaluated)
                    let lambda_reg = self.compile_lambda_internal(&lambda, true)?;

                    // Move lambda to target register
                    self.emit_instruction(Instruction::Move {
                        dest: target_reg,
                        src: lambda_reg,
                    });
                } else {
                    let arg_reg = self.compile_value(&arg.value)?;
                    // Move argument to the target consecutive register
                    self.emit_instruction(Instruction::Move {
                        dest: target_reg,
                        src: arg_reg,
                    });
                }
            }
            first_arg_reg
        };
        // TCO: restore tail-position state for the call emission itself.
        self.in_tail_position = saved_tail_position_for_args;

        // Helper: check if we need spread call
        let has_spread = spread_mask != 0;

        // Generate function call instruction based on the function type
        match fn_call.function.as_ref() {
            Value::Ref(Ref::Var(var_ref)) if var_ref.var.deep_path.is_some() => {
                // Method call on a deep path (e.g., Shape.Circle({...}))
                // First, compile the reference to get the function value (e.g., FunctionRef)
                let function_value_reg = self.compile_reference(&Ref::Var(var_ref.clone()))?;

                // Now use this as a dynamic function call
                if has_spread {
                    self.emit_instruction(Instruction::CallWithSpread {
                        dest: dest_reg,
                        function: function_value_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                        spread_mask,
                    });
                } else {
                    self.emit_instruction(Instruction::Call {
                        dest: dest_reg,
                        function: function_value_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                    });
                }

                tracing::trace!(
                    "Compiler: Generated dynamic method call for '{}.{:?}'",
                    var_ref.var.sym.name(),
                    var_ref.var.deep_path
                );

                // Apply result_path if present
                if let Some(ref result_path) = fn_call.result_path {
                    return self.compile_deep_path_access(dest_reg, result_path);
                }
                return Ok(dest_reg);
            }
            Value::Ref(Ref::Var(var_ref)) => {
                // Variable function call (e.g., local variable)
                let function_name = var_ref.var.sym.name();

                // Handle built-in functions specially
                if function_name == "call-lib" {
                    // Validate call-lib function reference at compile time
                    self.validate_call_lib_function(fn_call);

                    // call-lib should preserve lazy arguments from the calling context
                    // This allows call-lib to inspect its arguments dynamically
                    tracing::trace!(
                        "Compiler: call-lib detected - preserving lazy arguments from calling context"
                    );

                    // Compile arguments using the normal lazy detection logic
                    // This will preserve lazy thunks if they come from lazy parameters
                    let first_arg_reg = self.allocate_register();
                    for (i, arg) in fn_call.args.iter().enumerate() {
                        let target_reg = first_arg_reg + i as RegisterId;

                        // For call-lib arguments, don't double-wrap lazy parameters
                        // If arg.lazy is true, the argument value is already a lazy parameter (thunk)
                        // from the calling function, so we should just pass it through without
                        // creating another lambda wrapper
                        tracing::trace!(
                            "Compiler: Processing call-lib argument {} (lazy={})",
                            i,
                            arg.lazy
                        );

                        // Just compile and pass the argument value directly
                        // If it's a lazy parameter variable reference, it will be the thunk itself
                        let arg_reg = self.compile_value(&arg.value)?;
                        self.emit_instruction(Instruction::Move {
                            dest: target_reg,
                            src: arg_reg,
                        });
                    }

                    let function_reg = first_arg_reg;
                    let args_reg = if fn_call.args.len() >= 2 {
                        first_arg_reg + 1
                    } else {
                        first_arg_reg
                    };

                    self.emit_instruction(Instruction::CallLibBuiltin {
                        dest: dest_reg,
                        function: function_reg,
                        args: args_reg,
                    });

                    tracing::trace!(
                        "Compiler: Generated CallLibBuiltin for 'call-lib' with normal arguments"
                    );

                    // Apply result_path if present
                    if let Some(ref result_path) = fn_call.result_path {
                        return self.compile_deep_path_access(dest_reg, result_path);
                    }
                    return Ok(dest_reg);
                }

                // Look up function ID by name with arity and type-based resolution
                let arity = fn_call.args.len();
                let qualified_function_name =
                    format!("{}/{}", self.current_namespace, function_name);

                // FIX #2: Try type-based lookup first when we can infer argument types at compile time
                // This allows distinguishing TypeName({...}) from TypeName(int_value) at compile time
                let compile_time_types: Vec<Option<String>> = fn_call
                    .args
                    .iter()
                    .map(|arg| Self::infer_compile_time_type(&arg.value))
                    .collect();

                let mut function_id: FunctionId = 0;
                let mut lookup_key = String::new();

                // If we have type info for all args, try type-based lookup first
                if compile_time_types.iter().all(|t| t.is_some()) {
                    let type_strs: Vec<&str> = compile_time_types
                        .iter()
                        .map(|t| t.as_ref().unwrap().as_str())
                        .collect();
                    let type_sig = type_strs.join(",");

                    // Try qualified typed key first
                    let qualified_typed_key =
                        format!("{}/{}:{}", qualified_function_name, arity, type_sig);
                    if let Some(&id) = self.function_mapping.get(&qualified_typed_key) {
                        function_id = id;
                        lookup_key = qualified_typed_key;
                    } else {
                        // Try unqualified typed key
                        let typed_key = format!("{}/{}:{}", function_name, arity, type_sig);
                        if let Some(&id) = self.function_mapping.get(&typed_key) {
                            function_id = id;
                            lookup_key = typed_key;
                        }
                    }
                }

                // Fallback to arity-based lookup (qualified first for correct scoping)
                if function_id == 0 {
                    let arity_key = format!("{}/{}", function_name, arity);
                    let qualified_arity_key = format!("{}/{}", qualified_function_name, arity);

                    if let Some(&id) = self.function_mapping.get(&qualified_arity_key) {
                        function_id = id;
                        lookup_key = qualified_arity_key;
                    } else if let Some(&id) = self.function_mapping.get(&qualified_function_name) {
                        function_id = id;
                        lookup_key = qualified_function_name.clone();
                    } else if let Some(&id) = self.function_mapping.get(&arity_key) {
                        function_id = id;
                        lookup_key = arity_key;
                    } else if let Some(&id) = self.function_mapping.get(function_name) {
                        function_id = id;
                        lookup_key = function_name.to_string();
                    }
                }

                if has_spread {
                    // For spread calls, use the simple function name in a register
                    // This allows runtime to resolve core functions like Vec correctly
                    // (qualified names like ::test/Vec won't resolve for core types)
                    let function_name_reg = self.allocate_register();
                    let function_name_constant = self
                        .program
                        .add_constant(Constant::Val(function_name.to_string().into()));

                    // Load function name into register
                    self.emit_instruction(Instruction::LoadConst {
                        dest: function_name_reg,
                        constant: function_name_constant,
                    });

                    self.emit_instruction(Instruction::CallWithSpread {
                        dest: dest_reg,
                        function: function_name_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                        spread_mask,
                    });

                    tracing::trace!(
                        "Compiler: Generated CallWithSpread for '{}' with function name in register {} (spread_mask: {:b})",
                        qualified_function_name,
                        function_name_reg,
                        spread_mask
                    );
                } else if function_id != 0 {
                    // TCO: Check if this is a self-recursive tail call
                    let is_self_recursive = self.current_function_id == Some(function_id);
                    let is_tail_call = self.in_tail_position && is_self_recursive;

                    if is_tail_call {
                        // Emit TailCall for self-recursive call in tail position
                        self.emit_instruction(Instruction::TailCall {
                            function_id,
                            args_start,
                            args_count: fn_call.args.len() as u8,
                        });

                        tracing::debug!(
                            "Compiler: Generated TailCall for self-recursive '{}' (ID: {})",
                            function_name,
                            function_id
                        );
                    } else {
                        self.emit_instruction(Instruction::CallUserFunction {
                            dest: dest_reg,
                            function_id,
                            args_start,
                            args_count: fn_call.args.len() as u8,
                        });

                        tracing::trace!(
                            "Compiler: Generated CallUserFunction for '{}' using key '{}' (ID: {})",
                            function_name,
                            lookup_key,
                            function_id
                        );
                    }
                } else {
                    // Function not found in mapping, store function name in a register and call it
                    let function_name_reg = self.allocate_register();
                    let function_name_constant = self
                        .program
                        .add_constant(Constant::Val(function_name.into()));

                    // Load function name into register
                    self.emit_instruction(Instruction::LoadConst {
                        dest: function_name_reg,
                        constant: function_name_constant,
                    });

                    // Generate call with function name register
                    self.emit_instruction(Instruction::Call {
                        dest: dest_reg,
                        function: function_name_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                    });

                    tracing::trace!(
                        "Compiler: Function '{}' not found in mapping, generated Call with function name in register {}",
                        function_name,
                        function_name_reg
                    );
                }
            }
            Value::Ref(Ref::Ns(ns_ref)) => {
                // Namespace function call (e.g., ::hot::test/run-tests or ::http/get with alias)
                // First, resolve any namespace aliases
                let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);

                // Construct the full function name from namespace path and function name
                let namespace_parts: Vec<String> = resolved_ns_ref
                    .ns
                    .0
                    .iter()
                    .map(|ns_part| match ns_part {
                        crate::lang::ast::NsPathPart::Sym(sym) => match sym {
                            crate::lang::ast::Sym::String(s) => s.clone(),
                        },
                    })
                    .collect();
                let namespace_str = namespace_parts.join("::");

                // Include the function name from the function_name field if present
                let full_function_name = if let Some(func_name) = &resolved_ns_ref.function_name {
                    format!("::{}/{}", namespace_str, func_name)
                } else {
                    format!("::{}", namespace_str)
                };

                // For spread calls, always use function name in a register for runtime lookup
                if has_spread {
                    let function_name_reg = self.allocate_register();
                    let function_name_constant = self
                        .program
                        .add_constant(Constant::Val(full_function_name.clone().into()));

                    // Load function name into register
                    self.emit_instruction(Instruction::LoadConst {
                        dest: function_name_reg,
                        constant: function_name_constant,
                    });

                    self.emit_instruction(Instruction::CallWithSpread {
                        dest: dest_reg,
                        function: function_name_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                        spread_mask,
                    });

                    tracing::trace!(
                        "Compiler: Generated CallWithSpread for namespace function '{}' with function name in register {} (spread_mask: {:b})",
                        full_function_name,
                        function_name_reg,
                        spread_mask
                    );
                } else if let Some(&function_id) = self.function_mapping.get(&full_function_name) {
                    self.emit_instruction(Instruction::CallUserFunction {
                        dest: dest_reg,
                        function_id,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                    });

                    tracing::trace!(
                        "Compiler: Generated CallUserFunction for namespace function '{}' (ID: {})",
                        full_function_name,
                        function_id
                    );
                } else {
                    // Try to find by partial name matching
                    let mut found_function_id = None;
                    for (name, &id) in &self.function_mapping {
                        // Look for exact function name match first
                        if name.ends_with("/run-tests") && name.contains("hot::test") {
                            found_function_id = Some(id);
                            tracing::trace!(
                                "Compiler: Found function by exact match: '{}' -> ID {}",
                                name,
                                id
                            );
                            break;
                        }
                    }

                    // If no exact match, try broader matching
                    if found_function_id.is_none() {
                        for (name, &id) in &self.function_mapping {
                            if name.contains(&namespace_str) && name.ends_with("run-tests") {
                                found_function_id = Some(id);
                                tracing::trace!(
                                    "Compiler: Found function by partial match: '{}' -> ID {}",
                                    name,
                                    id
                                );
                                break;
                            }
                        }
                    }

                    if let Some(function_id) = found_function_id {
                        self.emit_instruction(Instruction::CallUserFunction {
                            dest: dest_reg,
                            function_id,
                            args_start,
                            args_count: fn_call.args.len() as u8,
                        });

                        tracing::trace!(
                            "Compiler: Generated CallUserFunction for matched function (ID: {})",
                            function_id
                        );
                    } else {
                        // Function not found, store function name in a register and call it
                        let function_name_reg = self.allocate_register();
                        let function_name_constant = self
                            .program
                            .add_constant(Constant::Val(full_function_name.clone().into()));

                        // Load function name into register
                        self.emit_instruction(Instruction::LoadConst {
                            dest: function_name_reg,
                            constant: function_name_constant,
                        });

                        self.emit_instruction(Instruction::Call {
                            dest: dest_reg,
                            function: function_name_reg,
                            args_start,
                            args_count: fn_call.args.len() as u8,
                        });

                        tracing::trace!(
                            "Compiler: Namespace function '{}' not found in mapping, generated Call with function name in register {}",
                            full_function_name,
                            function_name_reg
                        );

                        // Debug: List available functions
                        tracing::trace!("🔍 Available functions:");
                        for (name, id) in self.function_mapping.iter().take(10) {
                            tracing::trace!("  [{}] {}", id, name);
                        }
                    }
                }
            }
            _ => {
                // For other function types, generate a generic call
                // Try to extract function name from the function value
                let function_name = match fn_call.function.as_ref() {
                    Value::Ref(Ref::Var(var_ref)) => var_ref.var.sym.name(),
                    _ => "unknown",
                };

                let function_name_reg = self.allocate_register();
                let function_name_constant = self
                    .program
                    .add_constant(Constant::Val(function_name.into()));

                // Load function name into register
                self.emit_instruction(Instruction::LoadConst {
                    dest: function_name_reg,
                    constant: function_name_constant,
                });

                // Generate call with function name register
                if has_spread {
                    self.emit_instruction(Instruction::CallWithSpread {
                        dest: dest_reg,
                        function: function_name_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                        spread_mask,
                    });

                    tracing::trace!(
                        "Compiler: Generic function call '{}' generated with CallWithSpread in register {} (spread_mask: {:b})",
                        function_name,
                        function_name_reg,
                        spread_mask
                    );
                } else {
                    self.emit_instruction(Instruction::Call {
                        dest: dest_reg,
                        function: function_name_reg,
                        args_start,
                        args_count: fn_call.args.len() as u8,
                    });

                    tracing::trace!(
                        "Compiler: Generic function call '{}' generated with function name in register {}",
                        function_name,
                        function_name_reg
                    );
                }
            }
        }

        // Apply result_path if present (e.g., fn()[0] or fn().prop)
        if let Some(ref result_path) = fn_call.result_path {
            return self.compile_deep_path_access(dest_reg, result_path);
        }

        Ok(dest_reg)
    }

    /// Compile a function call with a piped value as the first argument
    fn compile_piped_function_call(
        &mut self,
        fn_call: &crate::lang::ast::FnCall,
        piped_value_reg: RegisterId,
    ) -> Result<RegisterId, String> {
        tracing::trace!(
            "Compiler: Compiling piped function call with {} original args",
            fn_call.args.len()
        );

        let dest_reg = self.allocate_register();

        // Check if the target function has lazy parameters
        let (target_function_lazy_params, is_variadic_with_lazy_rest) =
            self.get_function_lazy_params(fn_call);

        // Check if the first parameter (which receives the piped value) should be lazy
        let first_param_is_lazy = target_function_lazy_params
            .first()
            .copied()
            .unwrap_or(false);

        // Compile all the original arguments first
        let args_start = self.allocate_register();
        let mut current_arg_reg = args_start;

        // First argument is the piped value
        if first_param_is_lazy {
            // Wrap the piped value in a lazy thunk to prevent auto-unwrapping
            // The piped value is already evaluated and in a register.
            // We create a zero-arg lambda that returns this register value when called.
            tracing::trace!("Compiler: Wrapping piped value in lazy thunk (first param is lazy)");

            // Generate a unique variable name to capture the piped value
            let capture_name = format!("$piped_lazy_{}", self.next_register);

            // Store the piped value in a variable that the lambda will capture
            let capture_name_id = self.program.add_string_ref(capture_name.clone());
            self.emit_instruction(Instruction::StoreVar {
                var_name: capture_name_id,
                value: piped_value_reg,
                metadata: None,
            });

            // Create lambda instructions that load the captured variable and return it
            let load_name_id = self.program.add_string_ref(capture_name.clone());
            let lambda_instructions = vec![
                Instruction::LoadVar {
                    dest: 0,
                    var_name: load_name_id,
                },
                Instruction::Return { value: 0 },
            ];

            // Pre-compute used registers
            let used_registers =
                crate::lang::bytecode::LambdaInfo::compute_used_registers(&lambda_instructions);

            // Create the lambda info
            let lambda_info = crate::lang::bytecode::LambdaInfo {
                parameters: vec![],
                instructions: lambda_instructions,
                register_count: 1,
                capture_vars: vec![capture_name],
                closure_env: AHashMap::new(),
                defining_namespace: self.current_namespace.clone(),
                is_lazy_param: true,
                used_registers,
                structural_hash_cache: Default::default(),
            };

            // Store lambda as constant and load it
            let lambda_val = Val::Box(Box::new(lambda_info));
            let constant_id = self
                .program
                .add_constant(crate::lang::bytecode::Constant::Val(lambda_val));
            let lambda_reg = self.allocate_register();
            self.emit_instruction(Instruction::LoadConst {
                dest: lambda_reg,
                constant: constant_id,
            });

            self.emit_instruction(Instruction::Move {
                dest: current_arg_reg,
                src: lambda_reg,
            });
        } else {
            // Non-lazy first param: just move the piped value directly
            self.emit_instruction(Instruction::Move {
                dest: current_arg_reg,
                src: piped_value_reg,
            });
        }
        current_arg_reg += 1;

        // Then compile the rest of the original arguments (with lazy handling)
        for (i, arg) in fn_call.args.iter().enumerate() {
            // The argument index in the target function is i+1 (since piped value is index 0)
            let param_index = i + 1;
            let from_target = if param_index < target_function_lazy_params.len() {
                target_function_lazy_params[param_index]
            } else {
                is_variadic_with_lazy_rest
            };
            let should_be_lazy = from_target || arg.lazy;

            if should_be_lazy {
                // Wrap in lazy thunk
                if let Value::Lambda(existing_lambda) = &arg.value
                    && existing_lambda.args.args.is_empty()
                {
                    let lambda_reg = self.compile_lambda_internal(existing_lambda, true)?;
                    self.emit_instruction(Instruction::Move {
                        dest: current_arg_reg,
                        src: lambda_reg,
                    });
                } else {
                    let lambda = crate::lang::ast::Lambda {
                        args: crate::lang::ast::FnArgs {
                            args: vec![],
                            variadic: false,
                        },
                        body: Box::new(arg.value.clone()),
                        flow_type: None,
                    };
                    let lambda_reg = self.compile_lambda_internal(&lambda, true)?;
                    self.emit_instruction(Instruction::Move {
                        dest: current_arg_reg,
                        src: lambda_reg,
                    });
                }
            } else {
                let arg_reg = self.compile_value(&arg.value)?;
                self.emit_instruction(Instruction::Move {
                    dest: current_arg_reg,
                    src: arg_reg,
                });
            }
            current_arg_reg += 1;
        }

        // Total argument count is original args + 1 (for the piped value)
        let total_args = fn_call.args.len() + 1;

        // Generate function call instruction based on the function type
        match fn_call.function.as_ref() {
            Value::Ref(Ref::Var(var_ref)) => {
                // Variable function call (e.g., local variable)
                let function_name = var_ref.var.sym.name();

                // Handle built-in functions specially
                if function_name == "call-lib" {
                    // call-lib is a built-in function, generate CallLibBuiltin instruction
                    // For CallLibBuiltin, we need function and args registers
                    let function_reg = args_start; // First arg is the function name
                    let args_reg = args_start + 1; // Second arg is the args array

                    self.emit_instruction(Instruction::CallLibBuiltin {
                        dest: dest_reg,
                        function: function_reg,
                        args: args_reg,
                    });

                    tracing::trace!("Compiler: Generated CallLibBuiltin for piped 'call-lib'");
                    return Ok(dest_reg);
                }

                // Look up function ID by name
                if let Some(&function_id) = self.function_mapping.get(function_name) {
                    self.emit_instruction(Instruction::CallUserFunction {
                        dest: dest_reg,
                        function_id,
                        args_start,
                        args_count: total_args as u8,
                    });

                    tracing::trace!(
                        "Compiler: Generated piped CallUserFunction for '{}' (ID: {}) with {} args",
                        function_name,
                        function_id,
                        total_args
                    );
                } else {
                    // Function not found in mapping, store function name in a register and call it
                    let function_name_reg = self.allocate_register();
                    let function_name_constant = self
                        .program
                        .add_constant(Constant::Val(function_name.into()));

                    self.emit_instruction(Instruction::LoadConst {
                        dest: function_name_reg,
                        constant: function_name_constant,
                    });

                    self.emit_instruction(Instruction::Call {
                        dest: dest_reg,
                        function: function_name_reg,
                        args_start,
                        args_count: total_args as u8,
                    });

                    tracing::trace!(
                        "Compiler: Generated piped Call for '{}' with {} args (not found in mapping)",
                        function_name,
                        total_args
                    );
                }
            }
            _ => {
                // For other function types, compile the function expression and call it
                let function_reg = self.compile_value(fn_call.function.as_ref())?;

                self.emit_instruction(Instruction::Call {
                    dest: dest_reg,
                    function: function_reg,
                    args_start,
                    args_count: total_args as u8,
                });

                tracing::trace!("Compiler: Generated piped Call with {} args", total_args);
            }
        }

        // Apply result_path if present (e.g., fn()[0] or fn().prop)
        if let Some(ref result_path) = fn_call.result_path {
            return self.compile_deep_path_access(dest_reg, result_path);
        }

        Ok(dest_reg)
    }

    /// Compile a lambda call with a piped value as the first argument
    fn compile_piped_lambda_call(
        &mut self,
        lambda: &crate::lang::ast::Lambda,
        piped_value_reg: RegisterId,
    ) -> Result<RegisterId, String> {
        tracing::trace!("Compiler: Compiling piped lambda call");

        // First compile the lambda to get a function reference
        let lambda_reg = self.compile_lambda(lambda)?;

        let dest_reg = self.allocate_register();

        // Set up arguments: piped value as first argument
        let args_start = self.allocate_register();
        self.emit_instruction(Instruction::Move {
            dest: args_start,
            src: piped_value_reg,
        });

        // Call the lambda with the piped value
        self.emit_instruction(Instruction::CallLambda {
            dest: dest_reg,
            lambda: lambda_reg,
            args_start,
            args_count: 1,
        });

        tracing::trace!("Compiler: Generated piped lambda call");
        Ok(dest_reg)
    }

    /// Compile a lambda function
    fn compile_lambda_internal(
        &mut self,
        lambda: &crate::lang::ast::Lambda,
        is_lazy_param: bool,
    ) -> Result<RegisterId, String> {
        // Extract parameter names from lambda arguments
        let parameters: Vec<String> = lambda
            .args
            .args
            .iter()
            .map(|arg| arg.var.sym.name().to_string())
            .collect();

        // Analyze lambda body for free variables (closure capture)
        let capture_vars = self.analyze_free_variables(&lambda.body, &parameters);

        tracing::trace!(
            "Compiler: Lambda parameters: {:?}, captured variables: {:?}",
            parameters,
            capture_vars
        );

        // Save current entry point instructions
        let saved_instructions = std::mem::take(&mut self.program.entry_point);

        // TCO: Save and reset TCO state - lambdas don't support TailCall instructions
        // because execute_lambda doesn't have a TCO loop. Resetting these prevents
        // the parent function's TCO context from leaking into the lambda body.
        let saved_function_name = self.current_function_name.take();
        let saved_function_id = self.current_function_id.take();
        let saved_tail_position = self.in_tail_position;
        self.in_tail_position = false;

        // Compile the lambda body into the entry point temporarily
        let body_reg = self.compile_value(&lambda.body)?;

        // TCO: Restore TCO state
        self.current_function_name = saved_function_name;
        self.current_function_id = saved_function_id;
        self.in_tail_position = saved_tail_position;

        // Add a return instruction for the lambda body result
        self.emit_instruction(Instruction::Return { value: body_reg });

        // Extract the compiled instructions for the lambda
        let lambda_instructions = std::mem::take(&mut self.program.entry_point);

        // Restore the original entry point instructions
        self.program.entry_point = saved_instructions;

        // Create closure environment by capturing current variable values
        let mut closure_env = AHashMap::new();
        for var_name in &capture_vars {
            // For now, we'll populate the closure environment at runtime
            // The compiler just identifies what needs to be captured
            closure_env.insert(var_name.clone(), crate::val::Val::Null);
        }

        // Create LambdaInfo structure with proper closure capture
        // register_count must be >= highest register used in instructions + 1
        let max_reg_in_instructions = lambda_instructions
            .iter()
            .filter_map(|i| match i {
                Instruction::LoadConst { dest, .. }
                | Instruction::Move { dest, .. }
                | Instruction::LoadVar { dest, .. }
                | Instruction::LoadVarOrDefault { dest, .. }
                | Instruction::Call { dest, .. }
                | Instruction::CallNative { dest, .. }
                | Instruction::Add { dest, .. }
                | Instruction::DotAccess { dest, .. }
                | Instruction::LoadFunctionRef { dest, .. }
                | Instruction::EndFlow { dest, .. }
                | Instruction::Pipe { dest, .. }
                | Instruction::CaptureVar { dest, .. }
                | Instruction::LoadScoped { dest, .. }
                | Instruction::DefineFunction { dest, .. } => Some(*dest),
                Instruction::Return { value } => Some(*value),
                _ => None,
            })
            .max()
            .unwrap_or(0);

        // Use the higher of next_register or max_reg + 1 to ensure we have enough capacity
        let register_count = std::cmp::max(self.next_register, max_reg_in_instructions + 1);

        // Pre-compute used registers for efficient save/restore during lambda execution
        let used_registers =
            crate::lang::bytecode::LambdaInfo::compute_used_registers(&lambda_instructions);

        let lambda_info = crate::lang::bytecode::LambdaInfo {
            parameters: parameters.clone(),
            instructions: lambda_instructions,
            register_count,
            capture_vars,
            closure_env,
            defining_namespace: self.current_namespace.clone(),
            is_lazy_param,
            used_registers,
            structural_hash_cache: Default::default(),
        };

        // Create a boxed LambdaInfo value
        let lambda_val = Val::Box(Box::new(lambda_info));

        // Add the lambda value as a constant
        let constant_id = self
            .program
            .add_constant(crate::lang::bytecode::Constant::Val(lambda_val));

        // Load the lambda constant into a register
        let reg = self.allocate_register();
        self.emit_instruction(Instruction::LoadConst {
            dest: reg,
            constant: constant_id,
        });

        tracing::trace!(
            "Compiler: Compiled lambda with {} parameters",
            parameters.len()
        );
        Ok(reg)
    }

    /// Compile a lambda function (regular, non-lazy)
    fn compile_lambda(&mut self, lambda: &crate::lang::ast::Lambda) -> Result<RegisterId, String> {
        self.compile_lambda_internal(lambda, false)
    }

    /// Compile an expression as a zero-arg thunk for deferred/parallel execution.
    /// Returns a register containing the LambdaInfo thunk.
    fn compile_expression_as_thunk(&mut self, expr: &Value) -> Result<RegisterId, String> {
        // Analyze expression for free variables that need to be captured
        let capture_vars = self.analyze_free_variables_in_value(expr);

        tracing::trace!(
            "Compiler: Creating thunk for expression, captured variables: {:?}",
            capture_vars
        );

        // Save current entry point instructions
        let saved_instructions = std::mem::take(&mut self.program.entry_point);

        // Save and reset TCO state
        let saved_function_name = self.current_function_name.take();
        let saved_function_id = self.current_function_id.take();
        let saved_tail_position = self.in_tail_position;
        self.in_tail_position = false;

        // Save register state and reset for thunk compilation
        let saved_next_register = self.next_register;
        self.next_register = 0;

        // Compile the expression into the entry point temporarily
        let body_reg = self.compile_value(expr)?;

        // Restore TCO state
        self.current_function_name = saved_function_name;
        self.current_function_id = saved_function_id;
        self.in_tail_position = saved_tail_position;

        // Add a return instruction for the expression result
        self.emit_instruction(Instruction::Return { value: body_reg });

        // Extract the compiled instructions for the thunk
        let thunk_instructions = std::mem::take(&mut self.program.entry_point);

        // Restore the original entry point instructions
        self.program.entry_point = saved_instructions;

        // Calculate register count for the thunk
        let max_reg_in_instructions = thunk_instructions
            .iter()
            .filter_map(|i| match i {
                Instruction::LoadConst { dest, .. }
                | Instruction::Move { dest, .. }
                | Instruction::LoadVar { dest, .. }
                | Instruction::LoadVarOrDefault { dest, .. }
                | Instruction::Call { dest, .. }
                | Instruction::CallNative { dest, .. }
                | Instruction::Add { dest, .. }
                | Instruction::DotAccess { dest, .. }
                | Instruction::LoadFunctionRef { dest, .. }
                | Instruction::EndFlow { dest, .. }
                | Instruction::Pipe { dest, .. }
                | Instruction::CaptureVar { dest, .. }
                | Instruction::LoadScoped { dest, .. }
                | Instruction::DefineFunction { dest, .. } => Some(*dest),
                Instruction::Return { value } => Some(*value),
                _ => None,
            })
            .max()
            .unwrap_or(0);

        let thunk_register_count = max_reg_in_instructions + 1;

        // Restore parent register counter
        self.next_register = saved_next_register;

        // Create closure environment
        let mut closure_env = AHashMap::new();
        for var_name in &capture_vars {
            closure_env.insert(var_name.clone(), crate::val::Val::Null);
        }

        // Pre-compute used registers for efficient save/restore
        let used_registers =
            crate::lang::bytecode::LambdaInfo::compute_used_registers(&thunk_instructions);

        // Create LambdaInfo structure (zero parameters = thunk)
        let thunk_info = crate::lang::bytecode::LambdaInfo {
            parameters: Vec::new(), // Zero args - this is a thunk
            instructions: thunk_instructions,
            register_count: thunk_register_count,
            capture_vars,
            closure_env,
            defining_namespace: self.current_namespace.clone(),
            is_lazy_param: false,
            used_registers,
            structural_hash_cache: Default::default(),
        };

        // Create a boxed LambdaInfo value
        let thunk_val = Val::Box(Box::new(thunk_info));

        // Add the thunk value as a constant
        let constant_id = self
            .program
            .add_constant(crate::lang::bytecode::Constant::Val(thunk_val));

        // Load the thunk constant into a register
        let reg = self.allocate_register();
        self.emit_instruction(Instruction::LoadConst {
            dest: reg,
            constant: constant_id,
        });

        tracing::trace!("Compiler: Compiled expression as thunk");
        Ok(reg)
    }

    /// Analyze free variables in a Value expression (for thunk capture)
    fn analyze_free_variables_in_value(&self, expr: &Value) -> Vec<String> {
        let mut free_vars = Vec::new();
        self.collect_free_variables_from_value(expr, &mut free_vars);
        // Deduplicate
        free_vars.sort();
        free_vars.dedup();
        free_vars
    }

    /// Recursively collect free variable references from a Value
    fn collect_free_variables_from_value(&self, expr: &Value, free_vars: &mut Vec<String>) {
        match expr {
            Value::Ref(hot_ref) => {
                if let Ref::Var(var_ref) = hot_ref {
                    let var_name = var_ref.var.sym.name().to_string();
                    // Skip namespace-qualified variables and known globals
                    if !var_name.contains("::") && !self.is_known_global(&var_name) {
                        free_vars.push(var_name);
                    }
                }
            }
            Value::FnCall(call) => {
                // Check callee (function expression)
                self.collect_free_variables_from_value(&call.function, free_vars);
                // Check arguments
                for arg in &call.args {
                    self.collect_free_variables_from_value(&arg.value, free_vars);
                }
            }
            Value::Flow(flow) => {
                for expr in &flow.expressions {
                    self.collect_free_variables_from_value(expr, free_vars);
                }
            }
            Value::Lambda(lambda) => {
                // Lambda parameters shadow outer variables
                let params: AHashSet<_> = lambda
                    .args
                    .args
                    .iter()
                    .map(|a| a.var.sym.name().to_string())
                    .collect();
                let mut inner_vars = Vec::new();
                self.collect_free_variables_from_value(&lambda.body, &mut inner_vars);
                for var in inner_vars {
                    if !params.contains(&var) {
                        free_vars.push(var);
                    }
                }
            }
            Value::TemplateLiteral(tl) => {
                for part in &tl.parts {
                    if let crate::lang::ast::TemplatePart::Expression(expr) = part {
                        self.collect_free_variables_from_value(expr, free_vars);
                    }
                }
            }
            Value::Cond(_, condition, flow) => {
                self.collect_free_variables_from_value(condition, free_vars);
                for expr in &flow.expressions {
                    self.collect_free_variables_from_value(expr, free_vars);
                }
            }
            Value::CondDefault(flow) => {
                for expr in &flow.expressions {
                    self.collect_free_variables_from_value(expr, free_vars);
                }
            }
            Value::Raw(inner) | Value::Do(inner) => {
                self.collect_free_variables_from_value(inner, free_vars);
            }
            Value::Match(match_expr) => {
                self.collect_free_variables_from_value(&match_expr.value, free_vars);
                for arm in &match_expr.arms {
                    self.collect_free_variables_from_value(&arm.body, free_vars);
                }
            }
            Value::MatchArm(arm) => {
                self.collect_free_variables_from_value(&arm.body, free_vars);
            }
            Value::MultipleValues(values) => {
                for v in values {
                    self.collect_free_variables_from_value(v, free_vars);
                }
            }
            Value::MapWithSpread {
                base_entries,
                spread_entries,
            } => {
                for v in base_entries.values() {
                    self.collect_free_variables_in_val_for_value(v, free_vars);
                }
                for (_, spread_val) in spread_entries {
                    self.collect_free_variables_from_value(spread_val, free_vars);
                }
            }
            // Map / Vec literals: parser embeds non-literal entry values as
            // `Val::Box(AstNode(inner_value))`. Recurse through these so refs
            // inside literals are visible to thunk free-variable analysis.
            Value::Val(val, _) => {
                self.collect_free_variables_in_val_for_value(val, free_vars);
            }
            // These don't contain free variables
            Value::TypeDef(_)
            | Value::TypeImplementation(_)
            | Value::Fn(_)
            | Value::Unbound(_)
            | Value::VariadicExpansion(_)
            | Value::Placeholder(_) => {}
        }
    }

    /// Walk a `Val` looking for embedded `AstNode` boxes (used inside Map/Vec
    /// literals for non-literal entry values) and recurse back into the
    /// thunk-side AST walker.
    fn collect_free_variables_in_val_for_value(&self, val: &Val, free_vars: &mut Vec<String>) {
        match val {
            Val::Box(boxed) => {
                if let Some(ast_node) = boxed.as_any().downcast_ref::<crate::lang::ast::AstNode>() {
                    self.collect_free_variables_from_value(&ast_node.0, free_vars);
                }
            }
            Val::Map(map) => {
                for v in map.values() {
                    self.collect_free_variables_in_val_for_value(v, free_vars);
                }
            }
            Val::Vec(vec) => {
                for v in vec {
                    self.collect_free_variables_in_val_for_value(v, free_vars);
                }
            }
            _ => {}
        }
    }

    /// Check if a variable name is a known function (not a variable that needs capturing)
    fn is_known_global(&self, name: &str) -> bool {
        // Check if it's in the function mapping (namespace-prefixed functions)
        // or core functions - these are resolved by name at runtime
        self.function_mapping.contains_key(name) || self.core_functions.contains_key(name)
    }

    /// Compile a named function definition inside a flow (local function)
    /// This registers the function properly for TCO support
    fn compile_named_function_in_flow(
        &mut self,
        var_name: &str,
        function_defs: &[crate::lang::ast::FnDef],
    ) -> Result<RegisterId, String> {
        if let Some(first_fn_def) = function_defs.first() {
            // Create the qualified function name
            let function_name = format!("{}/{}", self.current_namespace, var_name);
            let arity = first_fn_def.args.args.len();
            let arity_key = format!("{}/{}", function_name, arity);
            let signature = self.create_function_signature(&function_name, first_fn_def);

            // Pre-register the function ID for TCO (before compiling the body)
            let placeholder_id = self.program.functions.len() as FunctionId;
            self.function_mapping
                .insert(arity_key.clone(), placeholder_id);
            self.function_mapping
                .insert(signature.clone(), placeholder_id);

            tracing::trace!(
                "Compiler: Pre-registering local function '{}' for TCO with ID {}",
                function_name,
                placeholder_id
            );

            // Extract parameter names for free variable analysis
            let parameters: Vec<String> = first_fn_def
                .args
                .args
                .iter()
                .map(|arg| arg.var.sym.name().to_string())
                .collect();

            // Analyze free variables in the function body for closure capture
            let capture_vars = self.analyze_free_variables(&first_fn_def.body, &parameters);

            // Compile body to instructions with proper function context
            let function_id =
                self.compile_function_definition_and_get_id(&function_name, first_fn_def, None)?;

            // Update function mapping if the ID changed
            if function_id != placeholder_id {
                self.function_mapping.insert(arity_key, function_id);
                self.function_mapping.insert(signature, function_id);
            }

            // Extract compiled function instructions to build a LambdaInfo-like value
            let func_info = self
                .program
                .functions
                .get(function_id as usize)
                .cloned()
                .ok_or_else(|| "Missing compiled function info".to_string())?;

            // Create closure environment placeholders (will be populated at runtime)
            let mut closure_env = AHashMap::new();
            for var_name in &capture_vars {
                closure_env.insert(var_name.clone(), crate::val::Val::Null);
            }

            // Pre-compute used registers for efficient save/restore
            let used_registers =
                crate::lang::bytecode::LambdaInfo::compute_used_registers(&func_info.instructions);

            let lambda_info = crate::lang::bytecode::LambdaInfo {
                parameters: func_info.param_names.clone(),
                instructions: func_info.instructions.clone(),
                register_count: func_info.register_count,
                capture_vars,
                closure_env,
                defining_namespace: func_info.namespace.clone(),
                is_lazy_param: false,
                used_registers,
                structural_hash_cache: Default::default(),
            };

            // Add boxed LambdaInfo as a constant and load it
            let reg = self.allocate_register();
            let constant_id = self
                .program
                .add_constant(Constant::Val(Val::Box(Box::new(lambda_info))));
            self.emit_instruction(Instruction::LoadConst {
                dest: reg,
                constant: constant_id,
            });

            Ok(reg)
        } else {
            let reg = self.allocate_register();
            let constant_id = self.program.add_constant(Constant::Val(Val::Null));
            self.emit_instruction(Instruction::LoadConst {
                dest: reg,
                constant: constant_id,
            });
            Ok(reg)
        }
    }

    /// Compile a type definition as a callable constructor function
    fn compile_type_constructor(
        &mut self,
        type_name: &str,
        type_def: &crate::lang::ast::TypeDef,
        is_core_type: bool,
    ) -> Result<RegisterId, String> {
        tracing::trace!("Compiler: Creating type constructor for '{}'", type_name);
        tracing::trace!("Compiler: Type def: {:?}", type_def);

        // Handle variant union types specially
        if let Some(variants) = &type_def.variants {
            return self.compile_variant_union_type(type_name, variants);
        }

        // Create a unique function name for the type constructor with namespace
        let base_constructor_name = format!("{}/{}", self.current_namespace, type_name);

        // Check if this is a core type - if so, add suffix to differentiate from hotlib function
        let constructor_name = if is_core_type {
            format!("{}__compiled", base_constructor_name)
        } else {
            base_constructor_name
        };

        // All types should be compiled normally - core metadata just means auto-import
        // The function body determines whether it calls call-lib to link to hotlib functions

        // FIX #1: For types with struct fields, ALWAYS create a default Map-based constructor.
        // This must be registered BEFORE explicit constructors so that calls to TypeName({...})
        // inside explicit constructor bodies can resolve to the default Map constructor.
        let has_struct_fields = type_def.fields.is_some();

        if has_struct_fields {
            tracing::trace!(
                "Compiler: Creating default Map constructor for struct type '{}'",
                constructor_name
            );
            let default_fn_id =
                self.create_default_type_constructor(&constructor_name, type_def)?;

            // Register with type-specific key so it can be found when arg is a Map
            let map_typed_key = format!("{}/1:Map", constructor_name);
            self.function_mapping
                .insert(map_typed_key.clone(), default_fn_id);

            // Only register at arity key if no explicit constructors will claim it
            if type_def.constructor_functions.is_none() {
                let arity_key = format!("{}/1", constructor_name);
                self.function_mapping.insert(arity_key, default_fn_id);
                self.function_mapping
                    .insert(constructor_name.clone(), default_fn_id);
            }

            // For core types, also register base name variants
            if constructor_name.ends_with("__compiled") {
                let base_name = constructor_name.trim_end_matches("__compiled");
                let base_map_key = format!("{}/1:Map", base_name);
                self.function_mapping.insert(base_map_key, default_fn_id);
                if type_def.constructor_functions.is_none() {
                    self.function_mapping
                        .insert(base_name.to_string(), default_fn_id);
                    let base_arity_key = format!("{}/1", base_name);
                    self.function_mapping.insert(base_arity_key, default_fn_id);
                }
            }

            tracing::trace!(
                "Compiler: Registered default Map constructor '{}' with function ID {}",
                map_typed_key,
                default_fn_id
            );
        }

        // If the type has explicit constructor functions defined, compile ALL overloads
        if let Some(constructor_functions) = &type_def.constructor_functions {
            for fn_def in constructor_functions {
                // Type constructors don't have Var source, use None
                let function_id =
                    self.compile_function_definition_and_get_id(&constructor_name, fn_def, None)?;

                // Create signature and arity keys for overload resolution
                let signature = self.create_function_signature(&constructor_name, fn_def);
                self.function_mapping.insert(signature.clone(), function_id);

                let arity = fn_def.args.args.len();
                let arity_key = format!("{}/{}", constructor_name, arity);
                // Register at arity key (may override default for non-Map types)
                self.function_mapping.insert(arity_key, function_id);

                // Also add a type-based signature using parameter type names for better matching
                let param_types: Vec<String> = fn_def
                    .args
                    .args
                    .iter()
                    .map(|arg| {
                        let type_annotation = arg
                            .type_annotation
                            .as_ref()
                            .cloned()
                            .unwrap_or_else(|| "Any".to_string());
                        // Normalize type annotation to match VM's get_val_type_name format
                        Self::normalize_type_annotation(&type_annotation)
                    })
                    .collect();
                let typed_key = format!("{}/{}:{}", constructor_name, arity, param_types.join(","));
                self.function_mapping.insert(typed_key.clone(), function_id);

                tracing::trace!(
                    "Compiler: Registered typed constructor overload: '{}' -> function ID {}",
                    typed_key,
                    function_id
                );

                // Also store bare name mapping as a convenience (will be disambiguated by arity later)
                self.function_mapping
                    .entry(constructor_name.clone())
                    .or_insert(function_id);

                tracing::trace!(
                    "Compiler: Registered type constructor overload '{}' (arity {}), sig '{}' with function ID {}",
                    constructor_name,
                    arity,
                    signature,
                    function_id
                );
            }
        } else if !has_struct_fields {
            // No struct fields and no explicit constructors (e.g., empty marker types)
            // Create a simple default constructor
            tracing::trace!(
                "Compiler: Creating default constructor for marker type '{}'",
                constructor_name
            );
            let function_id = self.create_default_type_constructor(&constructor_name, type_def)?;

            self.function_mapping
                .insert(constructor_name.clone(), function_id);
            let arity_key = format!("{}/1", constructor_name);
            self.function_mapping.insert(arity_key, function_id);

            if constructor_name.ends_with("__compiled") {
                let base_name = constructor_name.trim_end_matches("__compiled");
                self.function_mapping
                    .insert(base_name.to_string(), function_id);
                let base_arity_key = format!("{}/1", base_name);
                self.function_mapping.insert(base_arity_key, function_id);
            }

            tracing::trace!(
                "Compiler: Registered default type constructor '{}' with function ID {}",
                constructor_name,
                function_id
            );
        }

        // Check if we're in function scope and emit RegisterLocalType instruction
        // Use current_function_name instead of params - a zero-arg function is still a function scope!
        let is_function_scope = self.current_function_name.is_some();
        if is_function_scope {
            tracing::trace!(
                "Compiler: Registering function-scoped type constructor: {} (function: {})",
                type_name,
                constructor_name
            );

            // Emit instruction to register the type constructor in the VM's current scope
            let type_name_id = self.program.add_string_ref(type_name.to_string());
            let constructor_name_id = self.program.add_string_ref(constructor_name.clone());
            self.emit_instruction(crate::lang::bytecode::Instruction::RegisterLocalType {
                type_name: type_name_id,
                constructor_function_name: constructor_name_id,
            });
        }

        // Return a register containing the function reference
        let reg = self.allocate_register();
        let fn_name_id = self.program.add_string_ref(constructor_name);
        self.emit_instruction(Instruction::LoadFunctionRef {
            dest: reg,
            function_name: fn_name_id,
        });

        Ok(reg)
    }

    /// Compile a variant union type (tagged union)
    fn compile_variant_union_type(
        &mut self,
        type_name: &str,
        variants: &[crate::lang::ast::VariantDef],
    ) -> Result<RegisterId, String> {
        use crate::lang::refs::TypeRef;
        use indexmap::IndexMap;

        tracing::trace!(
            "Compiler: Creating variant union type for '{}' with {} variants",
            type_name,
            variants.len()
        );

        // Build the fully qualified type name
        let full_type_name = format!("{}/{}", self.current_namespace, type_name);

        // Build variant map: name -> optional type reference
        let variant_map: IndexMap<String, Option<String>> = variants
            .iter()
            .map(|v| (v.name.name().to_string(), v.type_ref.clone()))
            .collect();

        // Create TypeRef
        let type_ref = TypeRef::new(full_type_name.clone(), variant_map);

        // Add to constant pool as VariantTypeRef
        let type_ref_constant = self
            .program
            .add_constant(Constant::VariantTypeRef(type_ref));

        // Return a register containing the TypeRef
        let reg = self.allocate_register();
        self.emit_instruction(Instruction::LoadTypeRef {
            dest: reg,
            type_ref: type_ref_constant,
        });

        // Also create variant constructor functions for each variant
        for variant in variants {
            self.compile_variant_constructor(&full_type_name, variant)?;
        }

        tracing::trace!(
            "Compiler: Compiled variant union type '{}' with {} variants",
            full_type_name,
            variants.len()
        );

        Ok(reg)
    }

    /// Compile a constructor function for a single variant
    fn compile_variant_constructor(
        &mut self,
        type_name: &str,
        variant: &crate::lang::ast::VariantDef,
    ) -> Result<FunctionId, String> {
        let variant_name = variant.name.name();
        let constructor_name = format!("{}.{}", type_name, variant_name);

        tracing::trace!(
            "Compiler: Creating variant constructor '{}' (type_ref: {:?})",
            constructor_name,
            variant.type_ref
        );

        let function_id = self.program.functions.len() as FunctionId;

        // Create full type name constant (e.g., "::hot::type/Result.Ok")
        let full_variant_type = format!("{}.{}", type_name, variant_name);
        let type_name_constant = self
            .program
            .add_constant(Constant::Val(Val::from(full_variant_type)));

        // Create $type key constant
        let type_key_constant = self.program.add_constant(Constant::Val(Val::from("$type")));

        let function_info = if let Some(ref type_ref) = variant.type_ref {
            // Type-reference variant: takes one argument (the data)
            // First validates data through the associated type's constructor using ConstructTyped,
            // then wraps with {$type: "::namespace/Type.Variant", $val: typed_data}

            // Resolve the associated type reference to fully qualified name
            let (resolved_type_ref, _) = self.resolve_type_path(type_ref);

            // Build type_info for the associated type (for ConstructTyped instruction)
            let mut associated_type_info = indexmap::IndexMap::new();
            associated_type_info.insert(Val::from("$type"), Val::from(resolved_type_ref.clone()));
            // Mark this as a nested type reference (no $all_fields means VM will call the constructor)
            let associated_type_info_constant = self
                .program
                .add_constant(Constant::Val(Val::Map(Box::new(associated_type_info))));

            let empty_map_constant = self.program.add_constant(Constant::Val(Val::map_empty()));
            let data_var_constant = self.program.add_constant(Constant::Val(Val::from("data")));
            let val_key_constant = self.program.add_constant(Constant::Val(Val::from("$val")));

            FunctionInfo {
                name: constructor_name.clone(),
                namespace: self.current_namespace.clone(),
                arity: 1,
                is_variadic: false,
                param_names: vec!["data".to_string()],
                param_types: vec![0],
                return_type: 0,
                lazy_params: vec![false],
                flow_type: None,
                instructions: vec![
                    // R1 = input data (from parameter)
                    Instruction::LoadVar {
                        dest: 1,
                        var_name: data_var_constant,
                    },
                    // R6 = extracted inner value (if input is typed, extract $val; otherwise use as-is)
                    Instruction::ExtractInnerVal { dest: 6, src: 1 },
                    // R7 = validate through associated type's constructor using ConstructTyped
                    // This calls e.g. EnumTestCircle({radius: 5.0}) to validate the data
                    // Returns {$type: "Circle", $val: {radius: 5.0}} on success, or error on failure
                    Instruction::ConstructTyped {
                        dest: 7,
                        src: 6,
                        type_info: associated_type_info_constant,
                    },
                    // If validation failed (error), return the error immediately
                    Instruction::ReturnIfErr { src: 7 },
                    // R8 = extract just the raw data from the typed result
                    // We want $val to contain raw data like {radius: 5.0}, not {$type: "Circle", $val: {radius: 5.0}}
                    Instruction::ExtractInnerVal { dest: 8, src: 7 },
                    // R2 = empty map for result
                    Instruction::LoadConst {
                        dest: 2,
                        constant: empty_map_constant,
                    },
                    // R3 = $type key
                    Instruction::LoadConst {
                        dest: 3,
                        constant: type_key_constant,
                    },
                    // R4 = full type name (variant type like "Shape.Circle")
                    Instruction::LoadConst {
                        dest: 4,
                        constant: type_name_constant,
                    },
                    // Set $type field on result map
                    Instruction::SetElement {
                        collection: 2,
                        index: 3, // $type key
                        value: 4, // full type name
                    },
                    // R5 = $val key
                    Instruction::LoadConst {
                        dest: 5,
                        constant: val_key_constant,
                    },
                    // Set $val field on result map using just the raw validated data
                    Instruction::SetElement {
                        collection: 2,
                        index: 5, // $val key
                        value: 8, // raw data extracted from typed result
                    },
                    // Return the result map
                    Instruction::Return { value: 2 },
                ],
                register_count: 9,
                source: None,
            }
        } else {
            // Unit variant: takes no arguments, returns {$type: "::namespace/Type.Variant"}
            let empty_map_constant = self.program.add_constant(Constant::Val(Val::map_empty()));

            FunctionInfo {
                name: constructor_name.clone(),
                namespace: self.current_namespace.clone(),
                arity: 0,
                is_variadic: false,
                param_names: vec![],
                param_types: vec![],
                return_type: 0,
                lazy_params: vec![],
                flow_type: None,
                instructions: vec![
                    // R0 = empty map for result
                    Instruction::LoadConst {
                        dest: 0,
                        constant: empty_map_constant,
                    },
                    // R1 = $type key
                    Instruction::LoadConst {
                        dest: 1,
                        constant: type_key_constant,
                    },
                    // R2 = full type name
                    Instruction::LoadConst {
                        dest: 2,
                        constant: type_name_constant,
                    },
                    // Set $type field on result map
                    Instruction::SetElement {
                        collection: 0,
                        index: 1, // $type key
                        value: 2, // full type name
                    },
                    // Return the result map
                    Instruction::Return { value: 0 },
                ],
                register_count: 3,
                source: None,
            }
        };

        self.program.functions.push(function_info);

        // Register function in mapping
        self.function_mapping
            .insert(constructor_name.clone(), function_id);
        let arity = if variant.type_ref.is_some() { 1 } else { 0 };
        let arity_key = format!("{}/{}", constructor_name, arity);
        self.function_mapping.insert(arity_key, function_id);

        tracing::trace!(
            "Compiler: Registered variant constructor '{}' with function ID {}",
            constructor_name,
            function_id
        );

        Ok(function_id)
    }

    /// Create a default type constructor for types without explicit constructors
    /// This constructor recursively constructs nested custom types.
    fn create_default_type_constructor(
        &mut self,
        constructor_name: &str,
        type_def: &crate::lang::ast::TypeDef,
    ) -> Result<FunctionId, String> {
        // Create a constructor function that takes a Val::Map and creates a typed object
        let function_id = self.program.functions.len() as FunctionId;

        // For core types, the constructor_name has __compiled suffix, but the type tag should be the base name
        let type_tag_name = if constructor_name.ends_with("__compiled") {
            constructor_name.trim_end_matches("__compiled").to_string()
        } else {
            constructor_name.to_string()
        };

        tracing::trace!(
            "Compiler: Creating default constructor '{}' with type tag '{}' (fields: {:?})",
            constructor_name,
            type_tag_name,
            type_def.fields.as_ref().map(|f| f.len())
        );

        // Build type_info constant for ConstructTyped instruction
        // Format: {$type: "TypeName", $fields: {field_name: {$type: "FieldType", ...}, ...}}
        let type_info = self.build_type_info(&type_tag_name, type_def);
        let type_info_constant = self.program.add_constant(Constant::Val(type_info));

        let function_info = FunctionInfo {
            name: constructor_name.to_string(),
            namespace: self.current_namespace.clone(),
            arity: 1, // Type constructors take one argument (the data map)
            is_variadic: false,
            param_names: vec!["data".to_string()],
            param_types: vec![0], // TypeId 0 as placeholder
            return_type: 0,       // TypeId 0 as placeholder
            lazy_params: vec![false],
            flow_type: None, // Type constructors are not flow functions
            instructions: vec![
                // Load the input data (parameter 0) into register 1
                Instruction::LoadVar {
                    dest: 1,
                    var_name: self.program.add_constant(Constant::Val(Val::from("data"))),
                },
                // Use ConstructTyped to recursively construct nested types
                Instruction::ConstructTyped {
                    dest: 0,
                    src: 1,
                    type_info: type_info_constant,
                },
                // Return the typed object
                Instruction::Return { value: 0 },
            ],
            register_count: 2, // Need registers 0 (return), 1 (data)
            source: None,      // Type constructor without custom fn definitions
        };

        self.program.functions.push(function_info);
        Ok(function_id)
    }

    /// Build type_info Val for ConstructTyped instruction
    /// Format: {
    ///   $type: "TypeName",
    ///   $fields: {field_name: {$type: "FieldType", ...}, ...},
    ///   $required: ["field1", "field2", ...],
    ///   $all_fields: ["field1", "field2", ...]  // All expected field names for validation
    /// }
    fn build_type_info(&self, type_name: &str, type_def: &crate::lang::ast::TypeDef) -> Val {
        let mut type_info = indexmap::IndexMap::new();
        type_info.insert(Val::from("$type"), Val::from(type_name));

        // Build field type info if this type has fields
        if let Some(ref fields) = type_def.fields {
            let mut fields_info = indexmap::IndexMap::new();
            let mut required_fields = Vec::new();
            let mut all_fields = Vec::new();

            for field in fields {
                let field_name = field.name.to_string();

                // Track all field names
                all_fields.push(Val::from(field_name.clone()));

                // Track required fields (non-optional)
                if !Self::type_expr_allows_null(&field.type_expr) {
                    required_fields.push(Val::from(field_name.clone()));
                }

                // Check if this field has a custom (non-builtin) type that needs construction
                let field_type_info = self.build_field_type_info_from_expr(&field.type_expr, false);
                if !Self::is_empty_map(&field_type_info) {
                    fields_info.insert(Val::from(field_name), field_type_info);
                }
            }

            if !fields_info.is_empty() {
                type_info.insert(Val::from("$fields"), Val::Map(Box::new(fields_info)));
            }

            if !required_fields.is_empty() {
                type_info.insert(Val::from("$required"), Val::Vec(required_fields));
            }

            // Always set $all_fields (even if empty) to distinguish top-level type_info
            // from nested type references which only have $type
            type_info.insert(Val::from("$all_fields"), Val::Vec(all_fields));
        } else {
            // No fields defined - still mark as top-level type_info with empty $all_fields
            type_info.insert(Val::from("$all_fields"), Val::Vec(vec![]));
        }

        Val::Map(Box::new(type_info))
    }

    fn build_field_type_info_from_expr(&self, type_expr: &TypeExpr, include_builtin: bool) -> Val {
        match type_expr {
            TypeExpr::Union(parts) => {
                let members = parts
                    .iter()
                    .map(|part| self.build_field_type_info_from_expr(part, true))
                    .filter(|info| !Self::is_empty_map(info))
                    .collect::<Vec<_>>();

                if members.is_empty() {
                    return Val::map_empty();
                }

                let mut union_info = indexmap::IndexMap::new();
                union_info.insert(Val::from("$union"), Val::Vec(members));
                Val::Map(Box::new(union_info))
            }
            TypeExpr::Optional(inner) => {
                let mut info = self.build_field_type_info_from_expr(inner, true);
                if let Val::Map(map) = &mut info {
                    map.insert(Val::from("$optional"), Val::Bool(true));
                }
                info
            }
            TypeExpr::Generic(name, params) if name == "Vec" && params.len() == 1 => {
                let item_type_info = self.build_field_type_info_from_expr(&params[0], false);
                if !Self::is_empty_map(&item_type_info) || include_builtin {
                    let mut vec_info = indexmap::IndexMap::new();
                    vec_info.insert(Val::from("$type"), Val::from("Vec"));
                    if !Self::is_empty_map(&item_type_info) {
                        vec_info.insert(Val::from("$item_type"), item_type_info);
                    }
                    Val::Map(Box::new(vec_info))
                } else {
                    Val::map_empty()
                }
            }
            TypeExpr::Generic(name, _) if name == "Map" => Val::map_empty(),
            TypeExpr::Builtin(builtin) => self.build_builtin_type_info(builtin, include_builtin),
            TypeExpr::Any => {
                if include_builtin {
                    Self::simple_type_info("Any")
                } else {
                    Val::map_empty()
                }
            }
            TypeExpr::Simple(name) | TypeExpr::Named(name) => {
                if Self::is_builtin_type_name(name) {
                    if include_builtin {
                        Self::simple_type_info(name)
                    } else {
                        Val::map_empty()
                    }
                } else {
                    self.build_custom_type_info(name)
                }
            }
            TypeExpr::Literal(lit) => {
                let mut type_info = indexmap::IndexMap::new();
                type_info.insert(Val::from("$literal"), Val::from(lit.to_string()));
                Val::Map(Box::new(type_info))
            }
            TypeExpr::Generic(name, _) => self.build_custom_type_info(name),
        }
    }

    fn build_builtin_type_info(&self, builtin: &BuiltinType, include_builtin: bool) -> Val {
        if !include_builtin {
            return Val::map_empty();
        }

        Self::simple_type_info(&builtin.to_string())
    }

    fn build_custom_type_info(&self, type_name: &str) -> Val {
        let (qualified_name, _found) = self.resolve_type_path(type_name);

        let mut type_info = indexmap::IndexMap::new();
        type_info.insert(Val::from("$type"), Val::from(qualified_name));
        Val::Map(Box::new(type_info))
    }

    fn simple_type_info(type_name: &str) -> Val {
        let mut type_info = indexmap::IndexMap::new();
        type_info.insert(Val::from("$type"), Val::from(type_name));
        Val::Map(Box::new(type_info))
    }

    fn type_expr_allows_null(type_expr: &TypeExpr) -> bool {
        match type_expr {
            TypeExpr::Optional(_) => true,
            TypeExpr::Union(parts) => parts.iter().any(Self::type_expr_allows_null),
            TypeExpr::Literal(crate::lang::ast::LiteralType::Null) => true,
            TypeExpr::Builtin(BuiltinType::Null) => true,
            TypeExpr::Simple(name) | TypeExpr::Named(name) => name == "Null",
            _ => false,
        }
    }

    /// Check if a type name is a builtin type
    fn is_builtin_type_name(type_name: &str) -> bool {
        matches!(
            type_name,
            "Str"
                | "String"
                | "Int"
                | "Integer"
                | "Dec"
                | "Number"
                | "Bool"
                | "Boolean"
                | "Null"
                | "Vec"
                | "Map"
                | "Byte"
                | "Bytes"
                | "Any"
                | "Result"
        )
    }

    /// Check if a Val is an empty map
    fn is_empty_map(val: &Val) -> bool {
        matches!(val, Val::Map(m) if m.is_empty())
    }

    /// Compile a MapWithSpread value: start with an empty map, merge spreads and
    /// assoc base entries in insertion order.
    fn compile_map_with_spread(
        &mut self,
        base_entries: &IndexMap<Val, Val>,
        spread_entries: &[(usize, Value)],
    ) -> Result<RegisterId, String> {
        let dest_reg = self.allocate_register();

        // Start with an empty map
        let empty_map_constant = self.program.add_constant(Constant::Val(Val::map_empty()));
        self.emit_instruction(Instruction::LoadConst {
            dest: dest_reg,
            constant: empty_map_constant,
        });

        // Build a combined sequence of operations ordered by insertion index.
        // spread_entries carry their original insertion index; base_entries are
        // interleaved at the positions *not* occupied by spreads.
        let total = base_entries.len() + spread_entries.len();
        let spread_positions: std::collections::BTreeMap<usize, &Value> = spread_entries
            .iter()
            .map(|(idx, val)| (*idx, val))
            .collect();

        let mut base_iter = base_entries.iter();

        for i in 0..total {
            if let Some(spread_val) = spread_positions.get(&i) {
                // Evaluate spread value and merge into dest
                let spread_reg = self.compile_value(spread_val)?;
                self.emit_instruction(Instruction::MergeMaps {
                    dest: dest_reg,
                    source: spread_reg,
                });
            } else if let Some((key, value)) = base_iter.next() {
                // Compile key
                let key_reg = self.allocate_register();
                let key_constant = self.program.add_constant(Constant::Val(key.clone()));
                self.emit_instruction(Instruction::LoadConst {
                    dest: key_reg,
                    constant: key_constant,
                });

                // Compile value (may contain boxed AST nodes)
                let value_reg = if let Val::Box(b) = value {
                    if let Some(ast_node) = b.as_any().downcast_ref::<crate::lang::ast::AstNode>() {
                        self.compile_value(&ast_node.0)?
                    } else if let Some(fn_call) =
                        b.as_any().downcast_ref::<crate::lang::ast::FnCall>()
                    {
                        self.compile_function_call(fn_call)?
                    } else {
                        let val_reg = self.allocate_register();
                        let val_constant = self.program.add_constant(Constant::Val(value.clone()));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: val_reg,
                            constant: val_constant,
                        });
                        val_reg
                    }
                } else if Self::val_contains_boxed_nodes(value) {
                    let val_reg = self.allocate_register();
                    self.compile_literal_value(value, val_reg)?;
                    val_reg
                } else {
                    let val_reg = self.allocate_register();
                    let val_constant = self.program.add_constant(Constant::Val(value.clone()));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: val_reg,
                        constant: val_constant,
                    });
                    val_reg
                };

                self.emit_instruction(Instruction::SetElement {
                    collection: dest_reg,
                    index: key_reg,
                    value: value_reg,
                });
            }
        }

        Ok(dest_reg)
    }

    /// Compile a template literal like `"Hello ${name}"`
    fn compile_template_literal(
        &mut self,
        template: &crate::lang::ast::TemplateLiteral,
    ) -> Result<RegisterId, String> {
        tracing::trace!(
            "Compiler: Compiling template literal with {} parts",
            template.parts.len()
        );

        // Compile each part into a register
        let mut part_registers = Vec::new();

        for part in &template.parts {
            match part {
                crate::lang::ast::TemplatePart::Text(text) => {
                    // Compile text as a string constant
                    let reg = self.allocate_register();
                    let constant_id = self
                        .program
                        .add_constant(Constant::Val(Val::from(text.clone())));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: reg,
                        constant: constant_id,
                    });
                    part_registers.push(reg);
                }
                crate::lang::ast::TemplatePart::Expression(expr) => {
                    // Compile the expression and add it to the parts
                    let expr_reg = self.compile_value(expr.as_ref())?;
                    part_registers.push(expr_reg);
                }
            }
        }

        // Use TemplateInterpolate instruction to combine all parts
        let dest_reg = self.allocate_register();

        if part_registers.is_empty() {
            // Empty template literal
            let constant_id = self.program.add_constant(Constant::Val(Val::from("")));
            self.emit_instruction(Instruction::LoadConst {
                dest: dest_reg,
                constant: constant_id,
            });
        } else {
            // Ensure parts are in consecutive registers
            let parts_start = part_registers[0];
            for (i, &part_reg) in part_registers.iter().enumerate() {
                let target_reg = parts_start + i as RegisterId;
                if part_reg != target_reg {
                    self.emit_instruction(Instruction::Move {
                        dest: target_reg,
                        src: part_reg,
                    });
                }
            }

            self.emit_instruction(Instruction::TemplateInterpolate {
                dest: dest_reg,
                parts_start,
                parts_count: part_registers.len() as u8,
            });
        }

        tracing::trace!(
            "Compiler: Compiled template literal with {} parts",
            part_registers.len()
        );

        Ok(dest_reg)
    }

    /// Compile a type implementation
    /// When a type implementation arrow targets a dotted variant
    /// (e.g. `Bird -> Animal.Bird`), the parent enum's `TypeRef`
    /// constant probably does not yet know about that variant, and
    /// no `Animal.Bird` constructor function has been emitted. This
    /// helper extends the existing `TypeRef` constant in place and
    /// emits a variant constructor so the synthesized wrap function
    /// body — `Animal.Bird($src)` — resolves and dispatches at
    /// runtime exactly like a declared variant.
    fn ensure_arrow_variant_registered(
        &mut self,
        target_type: &str,
        source_type: &str,
    ) -> Result<(), String> {
        let Some((enum_path, variant_name)) = target_type.rsplit_once('.') else {
            return Ok(());
        };

        let enum_full = if enum_path.starts_with("::") {
            enum_path.to_string()
        } else {
            format!("{}/{}", self.current_namespace, enum_path)
        };

        let mut idx_to_update: Option<usize> = None;
        let mut existing_variants: indexmap::IndexMap<String, Option<String>> =
            indexmap::IndexMap::new();
        let mut existing_name = String::new();
        for (idx, c) in self.program.constants.iter().enumerate() {
            if let Constant::VariantTypeRef(tr) = c
                && (tr.name == enum_full || tr.name.ends_with(&format!("/{}", enum_path)))
            {
                existing_variants = tr.variants.clone();
                existing_name = tr.name.clone();
                idx_to_update = Some(idx);
                break;
            }
        }

        let Some(idx) = idx_to_update else {
            return Ok(());
        };

        if existing_variants.contains_key(variant_name) {
            return Ok(());
        }

        let (resolved_source, _) = self.resolve_type_path(source_type);
        existing_variants.insert(variant_name.to_string(), Some(resolved_source));

        let updated_tr = crate::lang::refs::TypeRef::new(existing_name.clone(), existing_variants);
        self.program.constants[idx] = Constant::VariantTypeRef(updated_tr);

        let synthesized_variant = crate::lang::ast::VariantDef {
            name: crate::lang::ast::Sym::String(variant_name.to_string()),
            type_ref: Some(source_type.to_string()),
        };
        self.compile_variant_constructor(&existing_name, &synthesized_variant)?;

        Ok(())
    }

    fn compile_type_implementation(
        &mut self,
        type_impl: &crate::lang::ast::TypeImplementation,
    ) -> Result<RegisterId, String> {
        tracing::trace!(
            "Compiler: Compiling type implementation {} -> {}",
            type_impl.source_type,
            type_impl.target_type
        );

        // For dotted targets like `Animal.Bird`, ensure the parent
        // enum's TypeRef knows about the variant and a constructor
        // function exists *before* we compile the (possibly
        // synthesized) wrap function body that calls `Animal.Bird($src)`.
        self.ensure_arrow_variant_registered(&type_impl.target_type, &type_impl.source_type)?;

        // Type implementations define how one type converts to another
        // For now, compile the implementation function and store it
        let impl_reg = self.compile_value(&type_impl.implementation)?;

        // Register the type implementation in the appropriate scope
        // Normalize target type name to short form for consistent lookup
        let target_type_short =
            if let Some(last_part) = type_impl.target_type.split('/').next_back() {
                last_part.to_string()
            } else {
                type_impl.target_type.clone()
            };
        // Resolve the arrow's source type through declaration-site scope
        // (local declaration, import/alias, core auto-import). Registering
        // the resolved qualified name is what keeps two namespaces'
        // same-short-named types from colliding in the global registry: a
        // short name only means something in the scope that declared the
        // arrow, so the registry must not be keyed by it.
        let (qualified_source, _source_found) = self.resolve_type_path(&type_impl.source_type);
        let key_resolved = (qualified_source.clone(), target_type_short.clone());
        let impl_name = format!(
            "impl_{}_{}",
            type_impl.source_type,
            type_impl.target_type.replace("/", "_").replace("::", "_")
        );
        // Register the implementation under its namespace-qualified name.
        // The impl function is compiled into the declaring namespace, so a
        // bare name only resolves for callers in that namespace (or via the
        // bare function_mapping entries, which do not survive bytecode-cache
        // restoration across compilation units). Dispatch calls
        // execute_function_call_by_qualified_name, which handles qualified
        // names in every unit.
        let qualified_impl_name = format!("{}/{}", self.current_namespace, impl_name);

        // Handle type implementations based on scope
        // Use current_function_name instead of params - a zero-arg function is still a function scope!
        let is_function_scope = self.current_function_name.is_some();

        if is_function_scope {
            tracing::trace!(
                "Compiler: Registering function-scoped type implementation: {:?} -> {}",
                key_resolved,
                impl_name
            );

            // For function-scoped implementations, we need to emit bytecode instructions
            // that will register the implementation in the VM's current scope when executed
            let source_type_id = self.program.add_string_ref(qualified_source.clone());
            let target_type_id = self.program.add_string_ref(target_type_short.clone());
            let impl_name_id = self.program.add_string_ref(qualified_impl_name.clone());
            self.emit_instruction(
                crate::lang::bytecode::Instruction::RegisterLocalImplementation {
                    source_type: source_type_id,
                    target_type: target_type_id,
                    implementation_function_name: impl_name_id,
                },
            );
        } else {
            tracing::trace!(
                "Compiler: Registering namespace-scoped type implementation: {:?} -> {}",
                key_resolved,
                impl_name
            );

            // Register under the scope-resolved qualified source name only.
            // No short-name key: the runtime registry is global, and a bare
            // source name is only meaningful in the declaring scope.
            self.type_implementations
                .insert(key_resolved.clone(), qualified_impl_name.clone());
        }

        // Also register as a function mapping in the current namespace so VM can call it by name
        // Compile the implementation under a qualified name
        let qualified_name = qualified_impl_name;
        if let crate::lang::ast::Value::Fn(fn_defs_vec) = &type_impl.implementation {
            // Value::Fn can contain multiple arities; compile each
            for fn_def in fn_defs_vec {
                // Type implementations don't have Var source, use None
                let function_id =
                    self.compile_function_definition_and_get_id(&qualified_name, fn_def, None)?;
                // Map name and arity
                self.function_mapping
                    .insert(qualified_name.clone(), function_id);
                let arity = fn_def.args.args.len();
                self.function_mapping
                    .insert(format!("{}/{}", qualified_name, arity), function_id);
                // Also map the unqualified impl name to help local resolution
                self.function_mapping
                    .entry(impl_name.clone())
                    .or_insert(function_id);
                self.function_mapping
                    .entry(format!("{}/{}", impl_name, arity))
                    .or_insert(function_id);
            }
        }

        // Store the implementation as a variable so VM can find it via unified lookup
        tracing::trace!(
            "Compiler: Storing implementation variable '{}' in namespace '{}'",
            impl_name,
            self.current_namespace
        );
        self.store_variable(&impl_name, impl_reg);

        tracing::trace!(
            "Compiler: Registered type implementation {} -> {}",
            type_impl.source_type,
            type_impl.target_type
        );

        Ok(impl_reg)
    }

    /// Recursively check if a Val contains any boxed AstNodes or FnCalls that need compilation
    fn val_contains_boxed_nodes(val: &Val) -> bool {
        match val {
            Val::Box(b) => {
                b.as_any()
                    .downcast_ref::<crate::lang::ast::AstNode>()
                    .is_some()
                    || b.as_any()
                        .downcast_ref::<crate::lang::ast::FnCall>()
                        .is_some()
            }
            Val::Map(map) => map.values().any(Self::val_contains_boxed_nodes),
            Val::Vec(vec) => vec.iter().any(Self::val_contains_boxed_nodes),
            _ => false,
        }
    }

    /// Compile a literal value
    fn compile_literal_value(&mut self, val: &Val, dest_reg: RegisterId) -> Result<(), String> {
        // Check if this value contains boxed AstNodes that need to be compiled
        match val {
            Val::Map(map) => {
                // Recursively check if the map contains any boxed AstNodes or FnCalls
                let has_boxed_nodes = map.values().any(Self::val_contains_boxed_nodes);

                if has_boxed_nodes {
                    // This map has boxed AST nodes - compile dynamically
                    tracing::trace!(
                        "Compiler: Map literal contains boxed AST nodes, compiling dynamically"
                    );

                    // Create an empty map
                    let empty_map_constant =
                        self.program.add_constant(Constant::Val(Val::map_empty()));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: dest_reg,
                        constant: empty_map_constant,
                    });

                    // For each key-value pair, compile the value and set it in the map
                    for (key, value) in map.iter() {
                        let key_reg = self.allocate_register();
                        let key_constant = self.program.add_constant(Constant::Val(key.clone()));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: key_reg,
                            constant: key_constant,
                        });

                        let value_reg = if let Val::Box(b) = value {
                            if let Some(ast_node) =
                                b.as_any().downcast_ref::<crate::lang::ast::AstNode>()
                            {
                                // Compile the AST node
                                self.compile_value(&ast_node.0)?
                            } else if let Some(fn_call) =
                                b.as_any().downcast_ref::<crate::lang::ast::FnCall>()
                            {
                                // Compile the function call
                                self.compile_function_call(fn_call)?
                            } else {
                                // Other boxed value - store as constant
                                let val_reg = self.allocate_register();
                                let val_constant =
                                    self.program.add_constant(Constant::Val(value.clone()));
                                self.emit_instruction(Instruction::LoadConst {
                                    dest: val_reg,
                                    constant: val_constant,
                                });
                                val_reg
                            }
                        } else if Self::val_contains_boxed_nodes(value) {
                            // Nested map/vec with boxed nodes - compile recursively
                            let val_reg = self.allocate_register();
                            self.compile_literal_value(value, val_reg)?;
                            val_reg
                        } else {
                            // Regular value - store as constant
                            let val_reg = self.allocate_register();
                            let val_constant =
                                self.program.add_constant(Constant::Val(value.clone()));
                            self.emit_instruction(Instruction::LoadConst {
                                dest: val_reg,
                                constant: val_constant,
                            });
                            val_reg
                        };

                        // Set the element in the map
                        self.emit_instruction(Instruction::SetElement {
                            collection: dest_reg,
                            index: key_reg,
                            value: value_reg,
                        });
                    }

                    return Ok(());
                }

                // No boxed nodes - store as constant
                let constant_id = self.program.add_constant(Constant::Val(val.clone()));
                self.emit_instruction(Instruction::LoadConst {
                    dest: dest_reg,
                    constant: constant_id,
                });
                Ok(())
            }
            Val::Vec(vec) => {
                // Recursively check if the vector contains any boxed AstNodes or FnCalls
                let has_boxed_nodes = vec.iter().any(Self::val_contains_boxed_nodes);

                if has_boxed_nodes {
                    // This vector has boxed AST nodes - compile dynamically
                    tracing::trace!(
                        "Compiler: Vector literal contains boxed AST nodes, compiling dynamically"
                    );

                    // Compile all elements into consecutive registers
                    // Track which elements are spread (VariadicExpansion)
                    let elements_start = self.allocate_register();
                    let mut element_regs = Vec::new();
                    let mut spread_mask: u64 = 0;

                    for (i, value) in vec.iter().enumerate() {
                        let (value_reg, is_spread) = if let Val::Box(b) = value {
                            if let Some(ast_node) =
                                b.as_any().downcast_ref::<crate::lang::ast::AstNode>()
                            {
                                // Check if the AST node is a VariadicExpansion
                                let is_spread = matches!(&ast_node.0, Value::VariadicExpansion(_));
                                // Compile the AST node
                                (self.compile_value(&ast_node.0)?, is_spread)
                            } else if let Some(fn_call) =
                                b.as_any().downcast_ref::<crate::lang::ast::FnCall>()
                            {
                                // Compile the function call
                                (self.compile_function_call(fn_call)?, false)
                            } else {
                                // Other boxed value - store as constant
                                let val_reg = self.allocate_register();
                                let val_constant =
                                    self.program.add_constant(Constant::Val(value.clone()));
                                self.emit_instruction(Instruction::LoadConst {
                                    dest: val_reg,
                                    constant: val_constant,
                                });
                                (val_reg, false)
                            }
                        } else if Self::val_contains_boxed_nodes(value) {
                            // Nested map/vec with boxed nodes - compile recursively
                            let val_reg = self.allocate_register();
                            self.compile_literal_value(value, val_reg)?;
                            (val_reg, false)
                        } else {
                            // Regular value - store as constant
                            let val_reg = self.allocate_register();
                            let val_constant =
                                self.program.add_constant(Constant::Val(value.clone()));
                            self.emit_instruction(Instruction::LoadConst {
                                dest: val_reg,
                                constant: val_constant,
                            });
                            (val_reg, false)
                        };
                        if is_spread {
                            spread_mask |= 1 << i;
                        }
                        element_regs.push(value_reg);
                    }

                    // Move elements to consecutive registers starting at elements_start
                    for (i, elem_reg) in element_regs.iter().enumerate() {
                        let target_reg = elements_start + i as RegisterId;
                        if *elem_reg != target_reg {
                            self.emit_instruction(Instruction::Move {
                                dest: target_reg,
                                src: *elem_reg,
                            });
                        }
                    }

                    // Create vector from elements
                    // Use MakeVecWithSpread if any elements need spreading
                    if spread_mask != 0 {
                        self.emit_instruction(Instruction::MakeVecWithSpread {
                            dest: dest_reg,
                            elements_start,
                            count: vec.len() as u16,
                            spread_mask,
                        });
                        tracing::trace!(
                            "Compiler: Created Vec with spread (spread_mask: {:b})",
                            spread_mask
                        );
                    } else {
                        self.emit_instruction(Instruction::MakeVec {
                            dest: dest_reg,
                            elements_start,
                            count: vec.len() as u16,
                        });
                    }

                    return Ok(());
                }

                // No boxed nodes - store as constant
                let constant_id = self.program.add_constant(Constant::Val(val.clone()));
                self.emit_instruction(Instruction::LoadConst {
                    dest: dest_reg,
                    constant: constant_id,
                });
                Ok(())
            }
            _ => {
                // For other value types, store as constant
                // Strings are just strings, even if they look like "::namespace/function" or "$variable"
                let constant_id = self.program.add_constant(Constant::Val(val.clone()));
                self.emit_instruction(Instruction::LoadConst {
                    dest: dest_reg,
                    constant: constant_id,
                });
                Ok(())
            }
        }
    }

    /// Compile deep path access (like colors.red or data.users[0].name)
    fn compile_deep_path_access(
        &mut self,
        base_reg: RegisterId,
        deep_path: &crate::lang::ast::DeepPath,
    ) -> Result<RegisterId, String> {
        self.compile_deep_path_recursive(base_reg, deep_path)
    }

    /// Recursively compile deep path access
    fn compile_deep_path_recursive(
        &mut self,
        current_reg: RegisterId,
        deep_path: &crate::lang::ast::DeepPath,
    ) -> Result<RegisterId, String> {
        match deep_path {
            crate::lang::ast::DeepPath::Key(key) => {
                // Map key access: obj.key
                let result_reg = self.allocate_register();
                let key_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(key.clone())));

                self.emit_instruction(Instruction::DotAccess {
                    dest: result_reg,
                    object: current_reg,
                    property: key_constant,
                });

                Ok(result_reg)
            }
            crate::lang::ast::DeepPath::Index(index) => {
                // Vector index access: obj[index]
                // Use DotAccess with the index as a string property for now
                // The VM will handle the conversion for vector indexing
                let result_reg = self.allocate_register();
                let index_str = index.to_string();
                let index_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(index_str)));

                self.emit_instruction(Instruction::DotAccess {
                    dest: result_reg,
                    object: current_reg,
                    property: index_constant,
                });

                Ok(result_reg)
            }
            crate::lang::ast::DeepPath::DynamicIndex(var_name) => {
                // Dynamic index access: obj[var] where var holds the index/key
                // First load the variable containing the index/key
                let index_reg = self.allocate_register();
                let var_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(var_name.clone())));

                self.emit_instruction(Instruction::LoadVar {
                    dest: index_reg,
                    var_name: var_constant,
                });

                // Use DynamicDotAccess instruction for runtime index resolution
                let result_reg = self.allocate_register();
                self.emit_instruction(Instruction::DynamicDotAccess {
                    dest: result_reg,
                    object: current_reg,
                    property: index_reg,
                });

                Ok(result_reg)
            }
            crate::lang::ast::DeepPath::Chain(left, right) => {
                // Chained access: obj.key1.key2 or obj[0].key
                let intermediate_reg = self.compile_deep_path_recursive(current_reg, left)?;
                self.compile_deep_path_recursive(intermediate_reg, right)
            }
            crate::lang::ast::DeepPath::Append => {
                // Append is only valid for setting, not for reading
                // Return the current register (this shouldn't normally be reached)
                Ok(current_reg)
            }
        }
    }

    /// Compile deep set assignment (e.g., hot.db.uri "value")
    fn compile_deep_set_assignment(
        &mut self,
        var_name: &str,
        deep_path: &crate::lang::ast::DeepPath,
        value_reg: RegisterId,
    ) -> Result<(), String> {
        tracing::trace!(
            "Compiler: Compiling deep set assignment for '{}' with path {:?}",
            var_name,
            deep_path
        );

        // 1. Load the existing variable (or create default if it doesn't exist)
        let base_reg = self.allocate_register();
        let var_name_constant = self
            .program
            .add_constant(Constant::Val(Val::from(var_name.to_string())));

        // Determine the default value based on the deep path
        // If the first path segment is an Append, default to empty vec; otherwise default to empty map
        let default_val = if matches!(deep_path, crate::lang::ast::DeepPath::Append) {
            Val::vec_empty()
        } else {
            Val::map_empty()
        };

        // Try to load the existing variable, but don't fail if it doesn't exist
        let default_constant = self.program.add_constant(Constant::Val(default_val));
        self.emit_instruction(Instruction::LoadVarOrDefault {
            dest: base_reg,
            var_name: var_name_constant,
            default_value: default_constant,
        });

        // 2. Set the deep property on the loaded/default variable
        self.compile_deep_set_recursive(base_reg, deep_path, value_reg)?;

        // 3. Store the modified variable back
        self.store_variable(var_name, base_reg);

        Ok(())
    }

    /// Recursively compile deep set operations
    fn compile_deep_set_recursive(
        &mut self,
        base_reg: RegisterId,
        deep_path: &crate::lang::ast::DeepPath,
        value_reg: RegisterId,
    ) -> Result<(), String> {
        tracing::trace!(
            "Compiler: compile_deep_set_recursive called with path: {:?}",
            deep_path
        );
        match deep_path {
            crate::lang::ast::DeepPath::Key(key) => {
                // Simple key assignment: obj.key = value
                let key_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(key.clone())));

                self.emit_instruction(Instruction::DotSet {
                    object: base_reg,
                    property: key_constant,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Index(index) => {
                // Vector index assignment: obj[index] = value
                let index_str = index.to_string();
                let index_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(index_str)));

                self.emit_instruction(Instruction::DotSet {
                    object: base_reg,
                    property: index_constant,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::DynamicIndex(var_name) => {
                // Dynamic index assignment: obj[var] = value
                // Load the variable containing the index
                let index_reg = self.allocate_register();
                let var_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(var_name.clone())));

                self.emit_instruction(Instruction::LoadVar {
                    dest: index_reg,
                    var_name: var_constant,
                });

                // Use DynamicDotSet for runtime index assignment
                self.emit_instruction(Instruction::DynamicDotSet {
                    object: base_reg,
                    property: index_reg,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Append => {
                // Vector append: obj[] = value (pushes value to the end of the vector)
                self.emit_instruction(Instruction::VecAppend {
                    vec: base_reg,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Chain(left, right) => {
                // Chained assignment: obj.key1.key2 = value
                // We need to navigate to the parent path and then set the final property

                // Create a register to hold the intermediate object
                let intermediate_reg = self.allocate_register();

                // Check if right is Append - if so, left should create a Vec as default
                let right_is_append = matches!(**right, crate::lang::ast::DeepPath::Append);

                // First, navigate to the parent object using the left path
                // This will recursively handle nested chains
                self.compile_deep_path_access_or_create_with_default(
                    base_reg,
                    left,
                    intermediate_reg,
                    right_is_append,
                )?;

                // Now set the property in the intermediate object
                self.compile_deep_set_recursive(intermediate_reg, right, value_reg)?;

                // Finally, store the modified intermediate object back
                // We need to set it back at the left path
                self.compile_deep_path_set_back(base_reg, left, intermediate_reg)?;

                Ok(())
            }
        }
    }

    /// Access a deep path, creating empty maps/vecs as needed
    /// The `next_is_append` parameter indicates if the next operation will be an Append,
    /// in which case we should create an empty Vec instead of an empty Map as the default
    fn compile_deep_path_access_or_create(
        &mut self,
        base_reg: RegisterId,
        deep_path: &crate::lang::ast::DeepPath,
        dest_reg: RegisterId,
    ) -> Result<(), String> {
        self.compile_deep_path_access_or_create_with_default(base_reg, deep_path, dest_reg, false)
    }

    /// Access a deep path, creating empty maps/vecs as needed
    /// The `next_is_append` parameter indicates if the next operation will be an Append,
    /// in which case we should create an empty Vec instead of an empty Map as the default
    fn compile_deep_path_access_or_create_with_default(
        &mut self,
        base_reg: RegisterId,
        deep_path: &crate::lang::ast::DeepPath,
        dest_reg: RegisterId,
        next_is_append: bool,
    ) -> Result<(), String> {
        match deep_path {
            crate::lang::ast::DeepPath::Key(key) => {
                // Simple key access with default based on what comes next
                let key_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(key.clone())));
                let default_constant = if next_is_append {
                    self.program.add_constant(Constant::Val(Val::vec_empty()))
                } else {
                    self.program.add_constant(Constant::Val(Val::map_empty()))
                };

                self.emit_instruction(Instruction::DotAccessOrDefault {
                    dest: dest_reg,
                    object: base_reg,
                    property: key_constant,
                    default_value: default_constant,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Chain(left, right) => {
                // Navigate to left first, then to right
                // Check if right is Append - if so, left should create a Vec as default
                let right_is_append = matches!(**right, crate::lang::ast::DeepPath::Append);
                let intermediate_reg = self.allocate_register();
                self.compile_deep_path_access_or_create_with_default(
                    base_reg,
                    left,
                    intermediate_reg,
                    right_is_append,
                )?;
                self.compile_deep_path_access_or_create_with_default(
                    intermediate_reg,
                    right,
                    dest_reg,
                    next_is_append,
                )?;
                Ok(())
            }
            crate::lang::ast::DeepPath::Index(index) => {
                // Index access with default based on what comes next
                let index_str = index.to_string();
                let index_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(index_str)));
                let default_constant = if next_is_append {
                    self.program.add_constant(Constant::Val(Val::vec_empty()))
                } else {
                    self.program.add_constant(Constant::Val(Val::map_empty()))
                };

                self.emit_instruction(Instruction::DotAccessOrDefault {
                    dest: dest_reg,
                    object: base_reg,
                    property: index_constant,
                    default_value: default_constant,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::DynamicIndex(var_name) => {
                // Dynamic index access with default empty map
                let index_reg = self.allocate_register();
                let var_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(var_name.clone())));

                self.emit_instruction(Instruction::LoadVar {
                    dest: index_reg,
                    var_name: var_constant,
                });

                self.emit_instruction(Instruction::DynamicDotAccess {
                    dest: dest_reg,
                    object: base_reg,
                    property: index_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Append => {
                // Append doesn't access, it's only used for setting
                // If we reach here in an access context, just load an empty vec
                let default_vec_constant =
                    self.program.add_constant(Constant::Val(Val::vec_empty()));
                self.emit_instruction(Instruction::LoadConst {
                    dest: dest_reg,
                    constant: default_vec_constant,
                });
                Ok(())
            }
        }
    }

    /// Set a value back into a deep path
    fn compile_deep_path_set_back(
        &mut self,
        base_reg: RegisterId,
        deep_path: &crate::lang::ast::DeepPath,
        value_reg: RegisterId,
    ) -> Result<(), String> {
        match deep_path {
            crate::lang::ast::DeepPath::Key(key) => {
                // Simple key assignment
                let key_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(key.clone())));

                self.emit_instruction(Instruction::DotSet {
                    object: base_reg,
                    property: key_constant,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Chain(left, right) => {
                // For chains, we need to navigate to the parent and then set
                let parent_reg = self.allocate_register();
                self.compile_deep_path_access_or_create(base_reg, left, parent_reg)?;
                self.compile_deep_path_set_back(parent_reg, right, value_reg)?;

                // Now set the modified parent back
                self.compile_deep_path_set_back(base_reg, left, parent_reg)?;

                Ok(())
            }
            crate::lang::ast::DeepPath::Index(index) => {
                // Index assignment (treating as string key)
                let index_str = index.to_string();
                let index_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(index_str)));

                self.emit_instruction(Instruction::DotSet {
                    object: base_reg,
                    property: index_constant,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::DynamicIndex(var_name) => {
                // Dynamic index assignment
                let index_reg = self.allocate_register();
                let var_constant = self
                    .program
                    .add_constant(Constant::Val(Val::from(var_name.clone())));

                self.emit_instruction(Instruction::LoadVar {
                    dest: index_reg,
                    var_name: var_constant,
                });

                self.emit_instruction(Instruction::DynamicDotSet {
                    object: base_reg,
                    property: index_reg,
                    value: value_reg,
                });

                Ok(())
            }
            crate::lang::ast::DeepPath::Append => {
                // Append to vector
                self.emit_instruction(Instruction::VecAppend {
                    vec: base_reg,
                    value: value_reg,
                });

                Ok(())
            }
        }
    }

    /// Allocate a new register
    fn allocate_register(&mut self) -> RegisterId {
        let reg = self.next_register;
        self.next_register += 1;
        reg
    }

    /// Emit an instruction to the entry point
    fn emit_instruction(&mut self, instruction: Instruction) {
        tracing::trace!("Emitting instruction: {:?}", instruction);

        // Track source location for this instruction
        let instruction_pointer = self.program.entry_point.len();
        if let Some(ref location) = self.current_source_location {
            tracing::trace!(
                "Adding source location for instruction {}: {}:{}:{}",
                instruction_pointer,
                location.file.as_ref().unwrap_or(&"<unknown>".to_string()),
                location.line,
                location.column
            );
            self.program
                .source_map
                .add_location(instruction_pointer, location.clone());
        } else {
            tracing::trace!(
                "No source location set for instruction {}",
                instruction_pointer
            );
        }

        self.program.entry_point.push(instruction);
    }

    /// Store a variable with basic metadata for emitter events
    fn store_variable(&mut self, name: &str, register: RegisterId) {
        self.store_variable_with_meta(name, register, None);
    }

    /// Store a variable with optional AST metadata for emitter events
    fn store_variable_with_meta(
        &mut self,
        name: &str,
        register: RegisterId,
        ast_meta: Option<&Meta>,
    ) {
        tracing::trace!("Storing variable '{}' in register {}", name, register);

        // Store the variable name as a constant
        let var_name_constant = self
            .program
            .add_constant(Constant::Val(Val::from(name.to_string())));

        // Create metadata for emitter events, including AST metadata if available
        let metadata = Some(Box::new(crate::lang::bytecode::VariableMetadata {
            name: name.to_string(),
            namespace: self.current_namespace.clone(),
            static_scope: Some(self.current_namespace.clone()), // Use namespace as static scope for now
            meta: ast_meta.map(|m| m.val.clone()),
            source: self.current_source_location.clone(),
        }));

        // Generate a StoreVar instruction to store the variable
        self.emit_instruction(Instruction::StoreVar {
            var_name: var_name_constant,
            value: register,
            metadata,
        });

        tracing::trace!(
            "Compiler: Generated StoreVar instruction for '{}' with var_name_constant={}, value_register=r{}, has_metadata=true, has_ast_meta={}",
            name,
            var_name_constant,
            register,
            ast_meta.is_some()
        );
    }

    /// Store a variable in a specific namespace (for type definitions in Pass 1)
    /// This uses StoreGlobal to avoid relying on VM's current_namespace state
    fn store_variable_global(&mut self, namespace: &str, name: &str, register: RegisterId) {
        tracing::trace!(
            "Storing global variable '{}' in namespace '{}' from register {}",
            name,
            namespace,
            register
        );

        // Store the namespace as a constant
        let namespace_constant = self
            .program
            .add_constant(Constant::Val(Val::from(namespace.to_string())));

        // Store the variable name as a constant
        let var_name_constant = self
            .program
            .add_constant(Constant::Val(Val::from(name.to_string())));

        // Generate a StoreGlobal instruction
        self.emit_instruction(Instruction::StoreGlobal {
            namespace: namespace_constant,
            var_name: var_name_constant,
            value: register,
        });

        // Also register in the namespace registry so it can be looked up at compile-time
        // This is important for resolving type names in match expressions
        let full_name = format!("{}/{}", namespace, name);
        let var_info = crate::lang::bytecode::VariableInfo {
            name: full_name.clone(),
            var_type: crate::lang::bytecode::VariableType::TypeConstructor,
            metadata: None,
            function_id: None,
        };
        self.program.namespaces.add_variable(namespace, var_info);

        tracing::trace!(
            "Compiler: Generated StoreGlobal instruction for '{}' in namespace '{}'",
            name,
            namespace
        );
    }

    /// Analyze free variables in a lambda body for closure capture
    fn analyze_free_variables(&self, body: &Value, parameters: &[String]) -> Vec<String> {
        let mut free_vars = AHashSet::new();
        let mut bound_vars = AHashSet::new();

        // Parameters are bound variables
        for param in parameters {
            bound_vars.insert(param.clone());
        }

        self.collect_free_variables(body, &mut free_vars, &mut bound_vars);

        // Convert to sorted vector for deterministic behavior
        let mut result: Vec<String> = free_vars.into_iter().collect();
        result.sort();
        result
    }

    /// Recursively collect free variables from AST nodes
    fn collect_free_variables(
        &self,
        value: &Value,
        free_vars: &mut AHashSet<String>,
        bound_vars: &mut AHashSet<String>,
    ) {
        match value {
            Value::Ref(Ref::Var(var_ref)) => {
                let var_name = var_ref.var.sym.name();
                // If variable is not bound and not excluded from capture, it's free
                if !bound_vars.contains(var_name)
                    && !self.should_exclude_from_capture(var_name, var_ref)
                {
                    free_vars.insert(var_name.to_string());
                }
            }
            Value::FnCall(fn_call) => {
                // Check function name
                self.collect_free_variables(&fn_call.function, free_vars, bound_vars);
                // Check arguments
                for arg in &fn_call.args {
                    self.collect_free_variables(&arg.value, free_vars, bound_vars);
                }
            }
            Value::Flow(flow) => {
                // Handle flow expressions, detecting variable assignment pattern.
                // Two encodings of `name expr` exist in the AST:
                //   1) Two adjacent Flow expressions: `[Ref(name), expr]`
                //   2) A single `MultipleValues([Ref(name), expr, ...])` element
                //      (this is how the parser emits same-line bindings like
                //      `s find-skill(skills, name)`).
                // Both must add `name` to bound_vars BEFORE analyzing the rhs.
                let mut i = 0;
                while i < flow.expressions.len() {
                    // Encoding 2: MultipleValues([Ref(name), ...rhs])
                    if let Value::MultipleValues(values) = &flow.expressions[i]
                        && values.len() >= 2
                        && let Value::Ref(Ref::Var(var_ref)) = &values[0]
                    {
                        let var_name = var_ref.var.sym.name();
                        bound_vars.insert(var_name.to_string());
                        for v in &values[1..] {
                            self.collect_free_variables(v, free_vars, bound_vars);
                        }
                        i += 1;
                        continue;
                    }

                    // Encoding 1: [Ref(name), rhs] across adjacent Flow expressions
                    if i + 1 < flow.expressions.len()
                        && let Value::Ref(Ref::Var(var_ref)) = &flow.expressions[i]
                    {
                        let var_name = var_ref.var.sym.name();
                        let value_expr = &flow.expressions[i + 1];

                        bound_vars.insert(var_name.to_string());
                        self.collect_free_variables(value_expr, free_vars, bound_vars);

                        i += 2;
                        continue;
                    }

                    // Not a variable assignment, analyze normally
                    self.collect_free_variables(&flow.expressions[i], free_vars, bound_vars);
                    i += 1;
                }
            }
            Value::MultipleValues(values) => {
                for val in values {
                    self.collect_free_variables(val, free_vars, bound_vars);
                }
            }
            Value::Lambda(lambda) => {
                // Create new bound variables scope for nested lambda
                let mut nested_bound = bound_vars.clone();
                for arg in &lambda.args.args {
                    nested_bound.insert(arg.var.sym.name().to_string());
                }
                self.collect_free_variables(&lambda.body, free_vars, &mut nested_bound);
            }
            Value::Cond(_label, condition, flow) => {
                self.collect_free_variables(condition, free_vars, bound_vars);
                self.collect_free_variables(&Value::Flow(flow.clone()), free_vars, bound_vars);
            }
            Value::CondDefault(flow) => {
                self.collect_free_variables(&Value::Flow(flow.clone()), free_vars, bound_vars);
            }
            Value::Match(match_expr) => {
                self.collect_free_variables(&match_expr.value, free_vars, bound_vars);
                for arm in &match_expr.arms {
                    // If there's a binding, add it to bound_vars for this arm's body
                    if let Some(binding) = &arm.binding {
                        let mut arm_bound = bound_vars.clone();
                        arm_bound.insert(binding.clone());
                        self.collect_free_variables(&arm.body, free_vars, &mut arm_bound);
                    } else {
                        self.collect_free_variables(&arm.body, free_vars, bound_vars);
                    }
                }
            }
            Value::TemplateLiteral(template) => {
                for part in &template.parts {
                    if let crate::lang::ast::TemplatePart::Expression(expr) = part {
                        self.collect_free_variables(expr.as_ref(), free_vars, bound_vars);
                    }
                }
            }
            // Map / Vec literals: parser stores entries that are not pure literals
            // as `Val::Box(AstNode(inner_value))`. Without descending into these, any
            // refs inside (e.g. `{label: or(label, "default")}`) are invisible to
            // closure capture analysis.
            Value::Val(val, _) => {
                self.collect_free_variables_in_val(val, free_vars, bound_vars);
            }
            Value::MapWithSpread {
                base_entries,
                spread_entries,
            } => {
                for v in base_entries.values() {
                    self.collect_free_variables_in_val(v, free_vars, bound_vars);
                }
                for (_, spread_val) in spread_entries {
                    self.collect_free_variables(spread_val, free_vars, bound_vars);
                }
            }
            Value::Raw(inner) | Value::Do(inner) => {
                self.collect_free_variables(inner, free_vars, bound_vars);
            }
            Value::MatchArm(arm) => {
                self.collect_free_variables(&arm.body, free_vars, bound_vars);
            }
            // Other value types don't contain variable references
            _ => {}
        }
    }

    /// Walk a `Val` looking for embedded `AstNode` boxes (used inside Map/Vec
    /// literals for non-literal entry values) and recurse back into the AST
    /// walker. Pure literals (Int, Str, etc.) are no-ops.
    fn collect_free_variables_in_val(
        &self,
        val: &Val,
        free_vars: &mut AHashSet<String>,
        bound_vars: &mut AHashSet<String>,
    ) {
        match val {
            Val::Box(boxed) => {
                if let Some(ast_node) = boxed.as_any().downcast_ref::<crate::lang::ast::AstNode>() {
                    self.collect_free_variables(&ast_node.0, free_vars, bound_vars);
                }
            }
            Val::Map(map) => {
                for v in map.values() {
                    self.collect_free_variables_in_val(v, free_vars, bound_vars);
                }
            }
            Val::Vec(vec) => {
                for v in vec {
                    self.collect_free_variables_in_val(v, free_vars, bound_vars);
                }
            }
            _ => {}
        }
    }

    /// Check if a variable should be excluded from closure capture
    fn should_exclude_from_capture(
        &self,
        var_name: &str,
        _var_ref: &crate::lang::ast::VarRef,
    ) -> bool {
        // Exclude VM builtins and functions that can be resolved through normal lookup
        if var_name == "call-lib" {
            return true;
        }

        // Exclude core functions since they're globally available
        if self.core_functions.contains_key(var_name) {
            return true;
        }

        // Only exclude truly core functions that are available globally
        // Do NOT exclude namespace-specific imports/aliases as they may not be available
        // when the lambda executes in a different namespace context
        if self.is_truly_global_function(var_name) {
            return true;
        }

        false
    }

    /// Check if a function should be excluded from lambda capture
    /// Only VM builtins should be excluded - everything else uses proper scoping
    fn is_truly_global_function(&self, function_name: &str) -> bool {
        // Only exclude VM builtins that are handled specially by the VM
        match function_name {
            "call-lib" => true,
            // Everything else (including core functions) should be captured or resolved
            // through proper scoping and core function lookup mechanisms
            _ => false,
        }
    }

    /// Check if a TypeDef value represents a core type.
    /// Meta is always on `var`, never on `TypeDef`.
    fn is_core_type_def(var: &Var, value: &crate::lang::ast::Value) -> bool {
        if let crate::lang::ast::Value::TypeDef(type_def) = value {
            if let Some(meta) = &var.meta {
                match &meta.val {
                    crate::val::Val::Vec(tags) => tags.iter().any(|tag| {
                        if let crate::val::Val::Str(tag_str) = tag {
                            tag_str.as_ref() == "core"
                        } else {
                            false
                        }
                    }),
                    crate::val::Val::Str(tag_str) => {
                        if tag_str.as_ref() == "core" {
                            return true;
                        }
                        if tag_str.contains("\"core\"")
                            && tag_str.contains("true")
                            && let Ok(parsed) =
                                serde_json::from_str::<serde_json::Value>(tag_str.as_ref())
                            && let Some(core_val) = parsed.get("core")
                            && let Some(core_bool) = core_val.as_bool()
                        {
                            return core_bool;
                        }
                        false
                    }
                    crate::val::Val::Map(map) => {
                        if let Some(core_val) = map.get(&crate::val::Val::from("core")) {
                            matches!(core_val, crate::val::Val::Bool(true))
                        } else {
                            false
                        }
                    }
                    _ => false,
                }
            } else {
                let type_name = type_def.name.name();
                matches!(
                    type_name,
                    "Vec" | "Map" | "Str" | "Int" | "Dec" | "Bool" | "Null"
                )
            }
        } else {
            false
        }
    }

    /// Check if a variable is marked as core (static method for compiler use)
    /// Supports multiple metadata formats:
    /// - meta ["core"] - vector containing "core" string
    /// - #"core" - direct "core" string
    /// - meta {core: true} - map with core key set to true
    fn is_core_var(var: &crate::lang::ast::Var) -> bool {
        if let Some(meta) = &var.meta {
            match &meta.val {
                // Check for meta ["core"] - vector containing "core" string
                crate::val::Val::Vec(tags) => tags.iter().any(|tag| {
                    if let crate::val::Val::Str(tag_str) = tag {
                        &**tag_str == "core"
                    } else {
                        false
                    }
                }),
                // Check for #"core" - direct "core" string
                crate::val::Val::Str(tag_str) => {
                    if &**tag_str == "core" {
                        return true;
                    }
                    // Check for meta ["core"] format stored as JSON string
                    if tag_str.starts_with("[")
                        && tag_str.ends_with("]")
                        && tag_str.contains("\"core\"")
                    {
                        // Try to parse as JSON array and check for "core" element
                        if let Ok(parsed) = serde_json::from_str::<Vec<String>>(tag_str) {
                            return parsed.iter().any(|s| s == "core");
                        }
                    }
                    // Also check if it's a JSON string containing core metadata
                    if tag_str.contains("\"core\"") && tag_str.contains("true") {
                        // Try to parse as JSON and check for core: true
                        if let Ok(parsed) = serde_json::from_str::<serde_json::Value>(tag_str)
                            && let Some(core_val) = parsed.get("core")
                            && let Some(core_bool) = core_val.as_bool()
                        {
                            return core_bool;
                        }
                    }
                    false
                }
                // Check for meta {core: true} - map with core key set to true
                crate::val::Val::Map(map) => {
                    if let Some(core_val) = map.get(&crate::val::Val::from("core")) {
                        matches!(core_val, crate::val::Val::Bool(true))
                    } else {
                        false
                    }
                }
                _ => false,
            }
        } else {
            false
        }
    }

    /// Public entry point for core variable extraction (Phase 0).
    /// Must be called before extract_send_targets when not using compile_program.
    pub fn extract_core_variables_from_program(&mut self, program: &Program) -> CompilerResult<()> {
        self.extract_core_variables(program)
    }

    /// Extract all variables marked with core metadata from the program
    /// This creates a registry of core variables that can be auto-imported
    fn extract_core_variables(&mut self, program: &Program) -> CompilerResult<()> {
        tracing::trace!(
            "Extracting core variables from {} namespaces",
            program.namespaces.len()
        );

        let mut core_var_count = 0;

        // Iterate through all namespaces and collect core variables
        for (ns_path, namespace) in &program.namespaces {
            let ns_path_str = ns_path.to_string();

            // Check all variables in the namespace
            for (var, value) in &namespace.scope.vars {
                let is_core = Self::is_core_var(var) || Self::is_core_type_def(var, value);
                if is_core {
                    core_var_count += 1;
                    tracing::trace!(
                        "Found core variable: {} in namespace {}",
                        var.sym.name(),
                        ns_path_str
                    );

                    // Determine the variable type based on the value
                    let var_type = match value {
                        crate::lang::ast::Value::Fn(_) => {
                            crate::lang::bytecode::VariableType::CoreFunction
                        }
                        crate::lang::ast::Value::TypeDef(_) => {
                            crate::lang::bytecode::VariableType::TypeConstructor
                        }
                        _ => crate::lang::bytecode::VariableType::Value,
                    };

                    // Create variable info for the registry
                    let var_info = crate::lang::bytecode::VariableInfo {
                        name: var.sym.name().to_string(),
                        var_type,
                        metadata: var.meta.as_ref().map(|m| m.val.clone()),
                        function_id: None,
                    };

                    // Add to namespace registry as a core variable
                    self.program.namespaces.add_variable(&ns_path_str, var_info);

                    // Also add to the new core variables registry
                    let core_var_type = match value {
                        crate::lang::ast::Value::Fn(_) => CoreVariableType::Function,
                        crate::lang::ast::Value::TypeDef(_) => CoreVariableType::TypeConstructor,
                        _ => CoreVariableType::Constant,
                    };

                    self.core_variables.add_core_variable(
                        var.sym.name().to_string(),
                        value.clone(),
                        ns_path_str.clone(),
                        core_var_type,
                    );
                }
            }
        }

        tracing::trace!("Extracted {} core variables total", core_var_count);
        Ok(())
    }

    /// Compile a pipe flow with result modifier using flow context
    fn compile_pipe_flow_with_modifier(&mut self, flow: &Flow) -> Result<RegisterId, String> {
        let result_reg = self.allocate_register();

        if flow.expressions.len() < 2 {
            return Err("Pipe flow requires at least 2 expressions".to_string());
        }

        // Map AST ResultModifier to bytecode FlowResultModifier
        // Default for pipe: One (return final piped result)
        let result_modifier = match flow.result_modifier {
            Some(crate::lang::ast::ResultModifier::Map) => {
                crate::lang::bytecode::FlowResultModifier::Map
            }
            Some(crate::lang::ast::ResultModifier::Vec) => {
                crate::lang::bytecode::FlowResultModifier::Vec
            }
            Some(crate::lang::ast::ResultModifier::One) | None => {
                crate::lang::bytecode::FlowResultModifier::One
            }
        };

        // Begin flow context with source location from AST
        self.emit_instruction(Instruction::BeginFlow {
            flow_type: crate::lang::bytecode::FlowType::Pipe,
            result_modifier,
            source: self.source_to_location(&flow.src).map(Box::new),
        });

        // Start with the first expression (the initial value)
        let mut current_reg = self.compile_value(&flow.expressions[0])?;

        // Track the initial value as step 0
        let initial_pipe_reg = self.allocate_register();
        self.emit_instruction(Instruction::Pipe {
            dest: initial_pipe_reg,
            src: current_reg,
        });

        // Apply each subsequent expression as a function to the current value
        for i in 1..flow.expressions.len() {
            let func_expr = &flow.expressions[i];

            // The function expression should be compiled as a function call
            // with the current value as the first argument
            match func_expr {
                Value::FnCall(fn_call) => {
                    // Create a new function call with current_reg as the first argument
                    current_reg = self.compile_piped_function_call(fn_call, current_reg)?;
                }
                Value::Lambda(lambda) => {
                    // Handle lambda expressions in pipe flows
                    current_reg = self.compile_piped_lambda_call(lambda, current_reg)?;
                }
                _ => {
                    // For other expressions, try to call them as functions with the piped value
                    let function_reg = self.compile_value(func_expr)?;
                    let dest_reg = self.allocate_register();

                    // Set up arguments: piped value as first argument
                    let args_start = self.allocate_register();
                    self.emit_instruction(Instruction::Move {
                        dest: args_start,
                        src: current_reg,
                    });

                    self.emit_instruction(Instruction::Call {
                        dest: dest_reg,
                        function: function_reg,
                        args_start,
                        args_count: 1,
                    });

                    current_reg = dest_reg;
                }
            }

            // Track each function result for result modifiers
            let step_pipe_reg = self.allocate_register();
            self.emit_instruction(Instruction::Pipe {
                dest: step_pipe_reg,
                src: current_reg,
            });
        }

        // End flow context and collect result
        self.emit_instruction(Instruction::EndFlow { dest: result_reg });

        Ok(result_reg)
    }

    /// Compile a serial flow with result modifier using flow context
    fn compile_serial_flow_with_modifier(&mut self, flow: &Flow) -> Result<RegisterId, String> {
        let result_reg = self.allocate_register();

        // Map AST ResultModifier to bytecode FlowResultModifier
        // Default for serial: One (return last result)
        let result_modifier = match flow.result_modifier {
            Some(crate::lang::ast::ResultModifier::Map) => {
                crate::lang::bytecode::FlowResultModifier::Map
            }
            Some(crate::lang::ast::ResultModifier::Vec) => {
                crate::lang::bytecode::FlowResultModifier::Vec
            }
            Some(crate::lang::ast::ResultModifier::One) | None => {
                crate::lang::bytecode::FlowResultModifier::One
            }
        };

        // Begin flow context with source location from AST
        self.emit_instruction(Instruction::BeginFlow {
            flow_type: crate::lang::bytecode::FlowType::Serial,
            result_modifier,
            source: self.source_to_location(&flow.src).map(Box::new),
        });

        // Activate flow-scoped aliases, saving previous state for restore
        let saved_aliases = if !flow.aliases.is_empty() {
            Some(self.current_aliases.clone())
        } else {
            None
        };
        let mut alias_cursor = 0;

        // Compile expressions in the flow context
        if !flow.expressions.is_empty() {
            let mut i = 0;
            while i < flow.expressions.len() {
                // Activate any aliases whose position matches the current expression index
                while alias_cursor < flow.aliases.len() && flow.aliases[alias_cursor].0 <= i {
                    let (_, ref alias, ref source) = flow.aliases[alias_cursor];
                    self.current_aliases.insert(alias.clone(), source.clone());
                    alias_cursor += 1;
                }

                // Check for variable assignment pattern: Ref(Var(name)) followed by expression
                if i + 1 < flow.expressions.len()
                    && let Value::Ref(Ref::Var(var_ref)) = &flow.expressions[i]
                {
                    // This looks like a variable assignment: var_name expression
                    let var_name = var_ref.var.sym.name();
                    let value_expr = &flow.expressions[i + 1];

                    tracing::trace!(
                        "Compiler: Detected variable assignment in serial flow with modifier: '{}' = {:?}",
                        var_name,
                        value_expr
                    );

                    // Compile the value expression
                    let value_reg = self.compile_value(value_expr)?;

                    // Store the variable
                    self.store_variable(var_name, value_reg);

                    i += 2; // Skip both expressions
                    continue;
                }

                // Not a variable assignment, compile normally
                self.compile_value(&flow.expressions[i])?;
                i += 1;
            }
        }

        // Restore aliases to pre-flow state
        if let Some(saved) = saved_aliases {
            self.current_aliases = saved;
        }

        // End flow context and collect result
        self.emit_instruction(Instruction::EndFlow { dest: result_reg });

        Ok(result_reg)
    }

    /// Compile a parallel flow with result modifier using flow context and dependency analysis
    fn compile_parallel_flow_with_modifier(&mut self, flow: &Flow) -> Result<RegisterId, String> {
        let result_reg = self.allocate_register();

        // Map AST ResultModifier to bytecode FlowResultModifier
        // For parallel flows without explicit modifier, default to Map (return all results)
        let result_modifier = match flow.result_modifier {
            Some(crate::lang::ast::ResultModifier::Map) => {
                crate::lang::bytecode::FlowResultModifier::Map
            }
            Some(crate::lang::ast::ResultModifier::Vec) => {
                crate::lang::bytecode::FlowResultModifier::Vec
            }
            Some(crate::lang::ast::ResultModifier::One) => {
                crate::lang::bytecode::FlowResultModifier::One
            }
            None => {
                // Default for parallel flows: return map of all variable results
                crate::lang::bytecode::FlowResultModifier::Map
            }
        };

        // Build dependency graph for this flow
        let (dependency_graph, _var_sources) = self.build_dependency_graph(flow)?;

        // Perform topological sort to detect circular dependencies
        let execution_order = self.topological_sort(&dependency_graph)?;

        // Group variables by dependency level
        let dependency_levels =
            self.group_by_dependency_levels(&dependency_graph, &execution_order);

        tracing::trace!(
            "Compiler: Parallel flow dependency analysis completed. {} variables in {} levels",
            execution_order.len(),
            dependency_levels.len()
        );
        for (level_idx, level) in dependency_levels.iter().enumerate() {
            tracing::trace!(
                "Compiler:   Level {}: {} variables ({})",
                level_idx,
                level.len(),
                level.join(", ")
            );
        }

        // Store dependency levels as metadata in a constant
        // The VM will use this to orchestrate parallel execution
        let dependency_levels_val = {
            let mut levels_vec = Vec::new();
            for level in &dependency_levels {
                let level_vec: Vec<Val> = level.iter().map(|s| Val::from(s.clone())).collect();
                levels_vec.push(Val::Vec(level_vec));
            }
            Val::Vec(levels_vec)
        };
        let metadata_constant = self
            .program
            .add_constant(Constant::Val(dependency_levels_val));

        // Begin parallel flow context with source location from AST
        self.emit_instruction(Instruction::BeginFlow {
            flow_type: crate::lang::bytecode::FlowType::Parallel,
            result_modifier,
            source: self.source_to_location(&flow.src).map(Box::new),
        });

        // Store the dependency metadata in a special register that the VM can access
        // We'll use a LoadConst instruction to make it available
        let metadata_reg = self.allocate_register();
        self.emit_instruction(Instruction::LoadConst {
            dest: metadata_reg,
            constant: metadata_constant,
        });

        // Compile all variable assignments as deferred thunks for parallel execution
        // Each variable expression becomes a zero-arg lambda that will be evaluated in parallel
        if !flow.expressions.is_empty() {
            let mut i = 0;
            while i < flow.expressions.len() {
                // Check for variable assignment pattern: Ref(Var(name)) followed by expression
                if i + 1 < flow.expressions.len()
                    && let Value::Ref(Ref::Var(var_ref)) = &flow.expressions[i]
                {
                    // This looks like a variable assignment: var_name expression
                    let var_name = var_ref.var.sym.name();
                    let value_expr = &flow.expressions[i + 1];

                    tracing::trace!(
                        "Compiler: Creating deferred thunk for parallel variable: '{}' = {:?}",
                        var_name,
                        value_expr
                    );

                    // Compile the value expression as a thunk (zero-arg lambda)
                    let thunk_reg = self.compile_expression_as_thunk(value_expr)?;

                    // Emit DeferredVarExpr instruction - VM will evaluate this in parallel
                    let var_name_constant = self
                        .program
                        .add_constant(Constant::StringRef(var_name.to_string().into()));
                    self.emit_instruction(Instruction::DeferredVarExpr {
                        var_name: var_name_constant,
                        thunk: thunk_reg,
                    });

                    i += 2; // Skip both expressions
                    continue;
                }

                // Not a variable assignment, compile normally (side effect expressions)
                self.compile_value(&flow.expressions[i])?;
                i += 1;
            }
        }

        // End flow context and collect result
        self.emit_instruction(Instruction::EndFlow { dest: result_reg });

        Ok(result_reg)
    }

    /// Compile a flow expression (serial, parallel, etc.)
    fn compile_flow(&mut self, flow: &Flow) -> Result<RegisterId, String> {
        match flow.flow_type {
            FlowType::Serial => {
                // Check if this flow has a result modifier - if so, use flow context
                if flow.result_modifier.is_some() {
                    return self.compile_serial_flow_with_modifier(flow);
                }

                // Execute expressions sequentially, return the last result
                let mut last_reg = self.allocate_register();

                // Activate flow-scoped aliases, saving previous state for restore
                let saved_aliases = if !flow.aliases.is_empty() {
                    Some(self.current_aliases.clone())
                } else {
                    None
                };
                let mut alias_cursor = 0;

                if flow.expressions.is_empty() {
                    // Empty flow returns null
                    let constant_id = self.program.add_constant(Constant::Val(Val::Null));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: last_reg,
                        constant: constant_id,
                    });
                } else {
                    // TCO: Save the tail position state - only the last expression is in tail position
                    let saved_tail_position = self.in_tail_position;

                    // Compile each expression in sequence, detecting variable assignments
                    let mut i = 0;
                    while i < flow.expressions.len() {
                        // Activate any aliases whose position matches the current expression index
                        while alias_cursor < flow.aliases.len() && flow.aliases[alias_cursor].0 <= i
                        {
                            let (_, ref alias, ref source) = flow.aliases[alias_cursor];
                            self.current_aliases.insert(alias.clone(), source.clone());
                            alias_cursor += 1;
                        }

                        // TCO: Only the last expression in a serial flow is in tail position
                        // For variable assignments (i+1 pattern), check if the value expression is last
                        let is_last_expression = i + 1 >= flow.expressions.len()
                            || (i + 2 >= flow.expressions.len()
                                && matches!(&flow.expressions[i], Value::Ref(Ref::Var(_))));

                        // Set tail position: only true for last expression if flow itself is in tail position
                        self.in_tail_position = saved_tail_position && is_last_expression;

                        // Check for variable assignment pattern: Ref(Var(name)) followed by expression
                        if i + 1 < flow.expressions.len()
                            && let Value::Ref(Ref::Var(var_ref)) = &flow.expressions[i]
                        {
                            // This looks like a variable assignment: var_name expression
                            let var_name = var_ref.var.sym.name();
                            let value_expr = &flow.expressions[i + 1];

                            tracing::trace!(
                                "Compiler: Detected variable assignment in serial flow: '{}' = {:?}",
                                var_name,
                                value_expr
                            );

                            // Special handling for function definitions to enable TCO
                            // When a function is defined with a name (e.g., sum-acc fn (n, acc) {...}),
                            // we need to register it properly so recursive calls can be optimized
                            if let Value::Fn(function_defs) = value_expr {
                                let value_reg =
                                    self.compile_named_function_in_flow(var_name, function_defs)?;
                                self.store_variable(var_name, value_reg);
                                last_reg = value_reg;
                                i += 2;
                                continue;
                            }

                            // Special handling for type definitions in flow variable assignments
                            // Use the variable name (e.g., "Measurement") instead of "anonymous_type"
                            if let Value::TypeDef(type_def) = value_expr {
                                tracing::trace!(
                                    "Compiler: Compiling type definition in flow for variable '{}'",
                                    var_name
                                );
                                let value_reg =
                                    self.compile_type_constructor(var_name, type_def, false)?;
                                self.store_variable(var_name, value_reg);
                                last_reg = value_reg;
                                i += 2;
                                continue;
                            }

                            // Special handling for type implementations in flow variable assignments
                            if let Value::TypeImplementation(type_impl) = value_expr {
                                tracing::trace!(
                                    "Compiler: Compiling type implementation in flow: {} -> {}",
                                    type_impl.source_type,
                                    type_impl.target_type
                                );
                                let value_reg = self.compile_type_implementation(type_impl)?;
                                self.store_variable(var_name, value_reg);
                                last_reg = value_reg;
                                i += 2;
                                continue;
                            }

                            // Compile the value expression
                            let value_reg = self.compile_value(value_expr)?;

                            // Store the variable
                            self.store_variable(var_name, value_reg);

                            last_reg = value_reg;
                            i += 2; // Skip both expressions
                            continue;
                        }

                        // Not a variable assignment, compile normally
                        last_reg = self.compile_value(&flow.expressions[i])?;
                        i += 1;
                    }

                    // TCO: Restore tail position state
                    self.in_tail_position = saved_tail_position;
                }

                // Restore aliases to pre-flow state
                if let Some(saved) = saved_aliases {
                    self.current_aliases = saved;
                }

                Ok(last_reg)
            }
            FlowType::Pipe => {
                // Check if this flow has a result modifier - if so, use flow context
                if flow.result_modifier.is_some() {
                    return self.compile_pipe_flow_with_modifier(flow);
                }

                // Pipe flow: value |> function |> function
                if flow.expressions.len() < 2 {
                    return Err("Pipe flow requires at least 2 expressions".to_string());
                }

                // Start with the first expression (the initial value)
                let mut current_reg = self.compile_value(&flow.expressions[0])?;

                // Apply each subsequent expression as a function to the current value
                for i in 1..flow.expressions.len() {
                    let func_expr = &flow.expressions[i];

                    // The function expression should be compiled as a function call
                    // with the current value as the first argument
                    match func_expr {
                        Value::FnCall(fn_call) => {
                            // Create a new function call with current_reg as the first argument
                            // We need to inject the piped value as the first argument
                            current_reg = self.compile_piped_function_call(fn_call, current_reg)?;
                        }
                        Value::Lambda(lambda) => {
                            // Handle lambda expressions in pipe flows
                            current_reg = self.compile_piped_lambda_call(lambda, current_reg)?;
                        }
                        _ => {
                            // For other expressions, try to call them as functions with the piped value
                            let function_reg = self.compile_value(func_expr)?;
                            let dest_reg = self.allocate_register();

                            // Set up arguments: piped value as first argument
                            let args_start = self.allocate_register();
                            self.emit_instruction(Instruction::Move {
                                dest: args_start,
                                src: current_reg,
                            });

                            // Call the function
                            self.emit_instruction(Instruction::Call {
                                dest: dest_reg,
                                function: function_reg,
                                args_start,
                                args_count: 1,
                            });

                            current_reg = dest_reg;
                        }
                    }
                }

                Ok(current_reg)
            }
            FlowType::Cond => {
                // Conditional flow: evaluate conditions in order, return first truthy result
                self.compile_cond_flow(flow, true) // true = short-circuit on first match
            }
            FlowType::CondAll => {
                // Conditional all flow: evaluate all conditions, return all truthy results
                self.compile_cond_flow(flow, false) // false = don't short-circuit
            }
            FlowType::Parallel | FlowType::ParallelShort => {
                // Parallel flow: variables can execute concurrently with dependency resolution
                // Check if this flow has a result modifier - if so, use flow context
                if flow.result_modifier.is_some() || flow.flow_type == FlowType::Parallel {
                    return self.compile_parallel_flow_with_modifier(flow);
                }

                // For ParallelShort without result modifier, fall back to serial execution
                // (parallel execution requires result modifier for proper variable tracking)
                tracing::warn!(
                    "Compiler: ParallelShort without result modifier - falling back to serial"
                );
                let mut last_reg = self.allocate_register();
                if flow.expressions.is_empty() {
                    let constant_id = self.program.add_constant(Constant::Val(Val::Null));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: last_reg,
                        constant: constant_id,
                    });
                } else {
                    let mut i = 0;
                    while i < flow.expressions.len() {
                        if i + 1 < flow.expressions.len()
                            && let Value::Ref(Ref::Var(var_ref)) = &flow.expressions[i]
                        {
                            let var_name = var_ref.var.sym.name();
                            let value_expr = &flow.expressions[i + 1];
                            let value_reg = self.compile_value(value_expr)?;
                            self.store_variable(var_name, value_reg);
                            last_reg = value_reg;
                            i += 2;
                            continue;
                        }
                        last_reg = self.compile_value(&flow.expressions[i])?;
                        i += 1;
                    }
                }
                Ok(last_reg)
            }
            FlowType::Match | FlowType::MatchShort => {
                // Match flow: body contains MatchArm expressions
                // This is compiled like a match expression where the value to match
                // is the first parameter of the enclosing function
                self.compile_match_flow(flow, true) // true = short-circuit on first match
            }
            FlowType::MatchAll | FlowType::MatchAllShort => {
                // Match-all flow: execute all matching arms
                self.compile_match_flow(flow, false) // false = don't short-circuit
            }
            _ => {
                // Unhandled flow type
                tracing::warn!("Compiler: Unhandled flow type: {:?}", flow.flow_type);
                let reg = self.allocate_register();
                let constant_id = self.program.add_constant(Constant::Val(Val::Null));
                self.emit_instruction(Instruction::LoadConst {
                    dest: reg,
                    constant: constant_id,
                });
                Ok(reg)
            }
        }
    }

    /// Compile a reference (variable, namespace, or deferred)
    fn compile_reference(&mut self, hot_ref: &Ref) -> Result<RegisterId, String> {
        match hot_ref {
            Ref::Var(var_ref) => {
                // Variable reference - check if it has a deep path (like colors.red)
                let var_name = &var_ref.var.sym.name();

                if let Some(deep_path) = &var_ref.var.deep_path {
                    // This is a deep path access like colors.red
                    // First load the base variable, then traverse the deep path
                    let local_type_path = format!("{}/{}", self.current_namespace, var_name);
                    let local_type_exists = self
                        .program
                        .namespaces
                        .namespaces
                        .get(&self.current_namespace)
                        .is_some_and(|ns_info| ns_info.variables.contains_key(&local_type_path));

                    if !self.current_function_params.contains(&var_name.to_string())
                        && !local_type_exists
                        && let Some(core_info) = self.core_variables.get(var_name)
                        && let crate::lang::ast::Value::TypeDef(type_def) = &core_info.value
                        && let Some(variants) = &type_def.variants
                    {
                        let full_type_name = format!("{}/{}", core_info.namespace_path, var_name);
                        let variant_map: indexmap::IndexMap<String, Option<String>> = variants
                            .iter()
                            .map(|v| (v.name.name().to_string(), v.type_ref.clone()))
                            .collect();
                        let type_ref = crate::lang::refs::TypeRef::new(full_type_name, variant_map);
                        let type_ref_constant = self
                            .program
                            .add_constant(Constant::VariantTypeRef(type_ref));
                        let type_ref_reg = self.allocate_register();
                        self.emit_instruction(Instruction::LoadTypeRef {
                            dest: type_ref_reg,
                            type_ref: type_ref_constant,
                        });
                        return self.compile_deep_path_access(type_ref_reg, deep_path);
                    }

                    let base_reg = self.allocate_register();
                    let var_name_constant = self
                        .program
                        .add_constant(Constant::Val(Val::from(var_name.to_string())));

                    self.emit_instruction(Instruction::LoadVar {
                        dest: base_reg,
                        var_name: var_name_constant,
                    });

                    // Now traverse the deep path
                    let result_reg = self.compile_deep_path_access(base_reg, deep_path)?;

                    tracing::trace!(
                        "Compiler: Generated deep path access for '{}.{:?}'",
                        var_name,
                        deep_path
                    );

                    Ok(result_reg)
                } else {
                    // Regular variable reference without deep path
                    let reg = self.allocate_register();

                    // Check if this is a function parameter
                    if self.current_function_params.contains(&var_name.to_string()) {
                        // This is a function parameter - generate LoadVar instruction for parameter
                        let var_name_constant = self
                            .program
                            .add_constant(Constant::Val(Val::from(var_name.to_string())));

                        self.emit_instruction(Instruction::LoadVar {
                            dest: reg,
                            var_name: var_name_constant,
                        });

                        tracing::trace!(
                            "Compiler: Generated LoadVar for function parameter '{}'",
                            var_name
                        );
                    } else {
                        // Check if this variable name corresponds to a function in the current namespace
                        let qualified_function_name =
                            format!("{}/{}", self.current_namespace, var_name);

                        if self.function_mapping.contains_key(&qualified_function_name) {
                            // This is a function reference in the current namespace
                            // Generate LoadFunctionRef to load the function reference
                            let fn_name_id =
                                self.program.add_string_ref(qualified_function_name.clone());
                            self.emit_instruction(Instruction::LoadFunctionRef {
                                dest: reg,
                                function_name: fn_name_id,
                            });

                            tracing::trace!(
                                "Compiler: Generated LoadFunctionRef for qualified function '{}'",
                                qualified_function_name
                            );
                        } else {
                            // This is a namespace variable - generate LoadVar instruction for namespace variable
                            let var_name_constant = self
                                .program
                                .add_constant(Constant::Val(Val::from(var_name.to_string())));

                            self.emit_instruction(Instruction::LoadVar {
                                dest: reg,
                                var_name: var_name_constant,
                            });

                            tracing::trace!(
                                "Compiler: Generated LoadVar for namespace variable '{}'",
                                var_name
                            );
                        }
                    }

                    Ok(reg)
                }
            }
            Ref::Ns(ns_ref) => {
                // Namespace reference - construct full name and load
                // First, resolve any namespace aliases
                let resolved_ns_ref = self.resolve_ns_ref_alias(ns_ref);

                let reg = self.allocate_register();
                let namespace_parts: Vec<String> = resolved_ns_ref
                    .ns
                    .0
                    .iter()
                    .map(|ns_part| match ns_part {
                        crate::lang::ast::NsPathPart::Sym(sym) => match sym {
                            crate::lang::ast::Sym::String(s) => s.clone(),
                        },
                    })
                    .collect();

                tracing::trace!(
                    "Compiler: Processing namespace reference in compile_reference: {:?} -> {:?} (current namespace: {})",
                    ns_ref,
                    resolved_ns_ref,
                    self.current_namespace
                );

                // Construct full name including function name if available
                // For function references like ::hot::ctx/ctx-set, we need to include the function name
                let full_name = if let Some(func_name) = &resolved_ns_ref.function_name {
                    // If function_name is available, this is a function reference like ::openai::api/request
                    let constructed_name =
                        format!("::{}/{}", namespace_parts.join("::"), func_name);
                    tracing::trace!(
                        "Compiler: Constructed full_name '{}' from ns_parts: {:?}, function_name: {:?}",
                        constructed_name,
                        namespace_parts,
                        func_name
                    );
                    constructed_name
                } else {
                    // Just the namespace path (with leading ::)
                    let constructed_name = format!("::{}", namespace_parts.join("::"));
                    tracing::trace!(
                        "Compiler: Constructed bare namespace '{}' from ns_parts: {:?}",
                        constructed_name,
                        namespace_parts
                    );
                    constructed_name
                };

                // Check if this looks like a function reference (contains '/')
                tracing::trace!(
                    "Compiler: Checking if '{}' contains '/': {}",
                    full_name,
                    full_name.contains('/')
                );
                if full_name.contains('/') {
                    // Anything with '/' is either a function reference or variable/type reference
                    // Namespace references use '::' exclusively (e.g., ::hot::io)
                    // Use the pre-extracted known_functions registry which includes all functions
                    // regardless of compilation order
                    let is_function = self.is_known_function(&full_name)
                        || full_name.ends_with("/call-lib")
                        || full_name.ends_with("/call");

                    if is_function {
                        // This is a function reference (built-in or user-defined)
                        // Generate LoadFunctionRef to load the actual function reference
                        let fn_name_id = self.program.add_string_ref(full_name.clone());
                        self.emit_instruction(Instruction::LoadFunctionRef {
                            dest: reg,
                            function_name: fn_name_id,
                        });

                        tracing::trace!(
                            "Compiler: Generated LoadFunctionRef for function reference '{}'",
                            full_name
                        );
                    } else {
                        // This is a variable reference like "::hot::dev/a"
                        // Generate LoadVar instruction for qualified variable lookup
                        let var_name_constant = self
                            .program
                            .add_constant(Constant::Val(Val::from(full_name.clone())));

                        self.emit_instruction(Instruction::LoadVar {
                            dest: reg,
                            var_name: var_name_constant,
                        });

                        tracing::trace!(
                            "Compiler: Generated LoadVar for qualified variable reference '{}'",
                            full_name
                        );
                    }
                } else {
                    // This is a bare namespace reference like ::hot::io
                    // Create a proper Namespace type value using ::hot::type/Namespace constructor
                    tracing::trace!(
                        "Compiler: Creating Namespace type value for bare namespace reference '{}'",
                        full_name
                    );

                    // First, create the namespace string argument
                    let arg_reg = self.allocate_register();
                    let ns_constant = self
                        .program
                        .add_constant(Constant::Val(Val::from(full_name.clone())));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: arg_reg,
                        constant: ns_constant,
                    });

                    // Call the Namespace constructor using CallLibBuiltin
                    // First, create a function reference for the Namespace constructor
                    let fn_ref_reg = self.allocate_register();
                    let fn_name_constant = self
                        .program
                        .add_constant(Constant::Val(Val::from("::hot::type/Namespace")));
                    self.emit_instruction(Instruction::LoadConst {
                        dest: fn_ref_reg,
                        constant: fn_name_constant,
                    });

                    // Create a vector containing the argument
                    let args_reg = self.allocate_register();
                    self.emit_instruction(Instruction::MakeVec {
                        dest: args_reg,
                        elements_start: arg_reg,
                        count: 1,
                    });

                    // Call the constructor
                    self.emit_instruction(Instruction::CallLibBuiltin {
                        dest: reg,
                        function: fn_ref_reg,
                        args: args_reg,
                    });
                }
                Ok(reg)
            }
        }
    }

    /// Compile match expression for variant unions
    /// match value { Variant1 => body1, Variant2(binding) => body2 }
    /// Uses the flow infrastructure (BeginFlow/EndFlow) for proper result modifier support
    /// Check if match arms contain Result.Ok or Result.Err patterns
    fn match_has_result_patterns(&self, arms: &[crate::lang::ast::MatchArm]) -> bool {
        arms.iter().any(|arm| {
            if let (Some(type_name), Some(_variant)) = (&arm.type_name, &arm.variant) {
                let (resolved, _) = self.resolve_type_path(type_name);
                resolved == "::hot::type/Result"
            } else if let (Some(type_name), None) = (&arm.type_name, &arm.variant) {
                let (resolved, _) = self.resolve_type_path(type_name);
                resolved == "::hot::type/Result"
            } else {
                false
            }
        })
    }

    fn compile_match_expression(
        &mut self,
        match_expr: &crate::lang::ast::MatchExpr,
    ) -> Result<RegisterId, String> {
        // Allocate a unique id for this match instance so its branch names don't
        // collide with sibling/nested cond/match flows after string interning.
        // See `next_cond_id` doc on `Compiler` for the original bug.
        let cond_id = self.next_cond_id;
        self.next_cond_id += 1;

        // Compile the value being matched
        let value_reg = self.compile_value(&match_expr.value)?;

        // Extract the match subject variable name (if any) for auto-shadowing
        // in Result variant arms. When users write `match result { Result.Err => { ... } }`,
        // `result` inside the arm should refer to the unwrapped $val payload.
        let match_subject_var_name = match match_expr.value.as_ref() {
            crate::lang::ast::Value::Ref(crate::lang::ast::Ref::Var(var_ref)) => {
                Some(var_ref.var.sym.name().to_string())
            }
            _ => None,
        };

        // If match arms contain Result patterns (Result.Ok / Result.Err),
        // ensure the subject is wrapped in a Result type. This makes
        // `match fn_call_result { Result.Ok => ... }` work even when the
        // function return value is not explicitly wrapped in Result.Ok.
        let value_reg = if self.match_has_result_patterns(&match_expr.arms) {
            let wrapped_reg = self.allocate_register();
            self.emit_instruction(Instruction::EnsureResult {
                dest: wrapped_reg,
                value: value_reg,
            });
            wrapped_reg
        } else {
            value_reg
        };

        // Allocate result register
        let result_reg = self.allocate_register();

        // Determine flow type and result modifier
        // match -> Match (short-circuit), match-all -> MatchAll (execute all)
        let flow_type = if match_expr.match_all {
            crate::lang::bytecode::FlowType::MatchAll
        } else {
            crate::lang::bytecode::FlowType::Match
        };

        // Map AST ResultModifier to bytecode FlowResultModifier
        // Default: match -> One, match-all -> Map (like cond/cond-all)
        let result_modifier = match &match_expr.result_modifier {
            Some(crate::lang::ast::ResultModifier::Map) => {
                crate::lang::bytecode::FlowResultModifier::Map
            }
            Some(crate::lang::ast::ResultModifier::Vec) => {
                crate::lang::bytecode::FlowResultModifier::Vec
            }
            Some(crate::lang::ast::ResultModifier::One) => {
                crate::lang::bytecode::FlowResultModifier::One
            }
            None => {
                // Default based on flow type: match -> One, match-all -> Map
                if match_expr.match_all {
                    crate::lang::bytecode::FlowResultModifier::Map
                } else {
                    crate::lang::bytecode::FlowResultModifier::One
                }
            }
        };

        // Begin the flow context
        self.emit_instruction(Instruction::BeginFlow {
            flow_type,
            result_modifier,
            source: self.source_to_location(&match_expr.src).map(Box::new),
        });

        // Get the type path of the matched value (works for primitives and custom types)
        let type_field_reg = self.allocate_register();
        self.emit_instruction(Instruction::GetTypePath {
            dest: type_field_reg,
            value: value_reg,
        });

        // For each arm, emit comparison and body using CondBranchStart/CondBranchEnd
        for arm in &match_expr.arms {
            let cmp_result_reg = self.allocate_register();

            // Construct the branch name for result collection (match-all keys).
            // Suffix with cond_id so branch names don't collide across sibling/
            // nested match expressions after string interning.
            let base_branch_name = if let Some(value_literal) = &arm.value_literal {
                // For value arms, use a string representation of the literal as the key
                match value_literal.as_ref() {
                    Value::Val(val, _) => val.format(crate::val::ValFormat::Hot),
                    _ => "_value".to_string(),
                }
            } else {
                match (&arm.type_name, &arm.variant) {
                    (Some(type_name), Some(variant)) => format!("{}.{}", type_name, variant),
                    (Some(type_name), None) => type_name.clone(),
                    (None, Some(variant)) => variant.clone(),
                    (None, None) => "_".to_string(),
                }
            };
            let branch_name = format!("{}\0c{}", base_branch_name, cond_id);

            // Emit comparison instruction
            if let Some(value_literal) = &arm.value_literal {
                // Value equality arm: compare subject value directly against the literal
                let literal_reg = self.compile_value(value_literal)?;
                self.emit_instruction(Instruction::Eq {
                    dest: cmp_result_reg,
                    left: value_reg,
                    right: literal_reg,
                });
            } else {
                match (&arm.type_name, &arm.variant) {
                    (Some(type_name), Some(variant)) => {
                        // Qualified variant (Type.Variant) - resolve type and do exact match
                        let (full_type_path, was_found) = self.resolve_type_path(type_name);

                        if !was_found {
                            return Err(format!(
                                "Type '{}' in match pattern could not be resolved. \
                             Type aliases used in match patterns must be defined at namespace level, \
                             not inside function bodies.",
                                type_name
                            ));
                        }

                        let full_variant_path = format!("{}.{}", full_type_path, variant);

                        // Load the expected full type path
                        let expected_type_reg = self.allocate_register();
                        let expected_type_constant = self
                            .program
                            .add_constant(Constant::Val(Val::from(full_variant_path)));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: expected_type_reg,
                            constant: expected_type_constant,
                        });

                        // Compare: $type == expected full path (exact match)
                        self.emit_instruction(Instruction::Eq {
                            dest: cmp_result_reg,
                            left: type_field_reg,
                            right: expected_type_reg,
                        });
                    }
                    (Some(type_name), None) => {
                        // Type-level matching (any variant of Type)
                        // For primitives like Int: exact match on "::hot::type/Int"
                        // For enums like Result.Ok: prefix match on "::hot::type/Result."
                        //
                        // Use the IsType instruction which handles both cases correctly
                        // without going through function call (avoids Result auto-unwrapping)
                        let (full_type_path, was_found) = self.resolve_type_path(type_name);

                        if !was_found {
                            return Err(format!(
                                "Type '{}' in match pattern could not be resolved. \
                             Type aliases used in match patterns must be defined at namespace level, \
                             not inside function bodies.",
                                type_name
                            ));
                        }

                        // Load the expected type path as a string
                        let expected_type_reg = self.allocate_register();
                        let expected_type_constant = self
                            .program
                            .add_constant(Constant::Val(Val::from(full_type_path.clone())));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: expected_type_reg,
                            constant: expected_type_constant,
                        });

                        // Use IsType instruction for unified type checking
                        // This handles: exact match (primitives) OR prefix match (enum variants)
                        self.emit_instruction(Instruction::IsType {
                            dest: cmp_result_reg,
                            value: value_reg,
                            type_path: expected_type_reg,
                        });
                    }
                    (None, Some(variant)) => {
                        // Bare variant name (deprecated) - use suffix matching
                        let arm_suffix_reg = self.allocate_register();
                        let suffix = format!(".{}", variant);
                        let arm_suffix_constant =
                            self.program.add_constant(Constant::Val(Val::from(suffix)));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: arm_suffix_reg,
                            constant: arm_suffix_constant,
                        });

                        // Compare: $type ends with ".Variant"
                        self.emit_instruction(Instruction::StrEndsWith {
                            dest: cmp_result_reg,
                            string: type_field_reg,
                            suffix: arm_suffix_reg,
                        });
                    }
                    (None, None) => {
                        // Default arm - always matches
                        let true_reg = self.allocate_register();
                        let true_constant =
                            self.program.add_constant(Constant::Val(Val::Bool(true)));
                        self.emit_instruction(Instruction::LoadConst {
                            dest: true_reg,
                            constant: true_constant,
                        });
                        self.emit_instruction(Instruction::Move {
                            dest: cmp_result_reg,
                            src: true_reg,
                        });
                    }
                }
            }

            // Emit conditional branch start
            let branch_name_id = self.program.add_string_ref(branch_name.clone());
            self.emit_instruction(Instruction::CondBranchStart {
                branch_name: branch_name_id,
                condition: cmp_result_reg,
                skip_target: 0, // Placeholder
            });

            // Enter a new lexical scope for this branch
            self.emit_instruction(Instruction::EnterScope {
                scope_type: crate::lang::bytecode::ScopeType::Flow,
            });

            // For Result.Ok/Result.Err arms, unwrap $val so the matched variable
            // refers to the payload, not the Result wrapper. This prevents
            // TemplateInterpolate from throwing on Result.Err values inside match
            // arms where the user explicitly handles the error case.
            let is_result_variant = matches!(
                (&arm.type_name, &arm.variant),
                (Some(type_name), Some(variant))
                if {
                    let (resolved, _) = self.resolve_type_path(type_name);
                    resolved == "::hot::type/Result"
                        && (variant == "Ok" || variant == "Err")
                }
            );

            if is_result_variant {
                let unwrapped_reg = self.allocate_register();
                self.emit_instruction(Instruction::ExtractInnerVal {
                    dest: unwrapped_reg,
                    src: value_reg,
                });

                // If there's an explicit binding, bind the unwrapped value
                if let Some(binding) = &arm.binding {
                    self.store_variable(binding, unwrapped_reg);
                }

                // If the match subject is a variable, shadow it with the unwrapped
                // value so that `${result}` in template literals uses the payload
                if let Some(ref var_name) = match_subject_var_name
                    && arm.binding.as_deref() != Some(var_name)
                {
                    self.store_variable(var_name, unwrapped_reg);
                }
            } else {
                // Non-Result arms: use the full value for bindings
                if let Some(binding) = &arm.binding {
                    self.store_variable(binding, value_reg);
                }
            }

            // Compile the body
            let body_result_reg = self.compile_value(&arm.body)?;

            // Exit the lexical scope
            self.emit_instruction(Instruction::ExitScope);

            // Store the branch result
            let branch_end_name_id = self.program.add_string_ref(branch_name.clone());
            self.emit_instruction(Instruction::CondBranchEnd {
                branch_name: branch_end_name_id,
                result: body_result_reg,
            });
        }

        // End the flow and collect results into result_reg
        self.emit_instruction(Instruction::EndFlow { dest: result_reg });

        Ok(result_reg)
    }

    /// Compile conditional flow (Cond or CondAll)
    fn compile_cond_flow(
        &mut self,
        flow: &Flow,
        short_circuit: bool,
    ) -> Result<RegisterId, String> {
        // Conditional flows work with condition => result pairs
        // Each expression should be a Value::Cond(condition, result) or a default result

        let result_reg = self.allocate_register();
        let mut has_default = false;
        let mut branch_results = Vec::new();

        // Allocate a unique id for this cond instance so its branch names don't
        // collide with sibling/nested cond flows after string interning.
        // See `next_cond_id` doc on `Compiler` for the original bug.
        let cond_id = self.next_cond_id;
        self.next_cond_id += 1;

        // Begin the flow context for proper short-circuiting
        let flow_type = if short_circuit {
            crate::lang::bytecode::FlowType::Cond
        } else {
            crate::lang::bytecode::FlowType::CondAll
        };

        // Map AST ResultModifier to bytecode FlowResultModifier
        // Default: cond -> One, cond-all -> Map
        let result_modifier = match flow.result_modifier {
            Some(crate::lang::ast::ResultModifier::Map) => {
                crate::lang::bytecode::FlowResultModifier::Map
            }
            Some(crate::lang::ast::ResultModifier::Vec) => {
                crate::lang::bytecode::FlowResultModifier::Vec
            }
            Some(crate::lang::ast::ResultModifier::One) => {
                crate::lang::bytecode::FlowResultModifier::One
            }
            None => {
                // Default based on flow type: cond -> One, cond-all -> Map
                if short_circuit {
                    crate::lang::bytecode::FlowResultModifier::One
                } else {
                    crate::lang::bytecode::FlowResultModifier::Map
                }
            }
        };

        self.emit_instruction(Instruction::BeginFlow {
            flow_type,
            result_modifier,
            source: self.source_to_location(&flow.src).map(Box::new),
        });

        // Initialize result register with null (in case no conditions match)
        let null_constant = self.program.add_constant(Constant::Val(Val::Null));
        self.emit_instruction(Instruction::LoadConst {
            dest: result_reg,
            constant: null_constant,
        });

        // TCO: Save tail position - cond branches are in tail position if the cond itself is
        let saved_tail_position = self.in_tail_position;

        for (i, expr) in flow.expressions.iter().enumerate() {
            match expr {
                Value::Cond(branch_name_opt, condition, result_flow) => {
                    // TCO: Condition evaluation is NOT in tail position
                    self.in_tail_position = false;
                    // Compile the condition
                    let condition_reg = self.compile_value(condition)?;

                    // Use provided branch label if available, otherwise generate.
                    // Suffix every name with `\0c<cond_id>` so identical labels in
                    // sibling/nested conds don't collide via string interning
                    // (the VM short-circuit logic compares ConstantIds). The
                    // NUL byte cannot appear in a user-supplied label, so the
                    // suffix is unambiguously strippable for user-facing names
                    // (see `vm::user_facing_branch_name`).
                    let branch_name = match branch_name_opt {
                        Some(label) => format!("{}\0c{}", label, cond_id),
                        None => format!("cond_branch_{}\0c{}", i, cond_id),
                    };

                    // Emit conditional branch start (using placeholder jump offset for now)
                    let branch_name_id = self.program.add_string_ref(branch_name.clone());
                    self.emit_instruction(Instruction::CondBranchStart {
                        branch_name: branch_name_id,
                        condition: condition_reg,
                        skip_target: 0, // Placeholder - would need proper jump calculation
                    });

                    // Enter a new lexical scope for this conditional branch
                    // This is crucial because the VM always executes all branches
                    self.emit_instruction(Instruction::EnterScope {
                        scope_type: crate::lang::bytecode::ScopeType::Flow,
                    });

                    // TCO: Branch result IS in tail position if the cond is in tail position
                    self.in_tail_position = saved_tail_position;

                    // Compile the result flow - ensure it's treated as a serial flow for variable assignments
                    let branch_result_reg = match result_flow.flow_type {
                        FlowType::Serial => {
                            // Already a serial flow, compile normally
                            self.compile_flow(result_flow)?
                        }
                        _ => {
                            // Convert to serial flow to ensure variable assignments work
                            tracing::trace!(
                                "Compiler: Converting cond branch to serial flow for proper variable assignment handling"
                            );
                            let serial_flow = Flow {
                                flow_type: FlowType::Serial,
                                expressions: result_flow.expressions.clone(),
                                result_modifier: result_flow.result_modifier.clone(),
                                src: result_flow.src.clone(),
                                aliases: result_flow.aliases.clone(),
                            };
                            self.compile_flow(&serial_flow)?
                        }
                    };

                    // Exit the lexical scope for this conditional branch
                    self.emit_instruction(Instruction::ExitScope);

                    // Store the branch result
                    let branch_end_name_id = self.program.add_string_ref(branch_name.clone());
                    self.emit_instruction(Instruction::CondBranchEnd {
                        branch_name: branch_end_name_id,
                        result: branch_result_reg,
                    });

                    branch_results.push((branch_name, branch_result_reg));

                    // For short-circuit (Cond), we would emit a jump to end after first match
                    // For now, we'll handle this in the VM
                }
                _ => {
                    // This might be a default case (=> { result }) or a standalone expression
                    // TCO: Default case is in tail position if the cond is in tail position
                    self.in_tail_position = saved_tail_position;

                    // For now, treat it as a default case
                    // IMPORTANT: Wrap default branch in CondBranchStart/CondBranchEnd with
                    // always-true condition so the VM can skip it when a previous branch matched.
                    // This is critical for TCO: otherwise TailCall in default branch executes
                    // even when a conditional branch already matched!
                    if !has_default {
                        // Suffix with `\0c<cond_id>` (see note on `next_cond_id`)
                        // so default arms in nested conds don't collide via string
                        // interning. The NUL separator is stripped before any
                        // user-facing exposure (see `vm::user_facing_branch_name`).
                        let default_branch_name = format!("cond_default\0c{}", cond_id);
                        let default_branch_name_id =
                            self.program.add_string_ref(default_branch_name.clone());

                        // Create an always-true condition (true literal)
                        let true_const = self.program.add_constant(Constant::Val(Val::Bool(true)));
                        let true_reg = self.allocate_register();
                        self.emit_instruction(Instruction::LoadConst {
                            dest: true_reg,
                            constant: true_const,
                        });

                        // Emit CondBranchStart with always-true condition
                        self.emit_instruction(Instruction::CondBranchStart {
                            branch_name: default_branch_name_id,
                            condition: true_reg,
                            skip_target: 0,
                        });

                        // Enter scope for the default branch
                        self.emit_instruction(Instruction::EnterScope {
                            scope_type: crate::lang::bytecode::ScopeType::Flow,
                        });

                        let default_reg = self.compile_value(expr)?;

                        // Exit scope
                        self.emit_instruction(Instruction::ExitScope);

                        // Emit CondBranchEnd to allow VM to skip when previous branch matched
                        self.emit_instruction(Instruction::CondBranchEnd {
                            branch_name: default_branch_name_id,
                            result: default_reg,
                        });

                        has_default = true;
                    }
                }
            }
        }

        // TCO: Restore tail position state
        self.in_tail_position = saved_tail_position;

        // End the flow context and collect the result
        self.emit_instruction(Instruction::EndFlow { dest: result_reg });

        tracing::trace!(
            "Compiler: Compiled {} flow with {} branches",
            if short_circuit { "Cond" } else { "CondAll" },
            branch_results.len()
        );

        Ok(result_reg)
    }

    /// Compile a match flow (match fn body containing MatchArm expressions)
    /// The first parameter of the enclosing function is used as the value to match
    fn compile_match_flow(
        &mut self,
        flow: &Flow,
        short_circuit: bool,
    ) -> Result<RegisterId, String> {
        // For match flows, we need to construct a MatchExpr from the MatchArm expressions
        // The value to match is the first parameter of the enclosing function

        // Get the first parameter name from the current function context.
        // This is used as the match subject AND as an implicit binding for each arm,
        // so that inside the branch body, the parameter name refers to the properly
        // resolved value (e.g., after EnsureResult evaluation for Result matching).
        let first_param_name = if let Some(first_param) = self.current_function_params.first() {
            first_param.clone()
        } else {
            return Err("Match flow requires at least one function parameter".to_string());
        };

        // Extract MatchArm expressions from the flow
        let arms: Vec<&crate::lang::ast::MatchArm> = flow
            .expressions
            .iter()
            .filter_map(|expr| match expr {
                Value::MatchArm(arm) => Some(arm.as_ref()),
                _ => None,
            })
            .collect();

        if arms.is_empty() {
            // No match arms, return null
            let reg = self.allocate_register();
            let constant_id = self.program.add_constant(Constant::Val(Val::Null));
            self.emit_instruction(Instruction::LoadConst {
                dest: reg,
                constant: constant_id,
            });
            return Ok(reg);
        }

        // Create a synthetic MatchExpr using the first parameter as the value to match
        let match_expr = crate::lang::ast::MatchExpr {
            value: Box::new(Value::Ref(Ref::Var(crate::lang::ast::VarRef {
                var: crate::lang::ast::Var {
                    sym: crate::lang::ast::Sym::String(first_param_name.clone()),
                    deep_set: None,
                    deep_path: None,
                    meta: None,
                    type_annotation: None,
                    src: None,
                },
                src: None,
            }))),
            arms: arms
                .into_iter()
                .map(|arm| crate::lang::ast::MatchArm {
                    type_name: arm.type_name.clone(),
                    variant: arm.variant.clone(),
                    value_literal: arm.value_literal.clone(),
                    // Use the first parameter name as an implicit binding for each arm
                    // so that inside the branch body, the parameter refers to the match
                    // subject value (after EnsureResult wrapping/thunk evaluation).
                    binding: arm
                        .binding
                        .clone()
                        .or_else(|| Some(first_param_name.clone())),
                    body: arm.body.clone(),
                    src: arm.src.clone(),
                })
                .collect(),
            match_all: !short_circuit,
            result_modifier: flow.result_modifier.clone(),
            src: flow.src.clone(),
        };

        self.compile_match_expression(&match_expr)
    }
